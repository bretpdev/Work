SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT DISTINCT
	LN60.LD_FOR_APL AS [ACT DT]
	,PD10.DF_SPE_ACC_ID AS [Account Number]
	,LN60.LN_SEQ AS [Loan Seq]
	,LN60.LD_FOR_BEG AS [Forb Begin Date]
	,LN60.LD_FOR_END AS [Forb End Date]
FROM
	UDW..LN60_BR_FOR_APV LN60
	INNER JOIN UDW..FB10_BR_FOR_REQ FB10
		ON LN60.BF_SSN = FB10.BF_SSN
		AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM
	INNER JOIN UDW..LN10_LON LN10
		ON LN60.BF_SSN = LN10.BF_SSN
		AND LN60.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON LN60.BF_SSN = PD10.DF_PRS_ID
WHERE
	LN10.LA_CUR_PRI > 0.00 --open
	AND LN10.LC_STA_LON10 = 'R' --released
	AND LN10.IC_LON_PGM IN ('SUBCNS','UNCNS','SUBSPC','UNSPC','PLUS','PLUSGB') --consolidation & plus loans
	AND LN60.LC_STA_LON60 = 'A'
	AND FB10.LC_FOR_STA = 'A'
	AND FB10.LC_STA_FOR10 = 'A'
	AND FB10.LC_FOR_TYP = '15'
	AND LN60.LD_FOR_BEG >= '2006-07-01'
ORDER BY
	PD10.DF_SPE_ACC_ID
	,LN60.LN_SEQ