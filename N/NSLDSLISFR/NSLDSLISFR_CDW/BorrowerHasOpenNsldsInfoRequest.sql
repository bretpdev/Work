CREATE PROCEDURE [nsldslisfr].[BorrowerHasOpenNsldsInfoRequest]
	@Ssn CHAR(9)
AS

SELECT
	CASE WHEN COUNT(1) > 0 THEN CAST(1 AS BIT) ELSE CAST(0 AS BIT) END AS HasOpenNsldsRequest
FROM
	CDW..GRRQ_NDS_PRS_REQ G
WHERE
	G.DD_LIS_REQ_BY_DPT >= DATEADD(DAY, -3, (CAST(GETDATE() AS DATE)))
	AND G.DF_PRS_ID = @Ssn

RETURN 0
