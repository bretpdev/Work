CREATE PROCEDURE [restapi].[GetDialerInfo]
	@AccountNumber VARCHAR(10)
AS
	SELECT TOP 1
		RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST)  AS FullName,
		RTRIM(PD30.DX_STR_ADR_1) AS Address1,
		RTRIM(PD30.DX_STR_ADR_2) AS Address2,
		RTRIM(PD30.DX_STR_ADR_3) AS Address3,
		RTRIM(PD30.DM_CT) AS City 
	FROM
		PD10_PRS_NME PD10
		INNER JOIN PD30_PRS_ADR PD30
			ON PD10.DF_PRS_ID = PD30.DF_PRS_ID
	WHERE
		PD10.DF_SPE_ACC_ID = @AccountNumber
