CREATE PROCEDURE [dbo].[GetOpenLoanBalance]
	@AccountIdentifier VARCHAR(10)
AS
	SELECT
		SUM(LN10.LA_CUR_PRI)
	FROM
		UDW..LN10_LON LN10
		INNER JOIN UDW..PD10_PRS_NME PD10
			ON PD10.DF_PRS_ID = LN10.BF_SSN
		LEFT JOIN UDW..RF10_RFR RF10
			ON RF10.BF_SSN = LN10.BF_SSN
	WHERE
		@AccountIdentifier IN (PD10.DF_SPE_ACC_ID, PD10.DF_PRS_ID, RF10.BF_RFR)
		AND LN10.LA_CUR_PRI > 0
		AND LN10.LC_STA_LON10 = 'R'