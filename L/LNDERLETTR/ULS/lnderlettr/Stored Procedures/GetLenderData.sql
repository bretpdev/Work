CREATE PROCEDURE [lnderlettr].[GetLenderData]
	@WF_ORG_LDR CHAR(8)
AS
SELECT DISTINCT
			RTRIM(LR25.IF_DOE_LDR) AS IF_DOE_LDR,
			RTRIM(LR10.IM_LDR_FUL) AS IM_LDR_FUL,
			RTRIM(LR25.IX_LDR_STR_ADR_1) AS IX_LDR_STR_ADR_1,
			RTRIM(LR25.IX_LDR_STR_ADR_2) AS IX_LDR_STR_ADR_2,
			RTRIM(LR25.IM_LDR_CT) AS IM_LDR_CT,
			RTRIM(LR25.IC_LDR_DOM_ST) AS IC_LDR_DOM_ST,
			RTRIM(LR25.IF_LDR_ZIP_CDE) AS IF_LDR_ZIP_CDE,
			RTRIM(LR25.IM_LDR_FGN_CNY) AS IM_LDR_FGN_CNY,
			RTRIM(LR25.II_LDR_VLD_ADR) AS II_LDR_VLD_ADR
	FROM
		UDW..LR25_LDR_DPT LR25
	INNER JOIN UDW..LR10_LDR_DMO LR10
		ON LR10.IF_DOE_LDR = LR25.IF_DOE_LDR
WHERE
	LR25.IF_DOE_LDR = @WF_ORG_LDR
	AND LR25.IC_LDR_DPT = '000'