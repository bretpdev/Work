CREATE PROCEDURE [clschllnfd].[GetLastPaymentDate]
	@BF_SSN CHAR(9),
	@LN_SEQ INT
AS
	SELECT
		 MAX(LD_FAT_EFF)
	FROM
		LN90_FIN_ATY
	WHERE
		BF_SSN = @BF_SSN
		AND LN_SEQ = @LN_SEQ
		AND LC_STA_LON90 = 'A'
		AND COALESCE(LC_FAT_REV_REA, ' ') = ' '
		AND PC_FAT_TYP = 10
		AND PC_FAT_SUB_TYP IN (10, 41)
RETURN 0