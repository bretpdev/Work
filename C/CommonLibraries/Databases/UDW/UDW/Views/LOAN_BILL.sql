
CREATE VIEW [dbo].[LOAN_BILL]
AS
SELECT DISTINCT A.DF_SPE_ACC_ID, A.LN_SEQ, ISNULL(A.BIL_MTD, ' ') AS BIL_MTD, A.PAID_AHEAD
FROM         dbo.BL10_Bill AS A INNER JOIN
                          (SELECT     DF_SPE_ACC_ID, LN_SEQ, CONVERT(VARCHAR, MAX(CAST(LD_BIL_DU_LON AS DATETIME)), 101) AS LD_BIL_DU_LON
                            FROM          dbo.BL10_Bill
                            GROUP BY DF_SPE_ACC_ID, LN_SEQ) AS B ON A.DF_SPE_ACC_ID = B.DF_SPE_ACC_ID AND A.LN_SEQ = B.LN_SEQ AND A.LD_BIL_DU_LON = B.LD_BIL_DU_LON


GO
