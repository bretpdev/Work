CREATE  PROCEDURE [dbo].[LT_US13B9CSG3_Fields]
	@BF_SSN char(9)
AS
BEGIN

	SELECT DISTINCT
		PD10.DM_PRS_1 PL1STNAMEE,
		PD10.DM_PRS_LST PLLSTNAMEE,
		PD30.DX_STR_ADR_1 PLSTREET1E,
		PD30.DX_STR_ADR_2 PLSTREET2E,
		PD30.DM_CT PLCITYE,
		PD30.DC_DOM_ST PLSTATEE,
		PD30.DF_ZIP_CDE PLZIPE,
		PD30.DM_FGN_CNY PLFRGNCNTE
	FROM 
		LN20_EDS LN20
		JOIN
		PD10_PRS_NME PD10 ON PD10.DF_PRS_ID = LN20.LF_EDS
		JOIN
		PD30_PRS_ADR PD30 ON PD30.DF_PRS_ID = LN20.LF_EDS
	WHERE 
		LN20.LC_EDS_TYP = 'M'
		AND
		LN20.BF_SSN = @BF_SSN

END