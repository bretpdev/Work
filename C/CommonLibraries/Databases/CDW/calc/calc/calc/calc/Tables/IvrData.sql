CREATE TABLE [dbo].[IvrData]
(
	[DF_SPE_ACC_ID] CHAR(10) NOT NULL PRIMARY KEY, 
    [BF_SSN] CHAR(9) NOT NULL, 
    [DM_PRS_1] VARCHAR(13) NOT NULL, 
    [DM_PRS_LST] VARCHAR(23) NOT NULL, 
	DM_PRS_MID VARCHAR(13) NOT NULL,
    [AccountHolderName]  AS (LTRIM(RTRIM([DM_PRS_1]))+ ' ' + LTRIM(RTRIM([DM_PRS_MID])) + ' ' + LTRIM(RTRIM([DM_PRS_LST]))), 
    [DD_BRT] DATE NOT NULL, 
    [DF_ZIP_CDE] VARCHAR(17) NOT NULL, 
    [DF_ZIP_CDE_IVR] AS (SUBSTRING([DF_ZIP_CDE],(1),(5))), 
    [LN_DELQ_MAX] INT NOT NULL, 
    [LA_CUR_PRI] DECIMAL(8, 2) NOT NULL, 
    [WA_TOT_BRI_OTS ] DECIMAL(12, 2) NOT NULL, 
    [TOT_DUE] DECIMAL(18, 2) NOT NULL, 
    [TOT_DUE_FEE] DECIMAL(18, 2) NOT NULL, 
    [LST_PMT_RCVD] DATE NOT NULL, 
    [LST_AMT_IVR] VARCHAR(8) NOT NULL, 
    [AddressSkip] BIT NOT NULL, 
    [PhoneSkip] BIT NOT NULL, 
    [Bankruptcy] BIT NOT NULL, 
    [DeliquencyGreaterThan60] AS CASE WHEN LN_DELQ_MAX > 60 THEN 1 ELSE 0 END, 
    [MultiDueDate] BIT NOT NULL, 
    [MonthlyPaymentAmount] VARCHAR(200) NOT NULL, 
    [Due Date] VARCHAR(50) NOT NULL
)
