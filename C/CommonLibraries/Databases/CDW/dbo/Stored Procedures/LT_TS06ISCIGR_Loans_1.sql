
CREATE PROCEDURE LT_TS06ISCIGR_Loans
	@AccountNumber char(10)
AS
BEGIN
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

	SET NOCOUNT ON;

	SELECT
		LN10.LA_LON_AMT_GTR [Total Principal Disbursed],
		DW01.WA_TOT_BRI_OTS [Total Outstanding Interest],
		LN72.LR_ITR [Interest Rate],
		LN10.LA_CUR_PRI + DW01.WA_TOT_BRI_OTS [Total Balance]
	FROM
		LN10_Loan LN10
		JOIN DW01_Loan DW01 ON LN10.DF_SPE_ACC_ID = DW01.DF_SPE_ACC_ID
		JOIN LN72_InterestRate LN72 ON LN10.DF_SPE_ACC_ID = LN72.DF_SPE_ACC_ID
	WHERE
		LN10.DF_SPE_ACC_ID = @AccountNumber

END

IF @@ROWCOUNT = 0 
	BEGIN
		RAISERROR('No data returned.  ([dbo].[LT_TS06ISCIGR_Loans])',11,2)
	END