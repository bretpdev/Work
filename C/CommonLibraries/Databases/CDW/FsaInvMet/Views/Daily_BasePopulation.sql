
CREATE VIEW [FsaInvMet].Daily_BasePopulation

AS

SELECT
	LN10.BF_SSN,
	LN10.LN_SEQ,
	LN10.LC_STA_LON10,
	LN10.LD_STA_LON10,
	LN10.LD_LON_EFF_ADD,
	LN10.LD_LON_ACL_ADD,
	LN10.LD_PIF_RPT,
	CASE 
		WHEN LN10.LA_CUR_PRI > 0 THEN LN10.LC_CAM_LON_STA
		ELSE '' 
	END AS LC_CAM_LON_STA, --UHEAA ONLY?
	PD32.DX_ADR_EML,
	CASE 
		WHEN LN10.IC_LON_PGM IN('DLPCNS','DLPLGB','DLPLUS','DLSCNS','DLSPCN','DLSSPL','DLSTFD','DLUCNS','DLUNST','DLUSPL','TEACH' ) THEN 'DL'
		WHEN LN10.IC_LON_PGM IN('CNSLDN','PLUS','PLUSGB','SLS','STFFRD','SUBCNS','SUBSPC','UNCNS','UNSPC','UNSTFD','FISL') THEN 'FL'
		ELSE ''
	END AS IC_LON_PGM,
	LN10.LA_CUR_PRI AS LA_OTS_PRI_ELG,
	COALESCE(DW01.WA_TOT_BRI_OTS,DW01.LA_NSI_OTS,0) AS WA_TOT_BRI_OTS,
	CASE 
		WHEN LN10.LA_CUR_PRI <= 0 THEN 0
		WHEN LN10.LC_STA_LON10 = 'D' THEN DATEDIFF(DAY,LN16.LD_DLQ_OCC, GETDATE()) /* get current day delinquency*/
		WHEN DW01.WC_DW_LON_STA IN ('04','05') THEN 0
		WHEN FORB.BF_SSN != '' THEN 0
        ELSE COALESCE(MAX(LN16.LN_DLQ_MAX) + 1,0)
	END AS LN_DLQ_MAX,
	CASE 
		WHEN FORB.BF_SSN != '' THEN '1' 
		ELSE '0' 
	END AS SPEC_FORB_IND,
	DW01.WC_DW_LON_STA,
	CASE 
		WHEN LN10.LC_STA_LON10 = 'D' AND LN10.LA_CUR_PRI = 0 THEN 2 
		WHEN LN10.LD_PIF_RPT IS NOT NULL THEN 1 ELSE 0 
	END AS ORD,
	CASE 
		WHEN LN80.BF_SSN IS NULL THEN 0 
		ELSE 1
	END AS BILL_SATISFIED,
	SegmentLogic.Segment,
	LN10.LF_LON_CUR_OWN
FROM 
	CDW..LN10_LON LN10
	LEFT JOIN CDW..DW01_DW_CLC_CLU DW01 
		ON LN10.BF_SSN = DW01.BF_SSN 
		AND LN10.LN_SEQ = DW01.LN_SEQ
	LEFT JOIN CDW..LN16_LON_DLQ_HST LN16
		ON LN10.BF_SSN = LN16.BF_SSN
		AND LN10.LN_SEQ = LN16.LN_SEQ
		AND LN16.LC_STA_LON16 = '1'
		AND LN16.LC_DLQ_TYP = 'P'
	LEFT JOIN 
	( -- email address
		SELECT
			*,
			ROW_NUMBER() OVER (PARTITION BY DF_PRS_ID ORDER BY PriorityNumber) [EmailPriority]
		FROM
		(
			SELECT
				PD32.DF_PRS_ID,
				PD32.DX_ADR_EML,
				CASE 
					WHEN DC_ADR_EML = 'H' THEN 1
					WHEN DC_ADR_EML = 'A' THEN 2
					WHEN DC_ADR_EML = 'W' THEN 3
				END [PriorityNumber]
			FROM
				PD32_PRS_ADR_EML PD32
			WHERE
				PD32.DI_VLD_ADR_EML = 'Y'
		) Email
	) PD32 
		ON PD32.DF_PRS_ID = LN10.BF_SSN 
		AND PD32.EmailPriority = 1 --highest priority email only
	LEFT JOIN
	( -- forbearance indicator
		SELECT 
			FB10.BF_SSN                                       
		FROM 
			CDW..FB10_BR_FOR_REQ FB10
			INNER JOIN CDW..LN60_BR_FOR_APV LN60 
				ON FB10.BF_SSN = LN60.BF_SSN 
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
		WHERE
			FB10.LC_FOR_STA = 'A'
			AND LN60.LC_STA_LON60 = 'A'
			AND FB10.LC_STA_FOR10 = 'A'      
			AND FB10.LC_FOR_TYP IN ('10','13','14','33','44')
			AND LN60.LD_FOR_END >= DATEADD(MONTH, DATEDIFF(MONTH, 0, GETDATE()), 0) -- first day of current month
			AND LN60.LD_FOR_APL <= GETDATE()
			AND LN60.LD_FOR_BEG <= GETDATE()
	) FORB 
		ON FORB.BF_SSN = LN10.BF_SSN
	LEFT JOIN CDW..LN80_LON_BIL_CRF LN80
		ON LN10.BF_SSN = LN80.BF_SSN
		AND LN10.LN_SEQ = LN80.LN_SEQ
		AND LN80.LD_BIL_DU_LON BETWEEN DATEADD(MONTH, DATEDIFF(MONTH, 0, GETDATE()), 0) /*first day of current month*/ AND DATEADD(MONTH, ((YEAR(DATEADD(MONTH, DATEDIFF(MONTH, 0, GETDATE()), 0)) - 1900) * 12) + MONTH(DATEADD(MONTH, DATEDIFF(MONTH, 0, GETDATE()), 0)), -1)/*last day current month*/
		AND LN80.LA_TOT_BIL_STS >= COALESCE(LN80.LA_BIL_CUR_DU,0)
		AND LN80.LC_STA_LON80 = 'A'
		AND LN80.LC_BIL_TYP_LON = 'P'
	LEFT JOIN
	( -- segment
		SELECT DISTINCT
			LN10.BF_SSN AS SSN,
			CASE 
				WHEN LN09.BF_SSN IS NOT NULL THEN 6
				WHEN LN10ConPlus.BF_SSN IS NOT NULL THEN 1
				WHEN LN10ConPlus.BF_SSN IS NULL AND SD10Max.LC_REA_SCL_SPR = '01' /*grauated*/
					AND COALESCE(SD10Max.LD_SCL_SPR, LN10DeferPlus.GradDeferDate, LN10GradPlus.GradRepayDate) > DATEADD(YEAR, -3, GETDATE()) THEN 2 /*less than 3 years ago*/
				WHEN LN10ConPlus.BF_SSN IS NULL
					AND SD10Max.LC_REA_SCL_SPR = '01' /*grauated*/
					AND COALESCE(SD10Max.LD_SCL_SPR, LN10DeferPlus.GradDeferDate, LN10GradPlus.GradRepayDate) <= DATEADD(YEAR, -3, GETDATE()) THEN 3 /*More than 3 years ago*/
				WHEN LN10ConPlus.BF_SSN IS NULL
					AND SD10Max.LC_REA_SCL_SPR != '01' /*didnt grauate*/
					AND SD10Max.LD_SCL_SPR > DATEADD(YEAR, -3, GETDATE())  THEN 4 /*Less than 3 years ago*/
				WHEN LN10ConPlus.BF_SSN IS NULL
					AND SD10Max.LC_REA_SCL_SPR != '01' /*didnt grauate*/
					AND SD10Max.LD_SCL_SPR <= DATEADD(YEAR, -3, GETDATE())  THEN 5 /*More than 3 years ago*/
				WHEN COALESCE(SD10Max.LD_SCL_SPR, LN10DeferPlus.GradDeferDate, LN10GradPlus.GradRepayDate) > DATEADD(YEAR, -3, GETDATE()) THEN 2 /*Less than 3 years ago*/
				WHEN COALESCE(SD10Max.LD_SCL_SPR, LN10DeferPlus.GradDeferDate, LN10GradPlus.GradRepayDate) <= DATEADD(YEAR, -3, GETDATE()) THEN 3 /*More than 3 years ago*/
				ELSE 7
			END [Segment]
		FROM 
			CDW..LN10_LON LN10
			LEFT JOIN
			( 
				SELECT DISTINCT
					BF_SSN
				FROM
					CDW..LN10_LON
				WHERE
					IC_LON_PGM IN ('DLPLUS', 'DLCNSL', 'DLPCNS', 'DLSCCN', 'DLSCNS', 'DLSCPL', 'DLSCSC', 'DLSCSL', 'DLSCST', 'DLSCUC', 'DLSCUN', 'DLSPCN', 'DLSSPL', 'DLUCNS', 'DLUSPL', 'DSCON', 'DUCON', 'DLSCPG', 'DLPLGB')
			) LN10ConPlus 
				ON LN10ConPlus.BF_SSN = LN10.BF_SSN
			LEFT JOIN
			(
				SELECT DISTINCT
					LN10.BF_SSN,
					DATEADD(DAY, -180, MAX(LN15.LD_DSB)) AS GradRepayDate
				FROM
					CDW..LN10_LON LN10
					INNER JOIN CDW..LN15_DSB LN15 
						ON LN15.BF_SSN = LN10.BF_SSN 
						AND LN15.LN_SEQ = LN10.LN_SEQ
				WHERE
					LN10.IC_LON_PGM IN ('DGPLUS','DLPLGB')
					AND LN15.LC_STA_LON15 = '1'
					AND LN15.LD_DSB <= GETDATE()
				GROUP BY
					LN10.BF_SSN
			) LN10GradPlus 
				ON LN10GradPlus.BF_SSN = LN10.BF_SSN
			LEFT JOIN
			(
				SELECT DISTINCT
					LN10.BF_SSN,
					DATEADD(DAY, -180, MAX(LN50.LD_DFR_END)) AS GradDeferDate
				FROM
					CDW..LN10_LON LN10
					INNER JOIN CDW..LN50_BR_DFR_APV LN50 
						ON LN50.BF_SSN = LN10.BF_SSN 
						AND LN50.LN_SEQ = LN10.LN_SEQ
					INNER JOIN CDW..DF10_BR_DFR_REQ DF10 
						ON DF10.BF_SSN = LN50.BF_SSN 
						AND DF10.LF_DFR_CTL_NUM = LN50.LF_DFR_CTL_NUM
				WHERE
					LN10.IC_LON_PGM IN ('DGPLUS','DLPLGB')
					AND LN50.LC_STA_LON50 = 'A'
					AND DF10.LC_DFR_TYP IN ('15','16','18')
					AND DF10.LC_DFR_STA = 'A'
					AND DF10.LC_STA_DFR10 = 'A'
					AND LN50.LD_DFR_END <= GETDATE()
				GROUP BY
					LN10.BF_SSN
			) LN10DeferPlus 
				ON LN10DeferPlus.BF_SSN = LN10.BF_SSN
			LEFT JOIN 
			(	
				SELECT DISTINCT
					LF_STU_SSN,
					MAX(LN_STU_SPR_SEQ) AS LN_STU_SPR_SEQ
				FROM
					CDW..SD10_STU_SPR
				WHERE
					LC_STA_STU10 = 'A'
				GROUP BY 
					LF_STU_SSN
			) SD10SEQ
				ON SD10SEQ.LF_STU_SSN = LN10.BF_SSN
			LEFT JOIN 
			(	
				SELECT DISTINCT
					LF_STU_SSN,
					LN_STU_SPR_SEQ,
					LC_REA_SCL_SPR,
					LD_SCL_SPR
				FROM
					CDW..SD10_STU_SPR
				WHERE
					LC_STA_STU10 = 'A'
					AND LD_SCL_SPR <= GETDATE()			
			) SD10Max
				ON SD10Max.LF_STU_SSN = LN10.BF_SSN
				AND SD10MAX.LN_STU_SPR_SEQ = SD10SEQ.LN_STU_SPR_SEQ
			LEFT JOIN
			(
				SELECT DISTINCT
					BF_SSN
				FROM 
					CDW..LN09_RPD_PIO_CVN
				WHERE
					LD_LON_RHB_PCV IS NOT NULL OR COALESCE(IF_LON_SRV_DFL_LON,'                ') != '                '
			) LN09 
				ON LN09.BF_SSN = LN10.BF_SSN
	) SegmentLogic 
		ON SegmentLogic.SSN = LN10.BF_SSN 
WHERE 
	LN10.LC_STA_LON10 IN ('R','D','L')
	AND LN10.LD_LON_ACL_ADD <= GETDATE()
	AND DW01.WC_DW_LON_STA NOT IN ('06','88','98') --These are considered status errors and output to an error file in the sas
GROUP BY 
	LN10.BF_SSN,
	LN10.LN_SEQ,
	LN10.LC_STA_LON10,
	LN10.LD_STA_LON10,
	LN10.LD_LON_EFF_ADD,
	LN10.LD_LON_ACL_ADD,
	LN10.LD_PIF_RPT,
	LN10.LC_CAM_LON_STA,
	PD32.DX_ADR_EML,
	CASE 
		WHEN LN10.IC_LON_PGM IN('DLPCNS','DLPLGB','DLPLUS','DLSCNS','DLSPCN','DLSSPL','DLSTFD','DLUCNS','DLUNST','DLUSPL','TEACH') THEN 'DL'
		WHEN LN10.IC_LON_PGM IN('CNSLDN','PLUS','PLUSGB','SLS','STFFRD','SUBCNS','SUBSPC','UNCNS','UNSPC','UNSTFD','FISL') THEN 'FL'
        ELSE ''
	END, 
	LN10.LA_CUR_PRI,
	COALESCE(DW01.WA_TOT_BRI_OTS, DW01.LA_NSI_OTS, 0),
	LN10.LC_STA_LON10,
	LN10.LC_SST_LON10,
	LN16.LD_DLQ_OCC,
	LN10.LA_CUR_PRI,
	DW01.WC_DW_LON_STA,
	FORB.BF_SSN,
	CASE WHEN LN80.BF_SSN IS NULL 
		THEN 0
        ELSE 1
	END,
	SegmentLogic.SEGMENT, 
	LN10.LF_LON_CUR_OWN