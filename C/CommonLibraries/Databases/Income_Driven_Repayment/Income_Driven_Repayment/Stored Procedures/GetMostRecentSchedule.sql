CREATE PROCEDURE [dbo].[GetMostRecentSchedule]
	@SSN CHAR(9),
	@TESTMODE BIT
AS
	DECLARE @QUERY VARCHAR(MAX), @TSQL VARCHAR(MAX)
	IF @TESTMODE = 0
		SELECT @TSQL = 'SELECT * FROM OPENQUERY(LEGEND_TEST_VUK1,'
	ELSE
		SELECT @TSQL = 'SELECT * FROM OPENQUERY(LEGEND,'
	  SELECT  @QUERY = @TSQL + 
	  '''
		SELECT 
			COUNT(*)
		FROM 
			PKUB.LN65_LON_RPS LN65 
			INNER JOIN
			(
				SELECT
					LN65.BF_SSN,
					LN65.LN_SEQ,
					MAX(LN65.LN_RPS_SEQ) AS LN_RPS_SEQ
				FROM
					PKUB.LN65_LON_RPS LN65
					INNER JOIN PKUB.LN10_LON LN10
						ON LN10.BF_SSN = LN65.BF_SSN
						AND LN10.LN_SEQ = LN65.LN_SEQ
						AND LN10.LC_STA_LON10 = ''''R''''
						AND LN10.LA_CUR_PRI > 0
				WHERE 
					LN65.BF_SSN = ''''' + @SSN + '''''
				GROUP BY
					LN65.BF_SSN,
					LN65.LN_SEQ
			)MAX_SCHEDULE
				ON MAX_SCHEDULE.BF_SSN = LN65.BF_SSN
				AND MAX_SCHEDULE.LN_SEQ = LN65.LN_SEQ
				AND MAX_SCHEDULE.LN_RPS_SEQ = LN65.LN_RPS_SEQ
		WHERE
			LN65.LC_TYP_SCH_DIS = ''''I5''''
	   '')'
      EXEC (@QUERY)

RETURN 0
