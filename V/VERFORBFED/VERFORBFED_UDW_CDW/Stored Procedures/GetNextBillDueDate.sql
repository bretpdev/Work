CREATE PROCEDURE [verforbfed].[GetNextBillDueDate]
	@AccountNumber CHAR(10)
AS
BEGIN
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED	
	SELECT 
		MAX(CAST(LN80.LD_BIL_DU_LON AS DATETIME))
	FROM
		PD10_PRS_NME PD10
		INNER JOIN [dbo].[LN80_LON_BIL_CRF] LN80 on LN80.BF_SSN = PD10.DF_PRS_ID
		INNER JOIN [dbo].DW01_DW_CLC_CLU DW01 ON DW01.BF_SSN = PD10.DF_PRS_ID AND DW01.LN_SEQ = LN80.LN_SEQ                                     
	WHERE 
		PD10.DF_SPE_ACC_ID = @AccountNumber
		AND
		LN80.LC_STA_LON80 = 'A'
		AND
		DW01.WC_DW_LON_STA in ('03', '14')

END
