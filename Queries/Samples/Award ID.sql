		SELECT
			LN10.BF_SSN
			,CONCAT(FS10.LF_FED_AWD, FORMAT (FS10.LN_FED_AWD_SEQ, '000')) AS AWARD_ID
			,LN09.IF_LON_SRV_DFL_LON
		FROM
			CDW..LN10_LON LN10
			JOIN CDW..FS10_DL_LON FS10
				ON LN10.BF_SSN = FS10.BF_SSN
				AND LN10.LN_SEQ = FS10.LN_SEQ
			JOIN CDW..LN09_RPD_PIO_CVN LN09
				ON LN10.BF_SSN = LN09.BF_SSN
				AND LN10.LN_SEQ = LN09.LN_SEQ
		WHERE
			LN10.LC_STA_LON10 = 'L'
		order by BF_SSN, AWARD_ID