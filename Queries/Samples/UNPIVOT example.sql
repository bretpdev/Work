DECLARE @BF_SSN CHAR(9) = ''
SELECT 
	E.BF_SSN,
	IN01.*
FROM
	(	
		SELECT DISTINCT
			E.BF_SSN, 
			E.Employer
		FROM 
			ODW..BR02_BR_EMP 
			UNPIVOT 
			(
				Employer FOR EmployerFieldName IN (BF_EMP_ID_1,BF_EMP_ID_2,BF_EMP_ID_3,BF_EMP_ID_4,BF_EMP_ID_5,BF_EMP_ID_6,BF_EMP_ID_7,BF_EMP_ID_8,BF_EMP_ID_9,BF_EMP_ID_10,BF_EMP_ID_11,BF_EMP_ID_12)
			) E
		WHERE
			LTRIM(RTRIM(Employer)) != ''
			AND BF_SSN = @BF_SSN
	) E
	INNER JOIN ODW..IN01_LGS_IDM_MST IN01
		ON E.Employer = IN01.IF_IST
ORDER BY
	E.BF_SSN