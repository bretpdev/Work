SELECT
	WQ21.WF_USR_ASN_TSK AS [User],
	PD10.DF_SPE_ACC_ID AS [Account Number],
	CAST (WQ21.WF_LST_DTS_WQ20 AS DATE) AS [Date Completed]
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..WQ21_TSK_QUE_HST WQ21
		ON PD10.DF_PRS_ID = WQ21.BF_SSN
WHERE
	WF_QUE = 'DR'
	AND WQ21.WC_STA_WQUE20 IN ('C','X')
	AND CAST (WQ21.WF_LST_DTS_WQ20 AS DATE) = CAST(DATEADD(DAY, -1, GETDATE()) AS DATE)
ORDER BY
	WF_USR_ASN_TSK