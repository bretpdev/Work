SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	WQ21.WF_QUE AS [QUEUE],
	WQ21.WF_SUB_QUE AS SUB_QUEUE,
	WQ21.WF_CRT_DTS_WQ21 AS TASK_CLOSED_DATE,
	WQ21.WF_CRT_DTS_WQ20 AS TASK_CREATE_DATE,
	WQ21.WF_USR_ASN_TSK AS USER_COMPLETED_TASK
FROM
	UDW..WQ21_TSK_QUE_HST WQ21
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ21.BF_SSN
WHERE 
	WF_QUE IN ('VB','VR','SF','S4')
	AND WC_STA_WQUE20 IN ('X','C')
	AND WQ21.WF_CRT_DTS_WQ21 BETWEEN '10/01/2019' AND '12/13/2019'
ORDER BY
	[QUEUE],
	TASK_CREATE_DATE