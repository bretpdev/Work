CREATE TABLE #Data (LA_CUR_PRI NUMERIC(X,X), LA_NSI_OTS NUMERIC(X,X), BF_SSN CHAR(X), LN_SEQ SMALLINT, Region VARCHAR(X))
INSERT INTO #Data
SELECT 
	*, 
	'PKUS' 
FROM OPENQUERY(LEGEND, 
'
SELECT 
	LA_CUR_PRI,
	LA_NSI_OTS, 
	BF_SSN, 
	LN_SEQ 
FROM 
	PKUS.LNXX_LON 
WHERE 
	LC_STA_LONXX NOT IN(''P'',''I'',''J'')
	AND 
	(
		LA_CUR_PRI != X.XX
		OR LA_NSI_OTS != X.XX
	)
')


SELECT 
	DX.*,
	D.* 
FROM 
	AuditCDW..LNXX_LON_JanXXXX DX 
	LEFT JOIN #Data D 
		ON D.BF_SSN = DX.BF_SSN 
		AND D.LN_SEQ = DX.LN_SEQ
		AND D.Region = 'PKUS'
		AND
		(
			D.LA_CUR_PRI != DX.LA_CUR_PRI
			OR D.LA_NSI_OTS != DX.LA_NSI_OTS
		)
WHERE
	DX.LC_STA_LONXX NOT IN('P','I','J')
	AND 
	(
		DX.LA_CUR_PRI != X.XX
		OR DX.LA_NSI_OTS != X.XX
	)
	