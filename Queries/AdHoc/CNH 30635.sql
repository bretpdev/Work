SELECT * FROM OPENQUERY(LEGEND,'
	SELECT DISTINCT
		PDXX.DF_SPE_ACC_ID		AS ACCT_#
		,LNXX.LN_SEQ			AS LOAN_SEQ
		,LNXX.LC_STA_LONXX		AS LNXX_STATUS
		,LNXX.LA_CUR_PRI		AS CURRENT_PRIN
		,LNXX.LC_TYP_SCH_DIS	AS REPAYMENT_TYPE
		,LNXX.LD_CRT_LONXX		AS REPAYMENT_START_DATE 
		,LNXX.LC_STA_LONXX		AS REPAYMENT_STATUS
		,LNXX.IC_LON_PGM
	FROM
		PKUB.LNXX_LON LNXX
		INNER JOIN PKUB.PDXX_PRS_NME PDXX
			ON LNXX.BF_SSN = PDXX.DF_PRS_ID
		INNER JOIN PKUB.LNXX_LON_RPS LNXX
			ON LNXX.BF_SSN = LNXX.BF_SSN
			AND LNXX.LN_SEQ = LNXX.LN_SEQ
	WHERE
		LNXX.LC_STA_LONXX = ''R''
		AND LNXX.LA_CUR_PRI > X 
		AND LNXX.LC_TYP_SCH_DIS IN (''FS'',''FG'')
		AND LNXX.LC_STA_LONXX = ''A''

');