SELECT
	--AY10.BF_SSN,
	--LN10.LA_CUR_PRI
	COUNT(*) AS BORROWER_COUNT,
	SUM(LN10.LA_CUR_PRI) AS TOTAL_PRINCIPAL_BALANCE
FROM
	UDW..AY10_BR_LON_ATY AY10
	INNER JOIN
	(
		SELECT	
			BF_SSN,
			SUM(LA_CUR_PRI) AS LA_CUR_PRI
		FROM
			UDW..LN10_LON
		GROUP BY
			BF_SSN
	) LN10
		ON LN10.BF_SSN = AY10.BF_SSN
WHERE
	PF_REQ_ACT = 'T393S'
	AND LC_STA_ACTY10 = 'A'
	AND LD_ATY_REQ_RCV >= '04-01-2020'