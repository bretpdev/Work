SELECT DISTINCT
	PD10.DF_PRS_ID,
	PD10.DF_SPE_ACC_ID,
	LN10.LF_LON_CUR_OWN,
	LN10.LN_SEQ,
	MAX(AY101.LD_ATY_REQ_RCV) OVER (PARTITION BY WQ20.BF_SSN) AS IDTFN,
	MAX(AY102.LD_ATY_REQ_RCV) OVER (PARTITION BY WQ20.BF_SSN) AS IDTRV
FROM
	UDW..WQ20_TSK_QUE WQ20
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ20.BF_SSN
	INNER JOIN UDW..LN10_LON LN10	
		ON LN10.BF_SSN = WQ20.BF_SSN
	LEFT JOIN UDW..AY10_BR_LON_ATY AY101
		ON AY101.BF_SSN = WQ20.BF_SSN
		AND AY101.PF_REQ_ACT = 'IDTFN'
	LEFT JOIN UDW..AY10_BR_LON_ATY AY102
		ON AY102.BF_SSN = WQ20.BF_SSN
		AND AY102.PF_REQ_ACT = 'IDTRV'
WHERE
	WF_QUE = 'LH' 
	AND WF_SUB_QUE = '01'