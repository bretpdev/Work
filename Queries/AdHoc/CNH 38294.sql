SELECT DISTINCT
	LTRIM(RTRIM(PDXX.DM_PRS_X)) AS [First Name],
	LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS [Last Name],
	LNXX.BF_SSN,
	AYXX.PF_REQ_ACT
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	LEFT JOIN
	(
		SELECT DISTINCT
			BF_SSN,
			PF_REQ_ACT
		FROM
			CDW..AYXX_BR_LON_ATY AYXX
		WHERE
			PF_REQ_ACT = 'DIFCR'
			AND LC_STA_ACTYXX = 'A'
			AND CAST(LD_ATY_REQ_RCV AS DATE) BETWEEN 'XXXX-XX-XX' AND 'XXXX-XX-XX'

	) AYXX
		ON AYXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	LNXX.LF_DOE_SCL_ORG IN ('XXXXXXXX') 


