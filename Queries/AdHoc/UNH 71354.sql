SELECT DISTINCT
	PD10.DF_SPE_ACC_ID
FROM
	UDW..WQ20_TSK_QUE WQ20
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ20.BF_SSN
	INNER JOIN 
	(
		SELECT
			BF_SSN,
			SUM(LA_CUR_PRI) AS BAL
		FROM
			UDW..LN10_LON
		GROUP BY
			BF_SSN
		hAVING SUM(LA_CUR_PRI) <= 0
	) LN10
		ON LN10.BF_SSN = WQ20.BF_SSN
WHERE
	WQ20.WF_QUE = 'SZ'
	AND WQ20.WF_SUB_QUE = '01'