SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	RTRIM(PDXX.DM_PRS_X) + ' ' + RTRIM(PDXX.DM_PRS_LST) AS [NAME],
	PDXX.DD_DSA_STA,
	PDXX.DD_DSA_APV,
	PDXX.DI_DSA_VET,
	pdXX.DI_DSA_VET_ADM_MCH,
	PDXX.DD_DSA_STA,
	PDXX.DX_PRS_DSA_TPD_REA
FROM 
	CDW..PDXX_GTR_DSA PDXX
	INNER JOIN CDW..PDXX_PRS_DSA PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
	INNER JOIN CDW..LNXX_LON LNXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	INNER JOIN CDW..LNXX_DSB LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
WHERE
	PDXX.DC_DSA_STA = 'XX'
	AND LNXX.LA_CUR_PRI > X
	AND LNXX.LC_STA_LONXX = 'R'
	AND LNXX.LD_DSB <= PDXX.DD_DSA_APV
	AND LNXX.LA_DSB - ISNULL(LNXX.LA_DSB_CAN,X) != X
	AND PDXX.DX_PRS_DSA_TPD_REA = 'APPAPPR'