SELECT DISTINCT
	WQXX.BF_SSN AS SSN,
	LTRIM(RTRIM(PDXX.DM_PRS_X)) + ' ' + LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS [NAME],
	CAST(WF_CRT_DTS_WQXX AS DATE) AS PROCESSED_DATE,
	WF_USR_ASN_TSK AS PROCESSOR_UT#
FROM
	CDW..WQXX_TSK_QUE_HST WQXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = WQXX.BF_SSN
WHERE
	WF_QUE = 'SX'
	AND WC_STA_WQUEXX = 'C'
	--AND CAST(WF_CRT_DTS_WQXX AS DATE) >= 'XX/XX/XXXX' --original request wanted from X/X/XXXX
	AND CAST(WF_CRT_DTS_WQXX AS DATE) BETWEEN CONVERT(DATE,'XXXXXXXX') AND CONVERT(DATE,'XXXXXXXX') --send BU missing borrowers from this date
ORDER BY
	PROCESSED_DATE
;