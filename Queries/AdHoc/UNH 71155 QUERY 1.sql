SELECT DISTINCT
	PD10.DF_SPE_ACC_ID as [Account Number],
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [Name],
	LK10.PX_DSC_MDM as [Current Status],
	CONVERT(VARCHAR(10), LN16.LD_DLQ_OCC, 101) as [Delinquency Date],
	ln16.LN_DLQ_MAX as [Days Delinquent]
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = LN10.BF_SSN
		AND DW01.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..LK10_LS_CDE_LKP LK10
		ON LK10.PM_ATR = 'WC-DW-LON-STA'
		AND LK10.PX_ATR_VAL = DW01.WC_DW_LON_STA
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	INNER JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = LN10.BF_SSN
		AND LN16.LN_SEQ = LN10.LN_SEQ
		AND LN16.LC_STA_LON16 = '1'
		AND DATEADD(DAY,-5,CAST(GETDATE() AS DATE)) <= LN16.LD_DLQ_MAX
	LEFT JOIN
	(
		SELECT 
			FB10.LC_FOR_TYP,
			LN60.LD_FOR_BEG,
			LN60.LD_FOR_END,
			FB10.LD_CRT_REQ_FOR,
			FB10.LF_LST_DTS_FB10,
			FB10.LC_FOR_STA,
			LN60.BF_SSN,
			LN60.LN_SEQ
		FROM
			UDW..FB10_BR_FOR_REQ FB10
			INNER JOIN UDW..LN60_BR_FOR_APV LN60
				ON LN60.BF_SSN = FB10.BF_SSN
				AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM
				
		WHERE
			LN60.LC_STA_LON60 = 'A'
			AND FB10.LC_STA_FOR10 = 'A'
			AND FB10.LC_FOR_STA = 'A' 
			AND LN60.LC_FOR_RSP != '003'
			AND FB10.LC_FOR_TYP = '40'
			AND LN60.LD_FOR_BEG > '03/13/2020'
	) Forb
		ON Forb.BF_SSN = LN10.BF_SSN
		AND Forb.LN_SEQ = LN10.LN_SEQ
WHERE 
	IF_GTR IN 
	(
	'000706', 
	'000725', 
	'000729', 
	'000740', 
	'000751', 
	'000927', 
	'000951'	 
	)
	AND LA_CUR_PRI > 0
	AND LC_STA_LON10 = 'R'
	AND FORB.BF_SSN IS NULL