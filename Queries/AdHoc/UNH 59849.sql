SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	LN10.LA_CUR_PRI,
	(WF_QUE +'-'+WF_SUB_QUE) AS [QUEUE]
FROM 
	CDW..WQ20_TSK_QUE WQ20
	INNER JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = WQ20.BF_SSN
	INNER JOIN 
	(
		SELECT	
			BF_SSN,
			SUM(LA_CUR_PRI) AS LA_CUR_PRI
		FROM
			UDW..LN10_LON
		GROUP BY
			BF_SSN
	) LN10
		ON LN10.BF_SSN = LN16.BF_SSN
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
WHERE
	WC_STA_WQUE20 NOT IN ('X','C')
	AND
	(
		(WF_QUE = 'VB' AND WF_SUB_QUE = 'FB')
		OR
		(WF_QUE = 'VR' AND WF_SUB_QUE = 'FB')
		OR
		(WF_QUE = 'SF' AND WF_SUB_QUE = '01')
		OR
		(WF_QUE = '2A' AND WF_SUB_QUE = '01')
	)
	
