SELECT
	PDXX.DF_PRS_ID AS SSN,
	PDXX.DM_PRS_X AS FirstName,
	PDXX.DM_PRS_LST AS LastName,
	AYXX.*
FROM
	AYXX_BR_LON_ATY AYXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON AYXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	AYXX.PF_REQ_ACT = 'DIFCR'


SELECT
	PDXX.DF_PRS_ID AS SSN,
	PDXX.DM_PRS_X AS FirstName,
	PDXX.DM_PRS_LST AS LastName,
	WQXX.*
FROM
	WQXX_TSK_QUE WQXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON WQXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	WQXX.PF_REQ_ACT = 'DIFCR'


SELECT
	PDXX.DF_PRS_ID AS SSN,
