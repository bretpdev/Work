	PDXX.DM_PRS_LST AS LastName,
	WQXX.*
FROM
	WQXX_TSK_QUE_HST WQXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON WQXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	WQXX.PF_REQ_ACT = 'DIFCR'
