SELECT distinct
	LTRIM(RTRIM(PD10.DM_PRS_1)) + ' ' + LTRIM(RTRIM(PD10.DM_PRS_LST)) as Name,
	DF_SPE_ACC_ID AS AccountNumber,
	LN50.LD_DFR_BEG AS DefermentBegin,
	LN50.LD_DFR_END AS DefermentEnd,
	WQ20.WD_ACT_REQ AS AppRecDate,
	ln50.LD_DFR_APL AS ProcessedDate,
	LN10.LA_CUR_PRI AS Balance
FROM
	UDW..DF10_BR_DFR_REQ DF10
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON DF10.BF_SSN = LN50.BF_SSN
		AND DF10.LF_DFR_CTL_NUM = LN50.LF_DFR_CTL_NUM
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DF10.BF_SSN
	INNER JOIN 
	(
		SELECT
			BF_SSN,
			SUM(LA_CUR_PRI) AS LA_CUR_PRI
		FROM
			UDW..LN10_LON
		GROUP BY
			BF_SSN
	) LN10
		ON LN10.BF_SSN = DF10.BF_SSN
	LEFT JOIN
	(
		SELECT
			BF_SSN,
			MAX(WD_ACT_REQ) AS WD_ACT_REQ
		FROM
			UDW..WQ20_TSK_QUE
		WHERE
			WF_QUE = 'S4'
		GROUP BY
			BF_SSN
	)WQ20
		ON WQ20.BF_SSN = DF10.BF_SSN
WHERE
	DF10.LC_DFR_TYP = '38'
	AND DF10.LC_DFR_STA = 'A'
	AND LN50.LC_STA_LON50 = 'A'
	AND DF10.LC_STA_DFR10 = 'A'
	AND 
		(
			YEAR(LN50.LD_DFR_APL) = 2018
			--OR
			--YEAR(LN50.LD_DFR_END) = 2018
		)