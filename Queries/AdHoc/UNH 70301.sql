USE UDW
GO

SELECT
	PD10.DF_SPE_ACC_ID AS [ACCOUNT NUMBER],
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [NAME],
	LN10.IC_LON_PGM AS [LOAN CODE],
	LN10.LN_SEQ AS [LOAN SEQUENCE],
	LN10.LD_LON_1_DSB AS [1ST DSB DATE],
	LN10.LA_CUR_PRI AS [BALANCE],
	RS.LC_TYP_SCH_DIS AS [REPAYMENT PLAN CODE],
	LN10.LD_LON_EFF_ADD AS [LOAN ADD DATE]
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW.CALC.RepaymentSchedules RS
		ON RS.BF_SSN = LN10.BF_SSN
		AND RS.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
WHERE 
	LN10.IC_LON_PGM IN ('CNSLDN', 'SUBCNS', 'UNCNS', 'SPCNSL', 'SUBSPC', 'UNSPC')
	AND LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'
	AND RS.LC_TYP_SCH_DIS IN ('IB','IL')
	AND RS.CurrentGradation = 1