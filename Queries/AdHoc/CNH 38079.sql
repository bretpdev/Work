SELECT
	PDXX.DF_SPE_ACC_ID,
	AYXX.PF_REQ_ACT,
	CONVERT(VARCHAR(XX), AYXX.LD_ATY_REQ_RCV, XXX) AS LD_ATY_REQ_RCV
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..BRXX_BR_EFT BRXX
		ON BRXX.BF_SSN = PDXX.DF_PRS_ID
	INNER JOIN CDW..LNXX_EFT_TO_LON LNXX
		ON BRXX.BF_SSN = LNXX.BF_SSN
		AND BRXX.BN_EFT_SEQ = LNXX.BN_EFT_SEQ
	LEFT JOIN
	(
		SELECT
			*
		FROM
			CDW..AYXX_BR_LON_ATY AYXX
		WHERE
			AYXX.PF_REQ_ACT = 'OLACH'
			AND AYXX.LC_STA_ACTYXX = 'A'
	) AYXX
		ON AYXX.BF_SSN = PDXX.DF_PRS_ID

WHERE
	BC_EFT_STA = 'A'
	AND LNXX.LD_EFT_EFF_BEG IS NULL