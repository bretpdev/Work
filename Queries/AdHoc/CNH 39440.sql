SELECT DISTINCT
	PDXX.DF_PRS_ID AS SSN,
	LTRIM(RTRIM(PDXX.DM_PRS_X)) + ' ' + LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS [BORROWER NAME],
	D.AWARD_ID,
	CASE 
		WHEN ISNULL(LNXX.LA_LON_AMT_GTR,X) = X AND LA_CUR_PRI = X THEN 'Loan Canceled'
		ELSE LKXX.PX_DSC_MDM
	END AS [STATUS],
	ISNULL(CASE
		WHEN TRANSFERS.COMMENT LIKE ('%SPLIT LOAN%') THEN 'SPLIT LOAN'
		ELSE TRANSFERS.TRANSFER_TYPE 
	END,'') AS TRANSFER_TYPE,
	ISNULL(REPAYMENT.LC_TYP_SCH_DIS,'') AS [SCHEDULE TYPE],
	ISNULL(CAST(REPAYMENT.LA_RPS_ISL AS VARCHAR(XX)),'') AS [PAYMENT AMOUNT]
FROM
	@DATA D
	INNER JOIN cdw..FSXX_DL_LON	FSXX
		ON FSXX.BF_SSN = D.BF_SSN
		AND (FSXX.LF_FED_AWD + RIGHT('XX' + CAST(FSXX.LN_FED_AWD_SEQ AS VARCHAR(X)), X)) = D.AWARD_ID
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = D.BF_SSN
	INNER JOIN CDW..DWXX_DW_CLC_CLU DWXX
		ON DWXX.BF_SSN = PDXX.DF_PRS_ID
		AND DWXX.LN_SEQ = FSXX.LN_SEQ
	INNER JOIN CDW..LNXX_LON LNXX
		ON LNXX.BF_SSN = DWXX.BF_SSN
		AND LNXX.LN_SEQ = DWXX.LN_SEQ
	INNER JOIN CDW..LKXX_LS_CDE_LKP LKXX
		ON LKXX.PM_ATR = 'WC-DW-LON-STA'
		AND LKXX.PX_ATR_VAL = DWXX.WC_DW_LON_STA
	LEFT JOIN
	(
		SELECT
			BF_SSN,
			LN_SEQ,
			LC_TYP_SCH_DIS,
			LA_RPS_ISL
		FROM
			CDW.calc.RepaymentSchedules RS
		WHERE
			CurrentGradation = X
	) REPAYMENT
		ON REPAYMENT.BF_SSN = D.BF_SSN
		AND REPAYMENT.LN_SEQ = DWXX.LN_SEQ
	LEFT JOIN
	(
		SELECT DISTINCT	
			LNXX.BF_SSN,
			LNXX.LN_SEQ,
			CASE 
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'PSLF'
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'TPD'
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'DMCS'
				ELSE ''
			END AS TRANSFER_TYPE,
			AXDCV.LX_ATY AS COMMENT
		FROM
			@DATA T
			INNER JOIN CDW..LNXX_FIN_ATY LNXX
				ON T.BF_SSN = LNXX.BF_SSN
			LEFT JOIN
			(
				SELECT DISTINCT
					LNXX.BF_SSN,
					LNXX.LN_SEQ,
					AYXX.LX_ATY
				FROM
					@DATA T INNER JOIN CDW..LNXX_LON_ATY LNXX
						ON T.BF_SSN = LNXX.BF_SSN
					INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
						ON AYXX.BF_SSN = LNXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = LNXX.LN_ATY_SEQ
					INNER JOIN CDW..AYXX_ATY_CMT AYXX
						ON AYXX.BF_SSN = AYXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
					INNER JOIN CDW..AYXX_ATY_TXT AYXX
						ON AYXX.BF_SSN = AYXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
						AND AYXX.LN_ATY_CMT_SEQ = AYXX.LN_ATY_CMT_SEQ
				WHERE
					AYXX.PF_REQ_ACT IN ('AXDCV')
			) AXDCV
				ON LNXX.BF_SSN = AXDCV.BF_SSN
				AND LNXX.LN_SEQ = AXDCV.LN_SEQ
		WHERE	
			LNXX.PC_FAT_TYP = 'XX'
			AND LNXX.PC_FAT_SUB_TYP IN ('XX','XX','XX')
			AND ISNULL(LNXX.LC_FAT_REV_REA,'') = ''
			AND LNXX.LC_STA_LONXX = 'A'
	) TRANSFERS
		ON d.BF_SSN = TRANSFERS.BF_SSN
		AND DWXX.LN_SEQ = TRANSFERS.LN_SEQ

ORDER BY PDXX.DF_PRS_ID
