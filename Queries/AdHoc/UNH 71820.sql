SELECT * FROM OPENQUERY(DUSTER,
'
SELECT
	MR5B.WN_RPS_TRM_RMN,
	LN80.*
FROM
	OLWHRM1.LN80_LON_BIL_CRF LN80
	INNER JOIN OLWHRM1.PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN80.BF_SSN
	INNER JOIN OLWHRM1.MR5B_MR_LON_MTH_02 MR5B
		ON MR5B.BF_SSN = LN80.BF_SSN
		AND MR5B.LN_SEQ = LN80.LN_SEQ
WHERE
	PD10.DF_SPE_ACC_ID = ''4606100802''
')

