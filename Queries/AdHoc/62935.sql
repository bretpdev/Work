SELECT DISTINCT
	--PD10.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	--LN10.LN_SEQ AS LOAN_SEQ,
	--LN10.LA_CUR_PRI AS LOAN_BALANCE,
	SUM(LN10.LA_CUR_PRI)-- OVER (PARTITION BY LN10.BF_SSN) AS TOTAL_BALANCE
	--ISNULL(LN16.LN_DLQ_MAX,0) AS DAYS_PAST_DUE,
	--ISNULL(LN90.PMT_AMT,0) as PAYMENTS_MADE_LAST_YEAR
FROM
	[AuditUDW].[dbo].[LN10_LON_Jul2019] LN10
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	LEFT JOIN 
	(
		SELECT 
			BF_SSN,
			LN_SEQ,
			SUM(ABS(ISNULL(LA_FAT_CUR_PRI,0) + ISNULL(LA_FAT_NSI,0) + ISNULL(LA_FAT_LTE_FEE,0))) AS PMT_AMT
		FROM 
			UDW..LN90_FIN_ATY LN90
		WHERE
			LN90.LD_FAT_EFF BETWEEN '08/01/2018' AND '07/30/2019'
			AND ISNULL(LN90.LC_FAT_REV_REA,'') = ''
			AND LN90.LC_STA_LON90 = 'A'
			AND LN90.PC_FAT_TYP = '10'
		GROUP BY
			BF_SSN,
			LN_SEQ
	) LN90
		ON LN90.BF_SSN = LN10.BF_SSN
		AND LN90.LN_SEQ = LN10.LN_SEQ
	
	LEFT JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = LN10.BF_SSN
		AND LN16.LN_SEQ = LN10.LN_SEQ
		AND LN16.LC_STA_LON16 = '1'
WHERE 
	IC_LON_PGM = 'TILP'
	AND LA_CUR_PRI > 0
	AND LC_STA_LON10 = 'R'
--ORDER BY
--	PD10.DF_SPE_ACC_ID,
--	LN10.LN_SEQ