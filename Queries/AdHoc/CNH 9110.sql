UPDATE @Populations
SET
	SSN = RIGHT('XXXXXXXX' + SSN, X)

SELECT
	*
FROM
	OPENQUERY
	(
		LEGEND,
		'
			SELECT DISTINCT
				PDXX.DM_PRS_X 			AS FIRST_NAME
				,PDXX.DM_PRS_LST 		AS LAST_NAME
				,LNXX.BF_SSN 			AS SSN
				,LNXX.LN_SEQ 			AS LOAN
				,FSXX.LF_FED_AWD 		AS AWARD_ID
				,FSXX.LN_FED_AWD_SEQ 	AS AWARD_ID_SEQ
				,DWXX.WC_DW_LON_STA 	AS STATUS
				,LNXX.LA_CUR_PRI 		AS CURR_PRINCIPAL
				,DWXX.WA_TOT_BRI_OTS	AS CURR_INTEREST
				,LNXX.LD_CRT_LONXX		AS REDISCLOSURED
				,LNXX.LA_ACR_INT_RPD 	AS INTEREST_AT_REDISCLOSURE
				,ROW_NUMBER() OVER (PARTITION BY LNXX.BF_SSN, LNXX.LN_SEQ ORDER BY LNXX.LD_CRT_LONXX) AS DateOrder
			FROM
				PKUB.LNXX_LON LNXX
				INNER JOIN PKUB.PDXX_PRS_NME PDXX ON LNXX.BF_SSN = PDXX.DF_PRS_ID	
				INNER JOIN PKUB.FSXX_DL_LON	FSXX ON LNXX.BF_SSN = FSXX.BF_SSN AND LNXX.LN_SEQ = FSXX.LN_SEQ 
				INNER JOIN PKUB.DWXX_DW_CLC_CLU DWXX ON LNXX.BF_SSN = DWXX.BF_SSN AND LNXX.LN_SEQ = DWXX.LN_SEQ 
				LEFT JOIN PKUB.LNXX_LON_RPS LNXX ON LNXX.BF_SSN = LNXX.BF_SSN AND LNXX.LN_SEQ = LNXX.LN_SEQ AND LNXX.LD_CRT_LONXX >= ''XXXX-XX-XX''
			ORDER BY
				LNXX.BF_SSN
		'
	) L
	INNER JOIN @Populations P on P.SSN = L.SSN
WHERE
	L.Redisclosured IS NULL
	OR
	(
		L.Redisclosured IS NOT NULL
		AND
		DateOrder = X
	)