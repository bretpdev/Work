USE UDW
GO
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	AY10.LF_USR_REQ_ATY,
	AY10.PF_REQ_ACT,
	AY10.LD_ATY_REQ_RCV
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
	INNER JOIN UDW..AY10_BR_LON_ATY AY10
		ON AY10.BF_SSN = LN10.BF_SSN
		AND AY10.PF_REQ_ACT IN ('DFAPV','DFDNY')
		AND AY10.LF_USR_REQ_ATY IN('UT02539','UT02541','UT02545','UT02546','UT02551')
		AND CAST(AY10.LD_ATY_REQ_RCV AS DATE) BETWEEN '2019-04-29' AND '2019-05-13'
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN50.BF_SSN = LN10.BF_SSN
		AND LN50.LN_SEQ = LN10.LN_SEQ
		AND LN50.LC_DFR_RSP != '003'
		AND LN50.LC_STA_LON50 = 'A'
	INNER JOIN UDW..DF10_BR_DFR_REQ DF10
		ON DF10.BF_SSN = LN50.BF_SSN
		AND DF10.LF_DFR_CTL_NUM = LN50.LF_DFR_CTL_NUM
		AND DF10.LC_STA_DFR10 = 'A'
		AND DF10.LC_DFR_STA = 'A'
		AND DF10.LC_DFR_TYP = '29'