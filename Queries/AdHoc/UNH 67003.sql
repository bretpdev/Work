DECLARE @BEGIN DATE = '2019-07-01'
DECLARE @END DATE = GETDATE()

SELECT DISTINCT
	LN33.BF_SSN,
	LN33.LN_SEQ,
	LN33.LD_CU_END
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..LN33_LON_CU_INF LN33
		ON LN10.BF_SSN = LN33.BF_SSN
		AND LN10.LN_SEQ = LN33.LN_SEQ
		AND LN33.LD_CU_END BETWEEN @BEGIN AND @END
WHERE 
	LN10.LF_LON_CUR_OWN LIKE '828476%' --Legacy portfolio
	OR LN10.LF_LON_CUR_OWN = '829769'  --Bana portfolio