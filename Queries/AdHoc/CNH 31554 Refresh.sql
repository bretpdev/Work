MERGE 
	dbo.DTXX_LNXX_LON_CRB LOCAL
USING
	(
		SELECT
			*
		FROM
			OPENQUERY
			(
				LEGEND_TEST_VUKX,
				'
					SELECT
						REMOTE.*
					FROM
						PKUB.DTXX_LNXX_LON_CRB REMOTE
				'
			) 
	) R ON R.BF_SSN = LOCAL.BF_SSN AND R.LN_SEQ = LOCAL.LN_SEQ AND LOCAL.LD_RPT_CRB = R.LD_RPT_CRB
	WHEN MATCHED THEN 
		UPDATE SET 			LOCAL.LN_LON_CRB_RPT_DTA = R.LN_LON_CRB_RPT_DTA,			LOCAL.LF_CRT_DTS_DTXX = R.LF_CRT_DTS_DTXX,			LOCAL.LF_CRT_USR_DTXX = R.LF_CRT_USR_DTXX,			LOCAL.LC_SPC_CMT = R.LC_SPC_CMT,			LOCAL.LC_SPC_CMT_UPD_TYP = R.LC_SPC_CMT_UPD_TYP,			LOCAL.LC_CRB_CSM_INF = R.LC_CRB_CSM_INF,			LOCAL.LC_CSM_INF_UPD_TYP = R.LC_CSM_INF_UPD_TYP,			LOCAL.LC_CRB_ECOA = R.LC_CRB_ECOA,			LOCAL.LC_ECOA_UPD_TYP = R.LC_ECOA_UPD_TYP,			LOCAL.LC_RPT_STA_CRB = R.LC_RPT_STA_CRB,			LOCAL.LC_STA_CRB_UPD_TYP = R.LC_STA_CRB_UPD_TYP,			LOCAL.LD_ACC_INF = R.LD_ACC_INF,			LOCAL.LC_ACC_INF_UPD_TYP = R.LC_ACC_INF_UPD_TYP,			LOCAL.LC_PRV_MTH_PAY_HST = R.LC_PRV_MTH_PAY_HST,			LOCAL.LC_PRV_PAY_UPD_TYP = R.LC_PRV_PAY_UPD_TYP,			LOCAL.LI_LON_CRB_FNL_STA = R.LI_LON_CRB_FNL_STA,			LOCAL.LC_FNL_STA_UPD_TYP = R.LC_FNL_STA_UPD_TYP,			LOCAL.LC_CLC_PRV_RPT_DIF = R.LC_CLC_PRV_RPT_DIF
			WHEN NOT MATCHED THEN
		INSERT 
		(
			BF_SSN,			LN_SEQ,			LD_RPT_CRB,			LN_LON_CRB_RPT_DTA,			LF_CRT_DTS_DTXX,			LF_CRT_USR_DTXX,			LC_SPC_CMT,			LC_SPC_CMT_UPD_TYP,			LC_CRB_CSM_INF,			LC_CSM_INF_UPD_TYP,			LC_CRB_ECOA,			LC_ECOA_UPD_TYP,			LC_RPT_STA_CRB,			LC_STA_CRB_UPD_TYP,			LD_ACC_INF,			LC_ACC_INF_UPD_TYP,			LC_PRV_MTH_PAY_HST,			LC_PRV_PAY_UPD_TYP,			LI_LON_CRB_FNL_STA,			LC_FNL_STA_UPD_TYP,			LC_CLC_PRV_RPT_DIF
		)
		VALUES 
		(
			R.BF_SSN,			R.LN_SEQ,			R.LD_RPT_CRB,			R.LN_LON_CRB_RPT_DTA,			R.LF_CRT_DTS_DTXX,			R.LF_CRT_USR_DTXX,			R.LC_SPC_CMT,			R.LC_SPC_CMT_UPD_TYP,			R.LC_CRB_CSM_INF,			R.LC_CSM_INF_UPD_TYP,			R.LC_CRB_ECOA,			R.LC_ECOA_UPD_TYP,			R.LC_RPT_STA_CRB,			R.LC_STA_CRB_UPD_TYP,			R.LD_ACC_INF,			R.LC_ACC_INF_UPD_TYP,			R.LC_PRV_MTH_PAY_HST,			R.LC_PRV_PAY_UPD_TYP,			R.LI_LON_CRB_FNL_STA,			R.LC_FNL_STA_UPD_TYP,			R.LC_CLC_PRV_RPT_DIF
		)
	WHEN NOT MATCHED BY SOURCE THEN
		DELETE;
