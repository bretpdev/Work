SELECT DISTINCT
	RTRIM(PD10.DM_PRS_1)+ RTRIM(' ' + PD10.DM_PRS_MID) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [Name],
	PD10.DF_SPE_ACC_ID AS [Account Number],
	SUM(LN10.LA_CUR_PRI) AS Balance
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..FB10_BR_FOR_REQ FB10
		ON PD10.DF_PRS_ID = FB10.BF_SSN
	INNER JOIN UDW..LN60_BR_FOR_APV LN60
		ON FB10.BF_SSN = LN60.BF_SSN
		AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
		AND LN60.LC_STA_LON60 = 'A'
	INNER JOIN UDW..LN10_LON LN10
		ON LN60.BF_SSN = LN10.BF_SSN
		AND LN60.LN_SEQ = LN10.LN_SEQ
WHERE
	FB10.LC_FOR_TYP = '10'
	AND FB10.LC_FOR_STA = 'A'
	AND FB10.LC_STA_FOR10 = 'A'
	AND (LN60.LD_FOR_BEG >= '2016-07-01' OR LN60.LD_FOR_END >= '2016-07-01')
GROUP BY
	RTRIM(PD10.DM_PRS_1)+ RTRIM(' ' + PD10.DM_PRS_MID) + ' ' + RTRIM(PD10.DM_PRS_LST),
	PD10.DF_SPE_ACC_ID
ORDER BY
	PD10.DF_SPE_ACC_ID