SELECT
DF_SPE_ACC_ID,
SUM(LID_C) AS SC 
FROM
(
select DISTINCT 
	DF_SPE_ACC_ID,
	LID, 
	L_COUNT_1,
	(COUNT(DISTINCT LID)) AS LID_C
from
(
SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	LN10.BF_SSN,
	LN10.LN_SEQ,
	ln10.ld_lon_1_dsb,
	LN10.LF_LON_ALT + CAST(LN10.LN_LON_ALT_SEQ AS VARCHAR(3)) AS LID,
	count(*) OVER(PARTITION BY BF_SSN, ld_lon_1_dsb) AS L_COUNT_1
FROM
	UDW..LN10_LON LN10 
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
WHERE
	LN10.LF_LON_CUR_OWN = '828476' --UHEAA OWNER CODE (NOT A BANA, ALIGN, OR TILP LOAN) 
	AND LN10.IC_LON_PGM IN ('CNSLDN', 'SUBCNS','SUBSPC', 'UNCNS', 'UNSPC')
	AND LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'
	--AND BF_SSN = '001627113' --AND LN_SEQ = 1
) p
	where p.L_COUNT_1 > 1
GROUP BY 
DF_SPE_ACC_ID,
	LID, 
	L_COUNT_1
) P
GROUP BY
DF_SPE_ACC_ID
HAVING SUM(LID_C) = 1

