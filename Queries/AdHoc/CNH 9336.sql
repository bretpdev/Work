
Find the following: (USE PKUS - LNXX EOM)

Find loans where LNXX.LD_END_GRC_PRD > X/XX/XX and LNXX.LF_FED_CLC_RSK = XXXXXX


Output = 
LNXX.BF_SSN
LNXX.LN_SEQ

SELECT
	L.BF_SSN,
	L.LN_SEQ,
	L.LD_END_GRC_PRD,
	L.WC_DW_LON_STA
FROM
	OPENQUERY
	(
		LEGEND,
		'
		SELECT
			LNXX.BF_SSN,
			LNXX.LN_SEQ,
			LNXX.LD_END_GRC_PRD,
			DWXX.WC_DW_LON_STA
		FROM
			PKUS.LNXX_LON LNXX
			INNER JOIN PKUB.DWXX_DW_CLC_CLU DWXX ON DWXX.BF_SSN = LNXX.BF_SSN AND DWXX.LN_SEQ = LNXX.LN_SEQ
		WHERE
			LNXX.LF_FED_CLC_RSK = ''XXXXXX''
			AND
			(
				LNXX.LD_END_GRC_PRD > ''XXXX-XX-XX''
				OR
				DWXX.WC_DW_LON_STA IN (''XX'', ''XX'')
			)
		'
	) L
