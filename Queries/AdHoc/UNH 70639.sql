SELECT DISTINCT
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [NAME],
	FB10.BF_SSN AS SSN,
	FB10.LC_FOR_TYP AS FORB_TYPE,
	LN60.LD_FOR_END AS END_DATE,
	FB10.LD_CRT_REQ_FOR AS APPLIED_DATE,
	SUM(LN10.LA_CUR_PRI) OVER (PARTITION BY LN10.BF_SSN) AS OUTSTANDING_BALANCE
FROM
	FB10_BR_FOR_REQ FB10
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = FB10.BF_SSN
	INNER JOIN LN60_BR_FOR_APV LN60
		ON LN60.BF_SSN = FB10.BF_SSN
		AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN60.BF_SSN
		AND LN10.LN_SEQ = LN60.LN_SEQ
WHERE
	LN60.LC_STA_LON60 = 'A'
	AND FB10.LC_STA_FOR10 = 'A'
	AND FB10.LC_FOR_STA = 'A' 
	AND YEAR(LN60.LD_FOR_END) >= 2040
	AND LN60.LC_FOR_RSP != '003'
	AND LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'