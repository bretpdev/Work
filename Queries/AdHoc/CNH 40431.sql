SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	--LNXX.LN_SEQ,
	ADXX.LC_TYP_FAT_ADJ_REQ,
	ADXX.LF_USR_FAT_ADJ_REQ,
	LNXX.LC_STA_LONXX,
	IIF(LNXX.LN_DLQ_MAX > X,'>X',NULL) AS DLQ_FLAG 
FROM
	LNXX_LON LNXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	INNER JOIN ADXX_PCV_ATY_ADJ ADXX
		ON LNXX.BF_SSN = ADXX.BF_SSN
	INNER JOIN LNXX_LON_DLQ_HST LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
WHERE
	ADXX.LC_TYP_FAT_ADJ_REQ = 'XX'
	AND ADXX.LF_USR_FAT_ADJ_REQ = 'TFXGX'
	AND LNXX.LA_CUR_PRI > X.XX
	AND LNXX.LC_STA_LONXX = 'R'
	AND LNXX.LC_STA_LONXX = 'X'
	AND LNXX.LN_DLQ_MAX > X
ORDER BY
	PDXX.DF_SPE_ACC_ID;