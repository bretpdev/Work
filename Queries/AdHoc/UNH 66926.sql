	LN10.LA_CUR_PRI > 0.00
	AND LN10.LC_STA_LON10 = 'R'
--	AND DATEADD(DAY, 121, ISNULL(SD10.LD_SCL_SPR, GETDATE())) > SchoolCodes.SchoolCloseDate
