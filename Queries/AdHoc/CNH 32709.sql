SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID, 
	PDXX.DM_PRS_LST,
	LNXX.LF_DOE_SCL_ORG
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..LNXX_LON LNXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	INNER JOIN CDW..LNXX_LON LNXX_X
		ON LNXX.BF_SSN = LNXX_X.BF_SSN
		AND LNXX.LF_DOE_SCL_ORG = LNXX_X.LF_DOE_SCL_ORG
		AND LNXX.LN_SEQ != LNXX_X.LN_SEQ
		AND LNXX_X.LC_WOF_WUP_REA != 'D'
WHERE
	LNXX.LC_WOF_WUP_REA = 'D'