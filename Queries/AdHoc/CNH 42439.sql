SELECT
	PDXX.DF_SPE_ACC_ID AS [ACCOUNT NUMBER],
	CONVERT(VARCHAR, AYXX.LD_ATY_REQ_RCV, XXX) AS [ARC DATE]
FROM
	CDW..AYXX_BR_LON_ATY AYXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE
	AYXX.PF_REQ_ACT = 'RCBDF'
	AND AYXX.LC_STA_ACTYXX = 'A'
	AND AYXX.LD_ATY_REQ_RCV BETWEEN 'XX/X/XXXX' AND 'XX/XX/XXXX'


