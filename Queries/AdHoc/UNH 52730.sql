--SCRA
SELECT DISTINCT * FROM OPENQUERY (DUSTER,'
	SELECT
		 LN72.BF_SSN
		,LN72.LC_INT_RDC_PGM
		,LN72.LD_ITR_EFF_BEG
	FROM
		OLWHRM1.LN72_INT_RTE_HST LN72
	WHERE
		LN72.LC_INT_RDC_PGM = ''M''
		AND DAYS(LN72.LD_ITR_EFF_BEG) BETWEEN DAYS(''07/01/2016'') AND DAYS(''06/30/2017'')
')
ORDER BY
	LD_ITR_EFF_BEG;

--CURE
SELECT DISTINCT * FROM OPENQUERY (DUSTER,'
	SELECT
		 LN33.BF_SSN
		,LN33.LD_CU_ENT
	FROM
		OLWHRM1.LN33_LON_CU_INF LN33
	WHERE
		DAYS(LN33.LD_CU_ENT) BETWEEN DAYS(''07/01/2016'') AND DAYS(''06/30/2017'')
');

