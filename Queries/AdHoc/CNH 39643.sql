SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	UnConsol.LN_SEQ AS UnsubLoan,
	UnConsol.IC_LON_PGM AS UnsubLoanProgram,
	DWXXUn.WC_DW_LON_STA AS UnsubStatus,
	SubConsol.LN_SEQ AS SubLoan,
	SubConsol.IC_LON_PGM AS SubLoanProgram,
	DWXXSub.WC_DW_LON_STA AS SubStatus,
	CAST(UnConsol.LD_LON_X_DSB AS DATE) AS DisbursementDate
FROM
	CDW..LNXX_LON UnConsol
	INNER JOIN CDW..LNXX_LON SubConsol
		ON SubConsol.BF_SSN = UnConsol.BF_SSN
		AND CAST(SubConsol.LD_LON_X_DSB AS DATE) = CAST(UnConsol.LD_LON_X_DSB AS DATE)
		AND SubConsol.IC_LON_PGM = 'DLSCNS'
	INNER JOIN CDW..DWXX_DW_CLC_CLU DWXXUn
		ON DWXXUn.BF_SSN = UnConsol.BF_SSN
		AND DWXXUn.LN_SEQ = UnConsol.LN_SEQ
	INNER JOIN CDW..DWXX_DW_CLC_CLU DWXXSub
		ON DWXXSub.BF_SSN = SubConsol.BF_SSN
		AND DWXXSub.LN_SEQ = SubConsol.LN_SEQ
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = UnConsol.BF_SSN
WHERE
	UnConsol.IC_LON_PGM = 'DLUCNS'
	AND DWXXUn.WC_DW_LON_STA != DWXXSub.WC_DW_LON_STA