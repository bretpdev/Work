SELECT DISTINCT
	BF_SSN,
	LN_SEQ
FROM
	UDW..LN10_LON
WHERE
	IC_LON_PGM = 'TILP'
	AND LA_CUR_PRI > 0.00

SELECT DISTINCT
	LN10.BF_SSN,
	LN10.LN_SEQ
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..DF10_BR_DFR_REQ DF10
		ON DF10.BF_SSN = LN10.BF_SSN
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN50.BF_SSN = LN10.BF_SSN
		AND LN50.LN_SEQ = LN10.LN_SEQ
		AND LN50.LF_DFR_CTL_NUM = DF10.LF_DFR_CTL_NUM
WHERE
	IC_LON_PGM = 'TILP'
	AND LA_CUR_PRI > 0.00
	AND DF10.LC_STA_DFR10 = 'A'
	AND DF10.LC_DFR_STA = 'A'
	AND LN50.LC_STA_LON50 = 'A'
	AND LN50.LC_DFR_RSP != '003'
	AND DF10.LC_DFR_TYP = '33'
	AND CAST(GETDATE() AS DATE) BETWEEN LN50.LD_DFR_BEG AND LN50.LD_DFR_END

SELECT DISTINCT
	LN10.BF_SSN,
	LN10.LN_SEQ
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = LN10.BF_SSN
		AND LN16.LN_SEQ = LN10.LN_SEQ
WHERE
	IC_LON_PGM = 'TILP'
	AND LA_CUR_PRI > 0.00
	AND LN16.LC_STA_LON16 = '1'