		SELECT 
			DFXX.LC_DFR_TYP,
			LNXX.LD_DFR_BEG,
			LNXX.LD_DFR_END,
			DFXX.LD_CRT_REQ_DFR,
			DFXX.LF_LST_DTS_DFXX,
			DFXX.LC_DFR_STA,
			LNXX.BF_SSN,
			LNXX.LN_SEQ
		FROM
			UDW..LNXX_BR_DFR_APV LNXX
			INNER JOIN UDW..DFXX_BR_DFR_REQ DFXX
				ON DFXX.BF_SSN = LNXX.BF_SSN
				AND DFXX.LF_DFR_CTL_NUM = LNXX.LF_DFR_CTL_NUM
			INNER JOIN 
			(
				SELECT
					MAX(LNXX.LD_STA_LONXX) AS MaxLNXX,
					LNXX.BF_SSN,
					LN_SEQ
				FROM
					UDW..LNXX_BR_DFR_APV LNXX
					INNER JOIN UDW..DFXX_BR_DFR_REQ DFXX
						ON DFXX.BF_SSN = LNXX.BF_SSN
						AND DFXX.LF_DFR_CTL_NUM = LNXX.LF_DFR_CTL_NUM
				WHERE
					LNXX.LC_STA_LONXX = 'A'
					AND DFXX.LC_STA_DFRXX = 'A'
				GROUP BY
					LNXX.BF_SSN,
					LNXX.LN_SEQ
			) LNXXMax
				ON LNXX.BF_SSN = LNXXMax.BF_SSN
				AND LNXX.LN_SEQ = LNXXMax.LN_SEQ
				AND LNXXMax.MaxLNXX = LNXX.LD_STA_LONXX
		WHERE
			LNXX.LC_STA_LONXX = 'A'
			AND DFXX.LC_STA_DFRXX = 'A'
			AND DFXX.LC_DFR_STA = 'D' 
			AND DFXX.LC_DFR_TYP IN ('XX','XX')

