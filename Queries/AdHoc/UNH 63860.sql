SELECT
	OQ.BF_SSN,
	OQ.LN_SEQ,
	CentralData.dbo.TRIM(PD10.DM_PRS_1) + ' ' + CentralData.dbo.TRIM(PD10.DM_PRS_LST) AS FullName,
	OQ.LD_ATY_REQ_RCV AS DateOfComment,
	DW01.WC_DW_LON_STA AS LoanStatus,
	RS.LC_TYP_SCH_DIS AS RepaymentPlan,
	RS.LA_RPS_ISL AS RepaymentAmount,
	ISNULL(LN10.LA_CUR_PRI,0.00) + ISNULL(DW01.WA_TOT_BRI_OTS,0.00) AS LoanBalance
FROM OPENQUERY(DUSTER,
'
SELECT
	LN85.BF_SSN,
	LN85.LN_SEQ,
	AY10.LN_ATY_SEQ,
	AY15.LN_ATY_CMT_SEQ,
	AY10.LD_ATY_REQ_RCV,
	AY20.LX_ATY
FROM
	OLWHRM1.AY10_BR_LON_ATY AY10
	INNER JOIN OLWHRM1.AY15_ATY_CMT AY15
		ON AY15.BF_SSN = AY10.BF_SSN
		AND AY15.LN_ATY_SEQ = AY10.LN_ATY_SEQ
		AND AY15.LC_STA_AY15 = ''A''
	INNER JOIN OLWHRM1.AY20_ATY_TXT AY20
		ON AY20.BF_SSN = AY15.BF_SSN
		AND AY20.LN_ATY_SEQ = AY15.LN_ATY_SEQ
		AND AY20.LN_ATY_CMT_SEQ = AY15.LN_ATY_CMT_SEQ
	INNER JOIN OLWHRM1.LN85_LON_ATY LN85
		ON LN85.BF_SSN = AY10.BF_SSN
		AND LN85.LN_ATY_SEQ = AY10.LN_ATY_SEQ
WHERE
	AY10.PF_REQ_ACT IN (''FBAPV'',''FBDNY'')
	AND AY10.LC_STA_ACTY10 = ''A''
	AND AY20.LX_ATY LIKE ''%FSA%''

FOR READ ONLY WITH UR
') OQ
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = OQ.BF_SSN
		AND LN10.LN_SEQ = OQ.LN_SEQ
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = LN10.BF_SSN
		AND DW01.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	LEFT JOIN UDW.calc.RepaymentSchedules RS
		ON RS.BF_SSN = LN10.BF_SSN
		AND RS.LN_SEQ = LN10.LN_SEQ
		AND RS.CurrentGradation = 1

