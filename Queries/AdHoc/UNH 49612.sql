--Legacy (non-BANA, non-Align) borrowers who are not currently on ACH, in an in-school, in grace or in-repayment status and currently have an ACH benefit eligibility great than .25.
SELECT * FROM OPENQUERY (DUSTER,'
	SELECT DISTINCT
		LN10.BF_SSN
		,PD10.DF_SPE_ACC_ID
		,LN10.LN_SEQ
		--,LN10.IF_CUR_OWN
		,LN10.LF_LON_CUR_OWN
		,LN10.LC_STA_LON10
		,DW01.WC_DW_LON_STA 
		,RP30.PR_EFT_RIR
		,LN83.LC_STA_LN83
		,LN10.IC_LON_PGM
		,LN10.LD_LON_1_DSB
		,RP30.PD_LON_1_DSB
	FROM
		OLWHRM1.LN10_LON LN10
		INNER JOIN OLWHRM1.DW01_DW_CLC_CLU DW01
			ON LN10.BF_SSN = DW01.BF_SSN
			AND LN10.LN_SEQ = DW01.LN_SEQ
		INNER JOIN OLWHRM1.RP30_EFT_RIR_PAR RP30
			ON LN10.IC_LON_PGM = RP30.IC_LON_PGM
			--AND LN10.LD_LON_1_DSB = RP30.PD_LON_1_DSB
			AND LN10.LF_LON_CUR_OWN = RP30.IF_OWN
		INNER JOIN OLWHRM1.PD10_PRS_NME PD10
			ON LN10.BF_SSN = PD10.DF_PRS_ID
		LEFT JOIN OLWHRM1.LN83_EFT_TO_LON LN83
			ON LN10.BF_SSN = LN83.BF_SSN
			AND LN10.LN_SEQ = LN83.LN_SEQ
	WHERE
		LN10.LF_LON_CUR_OWN LIKE ''828476%''
		AND LN10.LC_STA_LON10 = ''R''
		AND DW01.WC_DW_LON_STA IN (''01'', ''02'', ''03'') 
		AND RP30.PR_EFT_RIR >= ''.25''  
		AND (
				LN83.LC_STA_LN83 <> ''A''
				OR LN83.LC_STA_LN83 IS NULL
			)
		--AND PD10.DF_SPE_ACC_ID IN (
		--''2698186513'', 
		--''2184811385''
		--)
		AND RP30.PD_LON_1_DSB IS NULL
	ORDER BY
		PD10.DF_SPE_ACC_ID
');
