SELECT DISTINCT
	LNXX.BF_SSN AS SSN
	,LNXX.LN_SEQ AS LoanSequence
	,FBXX.LD_CRT_REQ_FOR AS EffectiveDate
	,FBXX.LD_FOR_REQ_BEG AS StartDate
	,FBXX.LD_FOR_BR_REQ_END AS EndDate
	,FBXX.LC_FOR_TYP
FROM
	LNXX_LON LNXX
	INNER JOIN LNXX_BR_FOR_APV LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
	INNER JOIN FBXX_BR_FOR_REQ FBXX
		ON LNXX.BF_SSN = FBXX.BF_SSN
		AND LNXX.LF_FOR_CTL_NUM = FBXX.LF_FOR_CTL_NUM
WHERE
	FBXX.LC_FOR_TYP = 'XX'
;

