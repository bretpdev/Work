SELECT
	*
FROM
	OPENQUERY
	(
		DUSTER,
		'
			SELECT DISTINCT
				PD10.DF_SPE_ACC_ID AS AccountNumber,
				PD10.DM_PRS_1 AS First_Name,
				PD10.DM_PRS_LST AS Last_Name,
				PD32.DX_ADR_EML AS Email,
				LN16.LN_DLQ_MAX AS Days_Delinquent,
				PD30.DX_STR_ADR_1 AS Address_1,
				PD30.DX_STR_ADR_2 AS Address_2,
				PD30.DX_STR_ADR_3 AS Address_3,
				PD30.DM_CT AS City,
				PD30.DC_DOM_ST AS State,
				PD30.DF_ZIP_CDE AS Zip,
				PD42.DN_DOM_PHN_ARA AS Area_Code,
				PD42.DN_DOM_PHN_ARA||PD42.DN_DOM_PHN_XCH||PD42.DN_DOM_PHN_LCL AS Phone
			FROM
				OLWHRM1.PD10_PRS_NME PD10
				INNER JOIN OLWHRM1.LN10_LON LN10
					ON LN10.BF_SSN = PD10.DF_PRS_ID
					AND LN10.LC_STA_LON10 = ''R''
					AND LN10.LA_CUR_PRI > 0
				INNER JOIN OLWHRM1.DW01_DW_CLC_CLU DW01
					ON DW01.BF_SSN = LN10.BF_SSN
					AND DW01.LN_SEQ = LN10.LN_SEQ
					AND DW01.WC_DW_LON_STA = ''03''
				INNER JOIN OLWHRM1.PD30_PRS_ADR PD30
					ON PD30.DF_PRS_ID = PD10.DF_PRS_ID
				LEFT OUTER JOIN OLWHRM1.PD32_PRS_ADR_EML PD32
					ON PD32.DF_PRS_ID = PD10.DF_PRS_ID
					AND PD32.DC_STA_PD32 = ''A''
				LEFT OUTER JOIN OLWHRM1.PD42_PRS_PHN PD42
					ON PD42.DF_PRS_ID = PD10.DF_PRS_ID
					AND PD42.DI_PHN_VLD = ''Y''
				LEFT OUTER JOIN
				( 
					SELECT
						BF_SSN,
						LN_SEQ,
						MAX(LN_DLQ_MAX) AS LN_DLQ_MAX
					FROM
						OLWHRM1.LN16_LON_DLQ_HST LN16
					WHERE
						LN16.LC_STA_LON16 = ''1''
					GROUP BY
						BF_SSN,
						LN_SEQ
				) LN16
					ON LN16.BF_SSN = LN10.BF_SSN
					AND LN16.LN_SEQ = LN10.LN_SEQ
			WHERE
				LEFT(PD30.DF_ZIP_CDE,5) IN(
					''25015'',''25025'',''25026'',''25035'',''25039'',''25045'',''25054'',''25061'',''25064'',
					''25067'',''25071'',''25075'',''25079'',''25083'',''25086'',''25102'',''25103'',''25107'',
					''25110'',''25112'',''25126'',''25132'',''25134'',''25143'',''25156'',''25160'',''25162'',
					''25177'',''25201'',''25202'',''25214'',''25301'',''25302'',''25303'',''25304'',''25305'',
					''25306'',''25309'',''25311'',''25312'',''25313'',''25314'',''25315'',''25317'',''25320'',
					''25321'',''25322'',''25323'',''25324'',''25325'',''25326'',''25327'',''25328'',''25329'',
					''25330'',''25331'',''25332'',''25333'',''25334'',''25335'',''25336'',''25337'',''25338'',
					''25339'',''25350'',''25356'',''25357'',''25358'',''25360'',''25361'',''25362'',''25364'',
					''25365'',''25375'',''25387'',''25389'',''25392'',''25396'')

		'
	)