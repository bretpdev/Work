SELECT DISTINCT
	PD10.DM_PRS_LST,
	PD10.DM_PRS_1,
	PD10.DF_PRS_ID AS BF_SSN,
	LN10.LF_LON_CUR_OWN,
	LN10.IF_GTR,
	CAST(LN90.LD_FAT_EFF AS DATE) AS ClaimPaidDate
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
	INNER JOIN UDW..LN90_FIN_ATY LN90
		ON LN90.BF_SSN = LN10.BF_SSN
		AND LN90.LN_SEQ = LN10.LN_SEQ
		AND COALESCE(RTRIM(LN90.LC_FAT_REV_REA),'') = ''
		AND LN90.PC_FAT_TYP = '10'
		AND LN90.PC_FAT_SUB_TYP = '30'
		AND LN90.LC_STA_LON90 = 'A'
		AND CAST(LN90.LD_FAT_EFF AS DATE) BETWEEN '07/01/2019' AND CAST(GETDATE() AS DATE)
	INNER JOIN UDW..LN40_LON_CLM_PCL LN40
		ON LN40.BF_SSN = LN10.BF_SSN
		AND LN40.LN_SEQ = LN10.LN_SEQ
		AND LN40.LC_REA_CLP_LON = '03'

