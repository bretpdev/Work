SELECT 
	PD10.DF_PRS_ID AS SSN,
	CB.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	CB.CreatedAt AS LETTER_CREATE_DATE,
	CB.RM_DSC_LTR_PRC AS LETTER_ID
	 
from 
	udw..LT20_LTR_REQ_PRC_Coborrower cb
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_SPE_ACC_ID = CB.DF_SPE_ACC_ID
where 
	PD10.DF_PRS_ID IN 
	(
	'159301444',
	'415945583',
	'241921063',
	'455067610',
	'493603106',
	'219229235',
	'237449288',
	'459952133',
	'461477248',
	'461547902',
	'456323230',
	'444422209',
	'342247566',
	'396269085',
	'538463538',
	'140440995'
	)
	AND CB.InactivatedAt IS NULL

	UNION ALL

SELECT
	PD10.DF_PRS_ID AS SSN,
	PP.AccountNumber AS ACCOUNT_NUMBER,
	PP.AddedAt AS LETTER_CREATE_DATE,
	L.Letter AS LETTER_ID
FROM
	ULS.[print].PrintProcessing PP
	INNER JOIN ULS.[print].ScriptData SD
		ON SD.ScriptDataId = PP.ScriptDataId
	INNER JOIN ULS.[print].Letters L
		ON L.LetterId = SD.LetterId
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_SPE_ACC_ID = PP.AccountNumber
where 
	PD10.DF_PRS_ID IN 
	(
	'159301444',
	'415945583',
	'241921063',
	'455067610',
	'493603106',
	'219229235',
	'237449288',
	'459952133',
	'461477248',
	'461547902',
	'456323230',
	'444422209',
	'342247566',
	'396269085',
	'538463538',
	'140440995'
	)
	AND PP.DeletedAt IS NULL

	UNION ALL

SELECT
	PD10.DF_PRS_ID AS SSN,
	PP.AccountNumber AS ACCOUNT_NUMBER,
	PP.AddedAt AS LETTER_CREATE_DATE,
	L.Letter AS LETTER_ID
FROM
	ULS.[print].PrintProcessingCoBorrower PP
	INNER JOIN ULS.[print].ScriptData SD
		ON SD.ScriptDataId = PP.ScriptDataId
	INNER JOIN ULS.[print].Letters L
		ON L.LetterId = SD.LetterId
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_SPE_ACC_ID = PP.AccountNumber
where 
	PD10.DF_PRS_ID IN 
	(
	'159301444',
	'415945583',
	'241921063',
	'455067610',
	'493603106',
	'219229235',
	'237449288',
	'459952133',
	'461477248',
	'461547902',
	'456323230',
	'444422209',
	'342247566',
	'396269085',
	'538463538',
	'140440995'
	)
	AND PP.DeletedAt IS NULL

ORDER BY LETTER_CREATE_DATE