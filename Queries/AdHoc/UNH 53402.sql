SELECT DISTINCT
	PD10.DF_SPE_ACC_ID
	,LN10.LN_SEQ
	,LN10.IC_LON_PGM
	,CASE
		WHEN LN10.LA_CUR_PRI > 0.00
		THEN '>0'
		ELSE NULL
	END AS LA_CUR_PRI
	,CASE
		WHEN SD10.LD_SCL_SPR < '2016-07-01'
		THEN '< 7/1/2016'
		ELSE NULL
	END AS LD_SCL_SPR
FROM
	..LN10_LON LN10
	INNER JOIN ..PD10_PRS_NME PD10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
	INNER JOIN ..SD10_STU_SPR SD10
		ON LN10.BF_SSN = SD10.LF_STU_SSN
WHERE
	LN10.IC_LON_PGM = 'TILP'
	AND 
	LN10.LA_CUR_PRI > 0.00
	AND 
	SD10.LD_SCL_SPR < '2016-07-01'
	AND
	LN10.LC_DFR_FOR_TYP = '06'
ORDER BY
	PD10.DF_SPE_ACC_ID
	,LN10.LN_SEQ
	,LN10.IC_LON_PGM
	,LA_CUR_PRI
	,LD_SCL_SPR 

