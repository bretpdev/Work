SELECT
	PD30.*
FROM
	CDW..AY10_BR_LON_ATY AY10
	INNER JOIN CDW..PD30_PRS_ADR PD30
		ON PD30.DF_PRS_ID = AY10.BF_SSN
WHERE
	PF_REQ_ACT = 'ADTLF'
	AND LD_ATY_REQ_RCV BETWEEN '10/01/2018' AND '12/10/2018'
	AND PD30.DC_DOM_ST = 'PR'

SELECT * FROM OPENQUERY(LEGEND, 
'
SELECT 
	PD31.*
FROM 
	PKUB.PD31_PRS_INA  PD31
	INNER JOIN PKUB.AY10_BR_LON_ATY AY10
		ON PD31.DF_PRS_ID = AY10.BF_SSN
WHERE 
	DC_DOM_ST_HST = ''PR'' 
	AND PF_REQ_ACT = ''ADTLF''
	AND LD_ATY_REQ_RCV BETWEEN ''10/01/2018'' AND ''12/10/2018''
')