--SSN, name, date bankruptcy was applied to account, principal balance, agent.
SELECT 
	PD24.DF_PRS_ID,
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS BORROWER_NAME,
	DD_BKR_STA AS DATE_APPLIED,
	LN10.BALANCE,
	DF_RSU_REA_LST_USR AS AGENT
FROM 
	UDW..PD24_PRS_BKR  PD24
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = PD24.DF_PRS_ID
	INNER JOIN
	(
		SELECT
			BF_SSN,
			SUM(LA_CUR_PRI) AS BALANCE
		FROM
			UDW..LN10_LON
		GROUP BY
			BF_SSN
	)LN10
		ON LN10.BF_SSN = PD24.DF_PRS_ID
WHERE 
	DC_BKR_STA = '06'
	AND DD_BKR_VER > '04/01/2020'