SELECT
	LN10.BF_SSN AS BorrSSN,
	LN10.LF_LON_ALT AS [Loan ID],
	LN10.LN_LON_ALT_SEQ AS [Loan ID Sequence],
	LN10.IF_GTR AS [Guarantor ID],
	LN10.LF_LON_CUR_OWN AS Servicer,
	LN10.LD_LON_GTR AS [Guaranty Date],
	DW01.WC_DW_LON_STA AS [Status],
	FT.Label AS [Status Type],
	LN10.LA_LON_AMT_GTR AS [Guarantor Amount],
	CASE WHEN LN10.IC_LON_PGM IN('SUBCNS','SUBSPC') THEN LN10.LA_CUR_PRI ELSE 0.00 END AS [SUB],
	CASE WHEN LN10.IC_LON_PGM IN('CNSLDN','UNCNS','UNSPC') THEN LN10.LA_CUR_PRI ELSE 0.00 END AS [UNSUB],
	0.00 AS [HEAL]
FROM
	[AuditUDW].[dbo].[LN10_LON_Jan2020] LN10
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = LN10.BF_SSN
		AND DW01.LN_SEQ = LN10.LN_SEQ
	LEFT JOIN UDW..FormatTranslation FT
		ON FT.FmtName = '$LONSTA'
		AND FT.[Start] = DW01.WC_DW_LON_STA
WHERE
	LN10.IC_LON_PGM IN ('CNSLDN','SUBCNS','SUBSPC','UNCNS','UNSPC')
	and LN10.IF_GTR = '000712'

