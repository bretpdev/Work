--Find all loans where LNXX.LF_DOE_SCL_ORG = XXXXXXXX or XXXXXXXX

--Output
--Acct #
--Loan seq


SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_SEQ

FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	LNXX.LF_DOE_SCL_ORG = ('XXXXXXXX')
ORDER BY
	DF_SPE_ACC_ID,
	LN_SEQ