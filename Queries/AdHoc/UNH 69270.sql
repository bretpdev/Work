SELECT DISTINCT
		LN16.BF_SSN AS BORROWER_SSN,
		LN20.LF_EDS AS ENDORSER_COMAKER_SSN,
		LN16.LD_DLQ_OCC AS DATE_OF_DELINQUENCY
FROM 
		UDW..LN16_LON_DLQ_HST LN16
		INNER JOIN UDW..LN20_EDS LN20
			ON LN20.BF_SSN = LN16.BF_SSN
			AND LN20.LN_SEQ = LN16.LN_SEQ
	WHERE
		LN16.LD_DLQ_OCC >= DATEADD(DAY, -10, '06/01/2020') 
		AND LN16.LC_DLQ_TYP = 'P'
		AND LN16.LN_DLQ_MAX >= 10