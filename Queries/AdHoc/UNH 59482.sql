SELECT 
	PD10.DF_SPE_ACC_ID,
	PD30.DX_STR_ADR_1,
	PD30.DX_STR_ADR_2,
	PD30.DX_STR_ADR_3,
	PD30.DM_CT,
	PD30.DC_DOM_ST,
	PD30.DF_ZIP_CDE,
	PD30.DM_FGN_ST,
	PD30.DM_FGN_CNY
FROM
	UDW..WQ21_TSK_QUE_HST WQ21
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ21.BF_SSN
	INNER JOIN UDW..PD30_PRS_ADR PD30
		ON PD30.DF_PRS_ID = PD10.DF_PRS_ID
		AND PD30.DC_ADR = 'L'
WHERE
	WQ21.WF_QUE = '66'
	AND CAST(WQ21.WD_ACT_REQ AS DATE) >= '2018-07-01'
