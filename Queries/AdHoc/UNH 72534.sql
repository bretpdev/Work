SELECT DISTINCT
	DC02.BF_SSN,
	COUNT(*) AS LOAN_COUNT,
	SUM(ISNULL(LA_CLM_BAL,0.00) - ISNULL(LA_CLM_PRJ_COL_CST,0.00)) AS PRINCIPAL_INTEREST,
	MAX(LAST_CON.LC) AS LAST_CONTACT
FROM
	@DATA D
	INNER JOIN ODW..DC02_BAL_INT DC02
		ON CAST(DC02.BF_SSN AS INT) = D.BF_SSN
	INNER JOIN 
	(
		SELECT
			BF_SSN,
			MAX(LD_DFL_LST_CNC) AS LC
		FROM	
			ODW..DC01_LON_CLM_INF
		WHERE
			LC_STA_DC10 = '03'
		GROUP BY
			BF_SSN
	) LAST_CON
		ON LAST_CON.BF_SSN = D.BF_SSN
GROUP BY
	DC02.BF_SSN

