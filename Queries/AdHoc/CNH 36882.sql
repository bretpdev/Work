GO

SELECT
	PDXX.DF_PRS_ID,
	AYXX.PF_REQ_ACT,
	AYXX.LD_ATY_REQ_RCV
FROM
	CDW..AYXX_BR_LON_ATY AYXX
	INNER JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE
	PF_REQ_ACT in ('ADTPD','TLDNY','TLFSA')
	AND LD_ATY_REQ_RCV BETWEEN 'XX/XX/XXXX' AND 'XX/XX/XXXX'
	AND PDXX.DC_DOM_ST = 'PR'

union 

SELECT * FROM OPENQUERY(LEGEND, 
'
SELECT 
	PDXX.DF_PRS_ID,
	AYXX.PF_REQ_ACT,
	AYXX.LD_ATY_REQ_RCV
FROM 
	PKUB.PDXX_PRS_INA  PDXX
	INNER JOIN PKUB.AYXX_BR_LON_ATY AYXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE 
	DC_DOM_ST_HST = ''PR'' 
	AND PF_REQ_ACT in (''ADTPD'',''TLDNY'',''TLFSA'')
	AND LD_ATY_REQ_RCV BETWEEN ''XX/XX/XXXX'' AND ''XX/XX/XXXX''
')