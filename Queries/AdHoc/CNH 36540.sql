SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_DLQ_MAX,
	BRXX.BD_EFT_STA
FROM 
	AYXX_BR_LON_ATY AYXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
	INNER JOIN LNXX_LON_DLQ_HST LNXX
		ON LNXX.BF_SSN = AYXX.BF_SSN
	INNER JOIN BRXX_BR_EFT BRXX
		ON BRXX.BF_SSN = AYXX.BF_SSN
WHERE 
	AYXX.PF_REQ_ACT = 'OLACH' 
	AND LNXX.LC_STA_LONXX = 'X'
	AND BRXX.BC_EFT_STA = 'A'