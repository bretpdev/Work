SELECT DISTINCT
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	LNXX.LN_FAT_SEQ,
	LNXX.PC_FAT_TYP,
	LNXX.PC_FAT_SUB_TYP,
	LNXX.LD_FAT_EFF,
	T.OPENDATE
FROM
	@SSNS T
	INNER JOIN CDW..LNXX_FIN_ATY LNXX
		ON T.SSN = LNXX.BF_SSN
		AND CAST(LNXX.LD_FAT_EFF AS DATE) >= CAST(T.OPENDATE AS DATE)
WHERE
	LNXX.PC_FAT_TYP = 'XX'
	AND LNXX.PC_FAT_SUB_TYP IN('XX','XX')
	AND ISNULL(LNXX.LC_FAT_REV_REA,'') = ''
	AND LNXX.LC_STA_LONXX = 'A'
ORDER BY
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	LNXX.LN_FAT_SEQ




--status reasons
SELECT DISTINCT
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	CASE
		WHEN TRANSFERS.COMMENT LIKE ('%SPLIT LOAN%') THEN 'SPLIT LOAN'
		ELSE TRANSFERS.TRANSFER_TYPE 
	END AS TRANSFER_TYPE,
	CASE 
        WHEN DISCHARGES.PF_REQ_ACT = 'ADDTH' THEN 'Death'
        WHEN DISCHARGES.PF_REQ_ACT = 'ADCSH' THEN 'Closed School'
        WHEN DISCHARGES.PF_REQ_ACT = 'ADTLF' THEN 'Teacher Loan Forgiveness'
        WHEN DISCHARGES.PF_REQ_ACT = 'ADFCR' THEN 'False Certification'
        WHEN DISCHARGES.PF_REQ_ACT = 'ADUPR' THEN 'Unpaid Refund'
        WHEN DISCHARGES.PF_REQ_ACT = 'IDAPB' THEN 'ID Theft'
        WHEN DISCHARGES.PF_REQ_ACT = 'TPDAP' THEN 'Total Permanent Disability'
        ELSE NULL
	END AS DISCHARGE_TYPE,
	TRANSFERS.COMMENT
FROM
	@SSNS T
	INNER JOIN CDW..LNXX_LON LNXX
		ON T.SSN = LNXX.BF_SSN
	LEFT JOIN
	(
		SELECT
			BF_SSN,
			SUM(
		FROM
			CDW.calc.RepaymentSchedules RS
		WHERE
			CurrentGradation = X
	) REPAYMENT
--TRANSFERS loans that were transferred
	LEFT JOIN
	(
		SELECT DISTINCT	
			LNXX.BF_SSN,
			LNXX.LN_SEQ,
			CASE 
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'PSLF'
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'TPD'
				WHEN LNXX.PC_FAT_SUB_TYP = 'XX' THEN 'DMCS'
				ELSE ''
			END AS TRANSFER_TYPE,
			AXDCV.LX_ATY AS COMMENT
		FROM
			@SSNS T
			INNER JOIN CDW..LNXX_FIN_ATY LNXX
				ON T.SSN = LNXX.BF_SSN
			LEFT JOIN
			(
				SELECT DISTINCT
					LNXX.BF_SSN,
					LNXX.LN_SEQ,
					AYXX.LX_ATY
				FROM
					@SSNS T INNER JOIN CDW..LNXX_LON_ATY LNXX
						ON T.SSN = LNXX.BF_SSN
					INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
						ON AYXX.BF_SSN = LNXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = LNXX.LN_ATY_SEQ
					INNER JOIN CDW..AYXX_ATY_CMT AYXX
						ON AYXX.BF_SSN = AYXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
					INNER JOIN CDW..AYXX_ATY_TXT AYXX
						ON AYXX.BF_SSN = AYXX.BF_SSN
						AND AYXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
						AND AYXX.LN_ATY_CMT_SEQ = AYXX.LN_ATY_CMT_SEQ
				WHERE
					AYXX.PF_REQ_ACT IN ('AXDCV')
			) AXDCV
				ON LNXX.BF_SSN = AXDCV.BF_SSN
				AND LNXX.LN_SEQ = AXDCV.LN_SEQ
		WHERE	
			LNXX.PC_FAT_TYP = 'XX'
			AND LNXX.PC_FAT_SUB_TYP IN ('XX','XX','XX')
			AND ISNULL(LNXX.LC_FAT_REV_REA,'') = ''
			AND LNXX.LC_STA_LONXX = 'A'
	) TRANSFERS
		ON LNXX.BF_SSN = TRANSFERS.BF_SSN
		AND LNXX.LN_SEQ = TRANSFERS.LN_SEQ
--DISCHARGES loan discharges with ARC to determine discharge type
	LEFT JOIN
    (
		SELECT DISTINCT
			LNXX.BF_SSN,
			LNXX.LN_SEQ,
			AYXX.LD_ATY_REQ_RCV,
			AYXX.PF_REQ_ACT
		FROM
			@SSNS T 
			INNER JOIN CDW..LNXX_LON LNXX
				ON T.SSN = LNXX.BF_SSN
			INNER JOIN CDW..LNXX_FIN_ATY LNXX
				ON LNXX.BF_SSN = LNXX.BF_SSN
				AND LNXX.LN_SEQ = LNXX.LN_SEQ
				AND LNXX.PC_FAT_TYP = 'XX' --discharge
				AND LNXX.PC_FAT_SUB_TYP  = 'XX' --discharge
				AND ISNULL(LNXX.LC_FAT_REV_REA,'') = ''
				AND LNXX.LC_STA_LONXX = 'A'
			INNER JOIN CDW..LNXX_LON_ATY LNXX
				ON LNXX.BF_SSN = LNXX.BF_SSN
				AND LNXX.LN_SEQ = LNXX.LN_SEQ
			INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
				ON AYXX.BF_SSN = LNXX.BF_SSN
				AND AYXX.LN_ATY_SEQ = LNXX.LN_ATY_SEQ
		WHERE
			LNXX.LA_CUR_PRI = X.XX -- zero balance
			AND AYXX.PF_REQ_ACT IN ('ADDTH','ADCSH','ADTLF','ADFCR','ADUPR','IDAPB','TPDAP')
    ) DISCHARGES
        ON LNXX.BF_SSN = DISCHARGES.BF_SSN
        AND LNXX.LN_SEQ = DISCHARGES.LN_SEQ
WHERE --only select loans which were transferred or discharged
	TRANSFERS.BF_SSN IS NOT NULL 
	OR DISCHARGES.BF_SSN IS NOT NULL
ORDER BY
	LNXX.BF_SSN,
	LNXX.LN_SEQ