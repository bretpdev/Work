SELECT DISTINCT
	pd10.DF_SPE_ACC_ID,
	ln90.*
FROM
	CDW..LN90_FIN_ATY LN90
	INNER JOIN CDW..PD10_PRS_NME PD10
		on PD10.DF_PRS_ID = LN90.BF_SSN
	INNER JOIN CDW..AD20_PCV_ATY_ADJ LN10
		ON LN10.BF_SSN = LN90.BF_SSN
		--AND LN10.LN_SEQ = LN90.LN_SEQ
WHERE
	LN90.PC_FAT_TYP = '50'
	AND LN90.PC_FAT_SUB_TYP = '02'
	AND LD_FAT_APL BETWEEN  '12/01/2019' AND '1/31/2020'
	AND ISNULL(ln90.LC_FAT_REV_REA,'') != ''
	AND LC_STA_LON90 = 'A'
	AND LN10.LC_WOF_WUP_REA = 'B'