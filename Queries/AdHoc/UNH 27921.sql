SELECT
	*
FROM OPENQUERY
(
DUSTER,
'
SELECT
	PD01.DF_SPE_ACC_ID,
	GA10.AA_CUR_PRI
FROM
	OLWHRM1.PD01_PDM_INF PD01
	INNER JOIN OLWHRM1.DC01_LON_CLM_INF DC01
		ON DC01.BF_SSN = PD01.DF_PRS_ID
	INNER JOIN OLWHRM1.GA10_LON_APP GA10
		ON GA10.AF_APL_ID = DC01.AF_APL_ID
		AND GA10.AF_APL_ID_SFX = DC01.AF_APL_ID_SFX
WHERE
	DC01.LC_STA_DC10 = ''03''
	AND DC01.LC_GRN IN (''06'',''07'')
	AND DC01.LC_REA_CLM_ASN_DOE != ''03''
'
)

select * from openquery(DUSTER, 'SELECT * FROM OLWHRM1.DC01_LON_CLM_INF WHERE LC_STA_DC10 = ''03'' AND LC_GRN IN (''06'',''07'')')