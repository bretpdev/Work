SELECT * FROM OPENQUERY(LEGEND,
'
	SELECT
		MONTH(DF_CRT_DTS_WB26) AS MON,
		YEAR(DF_CRT_DTS_WB26) AS YR,
		COUNT(*) AS VISITS
	FROM
		WEBFLS1.WB26_AU_LVL_EVT
	WHERE
		DF_CRT_DTS_WB26 BETWEEN ''01/01/2017'' AND ''03/31/2018''
	GROUP BY
		MONTH(DF_CRT_DTS_WB26),
		YEAR(DF_CRT_DTS_WB26)
	ORDER BY
		YEAR(DF_CRT_DTS_WB26),
		MONTH(DF_CRT_DTS_WB26)
')