SELECT
	PDXX.DF_SPE_ACC_ID,
	LTRIM(RTRIM(PDXX.DM_PRS_X)) AS  [FIRST NAME],
	LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS  [LAST NAME],
	WQXX.WF_CRT_DTS_WQXX AS [TASK CREATE DATE],
	WQXX.WF_CRT_DTS_WQXX AS [TASK CLOSED DATE],
	ISNULL(AYXX.PF_REQ_ACT, 'N/A') AS [ARC TYPE],
	WQXX.WF_USR_ASN_TSK AS [PROCESSOR UT] 
FROM
	CDW..WQXX_TSK_QUE_HST WQXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = WQXX.BF_SSN
	LEFT JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON AYXX.BF_SSN = WQXX.BF_SSN
		AND AYXX.LN_ATY_SEQ = WQXX.LN_ATY_SEQ
WHERE
	WF_QUE IN ('XA','XP')
	AND WF_USR_ASN_TSK = 'UTXXXXX'
	AND WC_STA_WQUEXX IN ('C','X')
	AND WF_CRT_DTS_WQXX BETWEEN 'XX/XX/XXXX' AND CAST(GETDATE() AS DATE)