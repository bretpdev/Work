SELECT * FROM OPENQUERY(DUSTER,
'
SELECT
	TRIM(DM_PRS_1) || '' '' || TRIM(DM_PRS_LST) AS "BORROWER NAME",
	SUM(LA_DSB - COALESCE(LA_DSB_CAN,0)) AS "AMOUNT BORROWED"
FROM
	OLWHRM1.PD10_PRS_NME PD10
	INNER JOIN OLWHRM1.LN10_LON LN10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	INNER JOIN OLWHRM1.LN15_DSB LN15
		ON LN15.BF_SSN = LN10.BF_SSN
		AND LN15.LN_SEQ = LN10.LN_SEQ
WHERE
	LN10.IC_LON_PGM = ''TILP''
	AND LN15.LD_DSB >= ''01/01/1997''
GROUP BY
	TRIM(DM_PRS_1) || '' '' || TRIM(DM_PRS_LST)

')