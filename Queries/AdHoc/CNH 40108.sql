SELECT
	LTRIM(RTRIM(PDXX.DM_PRS_X)) AS FirstName,
	LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS LastName,
	LTRIM(RTRIM(PDXX.DX_STR_ADR_X)) AS [AddressX],
	LTRIM(RTRIM(PDXX.DX_STR_ADR_X)) AS [AddressX],
	LTRIM(RTRIM(PDXX.DM_CT)) AS City,
	PDXX.DC_DOM_ST AS [State],
	PDXX.DF_ZIP_CDE AS ZipCode,
	PDXX.DF_SPE_ACC_ID AS AccountNumber,
	WQXX.PF_REQ_ACT AS TriggeringArc
FROM
	CDW..WQXX_TSK_QUE WQXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = WQXX.BF_SSN
	LEFT JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
		AND PDXX.DC_ADR = 'L'
		AND PDXX.DI_VLD_ADR = 'Y'
WHERE
	WQXX.WC_STA_WQUEXX NOT IN('X','C')
	AND WQXX.WF_QUE = 'XA'
	AND WQXX.WF_SUB_QUE = 'XX'
	AND WQXX.PF_REQ_ACT IN('ICRAL','IBALN','RPYNL')