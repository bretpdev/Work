SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [NAME],
	LN60.LN_SEQ AS LOAN_SEQ,
	LN10.LD_LON_EFF_ADD AS LOAN_ADD_DATE,
	MAX(LN60.LD_FOR_BEG) OVER (PARTITION BY PD10.DF_SPE_ACC_ID, LN60.LN_SEQ) AS DISABILITY_FORB_BEGIN_DATE,
	CL10.LC_REA_CLM_PCL AS CLAIM_TYPE,
	FB10.LD_CRT_REQ_FOR
FROM
	UDW..FB10_BR_FOR_REQ FB10
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = FB10.BF_SSN
	INNER JOIN UDW..LN60_BR_FOR_APV LN60
		ON LN60.BF_SSN = FB10.BF_SSN
		AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN60.BF_SSN
		AND LN10.LN_SEQ = LN60.LN_SEQ
	INNER JOIN UDW..LN40_LON_CLM_PCL LN40
		ON LN40.BF_SSN = LN60.BF_SSN
		AND LN40.LN_SEQ  = LN60.LN_SEQ
	INNER JOIN UDW..CL10_CLM_PCL CL10
		ON CL10.BF_SSN = LN40.BF_SSN
		AND CL10.LN_SEQ_CLM_PCL  = LN40.LN_SEQ_CLM_PCL
WHERE
	LC_FOR_TYP = '14'
	AND LN60.LD_FOR_BEG < LN10.LD_LON_EFF_ADD
	AND FB10.LD_CRT_REQ_FOR <= LN10.LD_LON_EFF_ADD
	AND CL10.LF_CLM_BCH IS NOT NULL
	AND YEAR(CL10.LD_CLM_RQR) >= 2019
ORDER BY
	PD10.DF_SPE_ACC_ID

	