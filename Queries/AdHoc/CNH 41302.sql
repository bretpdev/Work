SELECT
	AYXX.BF_SSN,
	AYXX.LN_ATY_SEQ,
	AYXX.LC_STA_ACTYXX AS OLD_LC_STA_ACTYXX,
	'E' AS NEW_LC_STA_ACTYXX,
	'CURRENT DATE TIME' AS LF_LST_DTS_AYXX
FROM
	CLS..ARCADDPROCESSING AAP
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_SPE_ACC_ID = AAP.ACCOUNTNUMBER
	INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON AYXX.BF_SSN = PDXX.DF_PRS_ID
		AND AYXX.LN_ATY_SEQ = AAP.LN_ATY_SEQ
WHERE SCRIPTID = 'CNH XXXXX' AND AYXX.LC_STA_ACTYXX = 'A'