select DISTINCT POP.*, S.DocId from openquery(DUSTER,
'
SELECT
	AY10.BF_SSN,
	AY10.PF_REQ_ACT,
	AY10.LD_ATY_REQ_RCV
FROM
	OLWHRM1.AY10_BR_LON_ATY AY10
WHERE
	PF_REQ_ACT IN (''DI3RD'',''ASBKP'',''BIBCR'',''ASDIS'',''DIKEY'',''ASMOC'',''DIBCR'',''DISSN'',''LPSJE'',''ASREQ'',''DIDTH'',''DITPD'',''DIINS'',''DIDFR'',''MIDEF'',''DIFRB'',''DITLF'',''IBRDF'',''AMDTH'',''AMDAR'',''AMPCA'',''AMTNS'',''AMFIX'',''AMSUB'',''AMRTN'',''AMCOR'')
AND AY10.LD_ATY_REQ_RCV BETWEEN ''12/03/2016'' AND ''12/08/2016''
AND AY10.LD_ATY_RSP IS NOT NULL
')POP
LEFT JOIN ULS..Sheet1$ S
	ON POP.PF_REQ_ACT = S.CompassOnlyArc
ORDER BY
	POP.BF_SSN