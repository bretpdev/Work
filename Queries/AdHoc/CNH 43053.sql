SELECT 
	LNXX.BF_SSN, 
	LNXX.LN_SEQ, 
	LNXX.IC_LON_PGM, 
	LNXX.LC_WOF_WUP_REA 
FROM 
	cdw..LNXX_LON LNXX 
	INNER JOIN CDW..LNXX_FIN_ATY LNXX 
		ON LNXX.BF_SSN = LNXX.BF_SSN 
		AND LNXX.LN_SEQ = LNXX.LN_SEQ 
		AND LNXX.LC_STA_LONXX = 'A' 
		AND LNXX.PC_FAT_TYP = 'XX' 
		AND LNXX.PC_FAT_SUB_TYP = 'XX'
		AND ISNULL(RTRIM(LNXX.LC_FAT_REV_REA),'') = '' 
WHERE 
	ISNULL(RTRIM(LNXX.LC_WOF_WUP_REA),'') != '' 
	AND LNXX.IC_LON_PGM IN('DLUSPL','DLSSPL') 
	AND CAST(LNXX.LD_FAT_EFF AS DATE) >= 'XXXX-XX-XX'

