(LEGEND, 
'SELECT 
	DISTINCT BF_SSN,
	LD_BIL_CRT 
FROM 
	PKUB.BLXX_BR_BIL WHERE LD_BIL_CRT = ''XXXX-XX-XX''
'
) June LEFT OUTER JOIN OPENQUERY
(LEGEND,
'SELECT 
	DISTINCT BF_SSN,
	LD_BIL_CRT 
FROM 
	PKUB.BLXX_BR_BIL WHERE LD_BIL_CRT >= ''XXXX-XX-XX'' AND LD_BIL_CRT <= ''XXXX-XX-XX''
'
) July ON June.BF_SSN = July.BF_SSN
