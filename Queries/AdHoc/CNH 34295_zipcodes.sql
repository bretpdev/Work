SELECT DISTINCT
	DF_ZIP_CDE
FROM
(
	SELECT DISTINCT 
		DF_ZIP_CDE
		,DC_DOM_ST
		,DI_VLD_ADR
--		,DM_CT
	FROM
		CDW..PDXX_PRS_ADR PDXX
	WHERE
		PDXX.DC_DOM_ST = 'PR'
		AND DI_VLD_ADR = 'Y'

	UNION ALL

	SELECT DISTINCT 
		DF_ZIP_CDE
		,DC_DOM_ST
		,DI_VLD_ADR
	--	,DM_CT
	FROM
		UDW..PDXX_PRS_ADR PDXX
	WHERE
		PDXX.DC_DOM_ST = 'PR'
		AND DI_VLD_ADR = 'Y'
)PRZIPS
WHERE
	DF_ZIP_CDE NOT LIKE '%XXX%'
	AND DF_ZIP_CDE NOT LIKE '%XXX%'
	AND DF_ZIP_CDE NOT LIKE '%XXX%'
ORDER BY
	DF_ZIP_CDE