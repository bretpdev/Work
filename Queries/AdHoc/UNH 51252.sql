SELECT
	*
FROM
	OPENQUERY
	(
		DUSTER,
		'
			SELECT
				COUNT(LN10.LN_SEQ) "LoanCount",
				LN35.IF_OWN
			FROM
				OLWHRM1.LN10_LON LN10
				INNER JOIN OLWHRM1.LN35_LON_OWN LN35 ON LN35.BF_SSN = LN10.BF_SSN AND LN35.LN_SEQ = LN10.LN_SEQ
			WHERE
				LN10.LC_STA_LON10 = ''R''
				AND
				LN35.LD_OWN_EFF_SR <= ''2016-12-31''
				AND
				(
					LN10.LD_PIF_RPT >= ''2016-12-31''
					OR
					LN10.LD_PIF_RPT IS NULL
				)
				AND
				LN35.IF_OWN IN (''826717'',''828476'',''830248'')
			GROUP BY
				LN35.IF_OWN
		'
	) 