 SELECT * into [UDW].[dbo].[UNH_60369] FROM (
	SELECT DISTINCT
		pd10.DF_PRS_ID AS BF_SSN,
		ln10.LN_SEQ,
		DW01.WC_DW_LON_STA,
		bl10.LC_IND_BIL_SNT,
		BL10.LD_BIL_CRT,
		
		ISNULL(LN16.LN_DLQ_MAX,0) AS LN_DLQ_MAX
		--S.*

	FROM
		UDW..PD10_PRS_NME PD10
		INNER JOIN UDW..LN10_LON LN10
			ON PD10.DF_PRS_ID = LN10.BF_SSN
		INNER JOIN  
		(
			SELECT DISTINCT
				*
			FROM 
				UDW..BR30_BR_EFT 
			WHERE 
				BC_SRC_DIR_DBT_APL = 'W'
				and BC_EFT_STA = 'A'
		) S
			ON S.BF_SSN = PD10.DF_PRS_ID
		INNER JOIN UDW..BR30_BR_EFT BR30
			ON BR30.BF_SSN = S.BF_SSN
			AND BR30.BN_EFT_SEQ = S.BN_EFT_SEQ
		INNER JOIN UDW..LN83_EFT_TO_LON LN83
			ON S.BF_SSN = LN83.BF_SSN	
			AND S.BN_EFT_SEQ = LN83.BN_EFT_SEQ
			and LN10.LN_SEQ = LN83.LN_SEQ
		INNER JOIN UDW..DW01_DW_CLC_CLU DW01
			ON DW01.BF_SSN = LN83.BF_SSN
			AND DW01.LN_SEQ = LN83.LN_SEQ
		INNER JOIN
		(
			SELECT distinct
				BL10.BF_SSN,
				LN80.LN_SEQ,
				MAX(BL10.LD_BIL_CRT) AS LD_BIL_CRT,
				MAX(BL10.LN_SEQ_BIL_WI_DTE) AS LN_SEQ_BIL_WI_DTE
			FROM
				UDW..BL10_BR_BIL BL10
				INNER JOIN UDW..LN80_LON_BIL_CRF LN80
					ON LN80.BF_SSN = BL10.BF_SSN
					AND LN80.LD_BIL_CRT = BL10.LD_BIL_CRT
			WHERE
				BL10.LC_STA_BIL10 = 'A'
				AND LN80.LC_STA_LON80 = 'A'
				AND MONTH(BL10.LD_BIL_DU) >= 1
			GROUP BY
				BL10.BF_SSN,
				LN80.LN_SEQ
		) MBILL
			ON MBILL.BF_SSN = LN10.BF_SSN
			AND MBILL.LN_SEQ = LN10.LN_SEQ
		INNER JOIN UDW..BL10_BR_BIL BL10
			ON BL10.BF_SSN = MBILL.BF_SSN
			AND BL10.LD_BIL_CRT = MBILL.LD_BIL_CRT
			AND BL10.LN_SEQ_BIL_WI_DTE = MBILL.LN_SEQ_BIL_WI_DTE
			AND BL10.LC_STA_BIL10 = 'A'
		LEFT JOIN UDW..LN16_LON_DLQ_HST LN16
			ON LN16.BF_SSN = LN10.BF_SSN
			AND LN16.LN_SEQ = LN10.LN_SEQ
			AND LN16.LC_STA_LON16 = '1'
		WHERE
			LC_IND_BIL_SNT = 'G'
			AND month(BL10.LD_BIL_DU) >= 1
			AND S.BC_EFT_STA = 'A'
			and BL10.LD_BIL_CRT > s.BD_EFT_STA
	
		) P





