SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT DISTINCT

LNXX.BF_SSN,
LNXX.LN_SEQ,
LNXX.IC_LON_PGM,
LNXX.LC_WOF_WUP_REA

FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..LNXX_FIN_ATY LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
	--INNER JOIN CDW..PDXX_PRS_DSA PDXX
	--	ON PDXX.DF_PRS_ID = LNXX.BF_SSN
WHERE
	LNXX.IC_LON_PGM IN ('DLUSPL','DLSSPL')
	AND LNXX.PC_FAT_TYP = 'XX'
	AND LNXX.PC_FAT_SUB_TYP = 'XX'
	AND LNXX.LC_STA_LONXX = 'A'
	AND ISNULL(LNXX.LC_FAT_REV_REA,'') = ''
	AND LNXX.LD_FAT_EFF >= 'XX/XX/XXXX'
	--AND LNXX.LC_WOF_WUP_REA = 'V'