SELECT
	AYXX.BF_SSN,
	RTRIM(PDXX.DM_PRS_X) AS FIRST_NAME,
	RTRIM(PDXX.DM_PRS_LST) AS LAST_NAME,
	CASE 
		WHEN AYXX.PF_REQ_ACT = 'DICSK' THEN 'Closed School app received'
		WHEN AYXX.PF_REQ_ACT = 'CSDNY' THEN 'Closed School app denied'
		WHEN AYXX.PF_REQ_ACT = 'ADCSH' THEN 'Closed School app approved'
	END AS APP_TYPE,
	AYXX.LD_ATY_REQ_RCV AS REC_DATE
FROM 
	CDW..AYXX_BR_LON_ATY AYXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE
	AYXX.PF_REQ_ACT IN ('DICSK','CSDNY','ADCSH')
	AND AYXX.LC_STA_ACTYXX = 'A'
	AND AYXX.LD_ATY_REQ_RCV BETWEEN 'XX/XX/XXXX' AND 'XX/XX/XXXX'
ORDER BY 
	AYXX.BF_SSN