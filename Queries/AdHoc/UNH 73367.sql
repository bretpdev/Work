SELECT DISTINCT
	LN54.*
FROM 
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
	INNER JOIN UDW..LN54_LON_BBS LN54
		ON LN54.BF_SSN = LN10.BF_SSN
		AND LN54.LN_SEQ = LN10.LN_SEQ
WHERE
	PD10.DF_SPE_ACC_ID = '4618459163'
	AND LN10.LN_SEQ = 9

