--Find all loans where LNXX.LF_DOE_SCL_ORG = XXXXXXXX or XXXXXXXX

--Output
--Acct #
--Loan seq


SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_SEQ,
	DIFCR.LD_ATY_REQ_RCV AS DIFCR_DATE

FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	LEFT JOIN
		(
			SELECT
				LNXX.BF_SSN,
				LNXX.LN_SEQ,
				AYXX.LD_ATY_REQ_RCV
			FROM
				CDW..LNXX_LON_ATY LNXX
				INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
					ON LNXX.BF_SSN = AYXX.BF_SSN
					AND LNXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
			WHERE
				AYXX.PF_REQ_ACT = 'DIFCR'
		)	DIFCR
			ON LNXX.BF_SSN = DIFCR.BF_SSN
			AND LNXX.LN_SEQ = DIFCR.LN_SEQ
WHERE
	LNXX.LF_DOE_SCL_ORG IN ('XXXXXXXX','XXXXXXXX')
ORDER BY
	DIFCR_DATE,
	DF_SPE_ACC_ID,
	LN_SEQ