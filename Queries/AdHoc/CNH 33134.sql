SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	WQXX.WF_QUE,
	WQXX.WF_SUB_QUE
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..WQXX_TSK_QUE WQXX
		ON WQXX.BF_SSN = PDXX.DF_PRS_ID
		AND WQXX.WF_QUE IN('DR','RB')
		AND WQXX.WC_STA_WQUEXX NOT IN('X','C')
	INNER JOIN CDW..LNXX_LON_DLQ_HST LNXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
		AND LNXX.LC_STA_LONXX = 'X'
	