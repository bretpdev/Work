SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	LTRIM(RTRIM(DM_PRS_1)) + ' ' +LTRIM(RTRIM(DM_PRS_LST))  AS NAME
FROM
	UDW..LN65_LON_RPS LN65
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN65.BF_SSN
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN65.BF_SSN
		AND LN10.LN_SEQ = LN65.LN_SEQ
WHERE
	LN65.LC_STA_LON65 = 'A'
	AND LN65.LN_RPS_SEQ = 1
	AND LN65.LC_TYP_SCH_DIS = 'EG'
	and LN10.LC_STA_LON10 = 'R'
	AND LN10.LA_CUR_PRI > 0