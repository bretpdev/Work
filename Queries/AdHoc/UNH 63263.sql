USE UDW
GO

SELECT
	PD10.DF_SPE_ACC_ID,
	LN50.LN_SEQ,
	DF10.LD_CRT_REQ_DFR,
	LN50.LD_DFR_BEG,
	LN50.LD_DFR_END
FROM
	UDW..DF10_BR_DFR_REQ DF10
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN50.BF_SSN = DF10.BF_SSN
		AND LN50.LF_DFR_CTL_NUM = DF10.LF_DFR_CTL_NUM
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DF10.BF_SSN
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN50.BF_SSN
		AND LN10.LN_SEQ = LN50.LN_SEQ
WHERE
	DF10.LC_DFR_TYP = '13'
	AND YEAR(DF10.LD_CRT_REQ_DFR) = 2019
	AND LN50.LC_STA_LON50 = 'A'
	AND DF10.LC_STA_DFR10 = 'A'
	AND DF10.LC_DFR_STA = 'A'
	AND LN50.LD_DFR_BEG > LN10.LD_LON_EFF_ADD
