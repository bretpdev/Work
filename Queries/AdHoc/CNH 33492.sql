select * from openquery(legend,
'
SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_SEQ,
	LNXX.LC_TYP_SCH_DIS
FROM
	PKUB.LNXX_LON_RPS LNXX
	INNER JOIN PKUB.PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
WHERE
	LNXX.LC_STA_LONXX = ''A''
	AND LNXX.LC_TYP_SCH_DIS IN (''CX'',''CX'')
		

')