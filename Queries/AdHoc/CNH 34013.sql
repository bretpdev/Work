SELECT * FROM OPENQUERY(LEGEND,
'
SELECT DISTINCT
	LNXX.BF_SSN,
	LNXX.LN_SEQ
FROM	
	PKUB.LNXX_LON LNXX
	INNER JOIN PKUB.LNXX_FIN_ATY LNXX
		ON LNXX.BF_SSN  = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
	INNER JOIN PKUB.PDXX_PRS_DSA PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
WHERE
	LNXX.PC_FAT_TYP = ''XX'' 
	AND PC_FAT_SUB_TYP = ''XX''
	AND LNXX.LC_STA_LONXX = ''A''
	AND LNXX.LC_FAT_REV_REA != ''X''
	AND LNXX.IC_LON_PGM IN (''DLSSPL'', ''DLUSPL'')
	AND PDXX.DX_PRS_DSA_TPD_REA = ''APPAPPR''
')