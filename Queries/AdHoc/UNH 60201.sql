SELECT DISTINCT
	 PD10.DF_SPE_ACC_ID
	,LN10.LN_SEQ
	,LN35.IF_OWN
	,LN35.IF_BND_ISS
	,LN10.IF_GTR
FROM
	LN10_LON LN10
	INNER JOIN LN35_LON_OWN LN35
		ON LN10.BF_SSN = LN35.BF_SSN
		AND LN10.LN_SEQ = LN35.LN_SEQ
	INNER JOIN PD10_PRS_NME PD10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
WHERE
	LN35.IF_OWN = '828476'
	AND LN35.IF_BND_ISS = '93002011'
	AND LN10.IF_GTR NOT IN ('000749','000800')
ORDER BY
	PD10.DF_SPE_ACC_ID
;
