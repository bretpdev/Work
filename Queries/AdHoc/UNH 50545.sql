SELECT
	*
FROM
	OPENQUERY
	(
		DUSTER,
		'
			SELECT
				LN80.BF_SSN,
				LN80.LN_SEQ,
				LN80.LD_BIL_CRT,
				LN80.LN_SEQ_BIL_WI_DTE,
				LN80.LN_BIL_OCC_SEQ,
				LN80.LI_FNL_BIL_LON
			FROM
				OLWHRM1.PD10_PRS_NME PD10
				INNER JOIN OLWHRM1.LN80_LON_BIL_CRF LN80 ON LN80.BF_SSN = PD10.DF_PRS_ID
			WHERE
				PD10.DF_SPE_ACC_ID IN
				(
					''5536402011'',
					''5415578392'',
					''9145817748'',
					''5431186071''
				)
			ORDER BY
				PD10.DF_PRS_ID,
				LN80.LN_SEQ,
				LN80.LD_BIL_CRT
		'
	)
