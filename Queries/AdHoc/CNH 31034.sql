SELECT * FROM OPENQUERY (LEGEND,'
	SELECT DISTINCT
		LNXX.BF_SSN			AS SSN
		,TRIM(PDXX.DM_PRS_X) || '' ''  || TRIM(PDXX.DM_PRS_LST) AS Name
		,LNXX.IC_LON_PGM	AS Loan_type
		,LNXX.LD_CRT_LONXX	AS REPAYE_Approval_date
		,LNXX.LC_TYP_SCH_DIS AS IDR_type
		,LNXX.LC_STA_LONXX
	FROM
		PKUB.LNXX_LON LNXX
		INNER JOIN PKUB.PDXX_PRS_NME PDXX
			ON LNXX.BF_SSN = PDXX.DF_PRS_ID
		INNER JOIN PKUB.LNXX_LON_RPS LNXX
			ON LNXX.BF_SSN = LNXX.BF_SSN
			AND LNXX.LN_SEQ = LNXX.LN_SEQ
	WHERE
		LNXX.LC_TYP_SCH_DIS = ''IX''
		AND LNXX.LC_STA_LONXX = ''A''
');