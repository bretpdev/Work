SELECT DISTINCT
	--PD10.DF_SPE_ACC_ID AS [Account Number],
	WQ.BF_SSN AS SSN,
	LN10.LN_SEQ AS [Loan Sequence],
	LN10.LA_CUR_PRI AS [Principal Balance],
	DW01.LA_NSI_OTS + DW01.WA_TOT_BRI_OTS AS [Outstanding Interest],
	WQ.WN_CTL_TSK AS [Task #],
	WQ.WF_QUE [Queue],
	WQ.WC_STA_WQUE20 AS [Status]
FROM
	UDW..WQ20_TSK_QUE WQ
	INNER JOIN UDW..LN85_LON_ATY LN85
		ON WQ.BF_SSN = LN85.BF_SSN
		AND WQ.LN_ATY_SEQ = LN85.LN_ATY_SEQ
	INNER JOIN UDW..LN10_LON LN10
		ON LN85.BF_SSN = LN10.BF_SSN
		AND LN85.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON LN10.BF_SSN = DW01.BF_SSN
		AND LN10.LN_SEQ = DW01.LN_SEQ
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON WQ.BF_SSN = PD10.DF_PRS_ID
WHERE
	WQ.WF_QUE = 'HV'
	AND WQ.WC_STA_WQUE20 NOT IN ('X', 'C')
	AND LN10.LA_CUR_PRI <= 0.00
	AND DW01.LA_NSI_OTS <= 0.00
	AND DW01.WA_TOT_BRI_OTS <= 0.00
ORDER BY
	--PD10.DF_SPE_ACC_ID,
	WQ.BF_SSN,
	LN10.LN_SEQ