'
SELECT 
	LNXXI.BF_SSN,
	LNXXI.LN_SEQ,
	LNXXI.LN_CAP_SEQ,
	LNXXI.LD_CAP_END,
	LNXXI.LC_CAP_TYP,
	LNXXI.LD_CAP_BEG,
	LNXXI.LC_STA_LONXX,
	LNXXI.LF_LST_DTS_LNXX,
	LNXXI.LA_CAP,
	LNXXA.BF_SSN AS A_BF_SSN,
	LNXXA.LN_SEQ AS A_LN_SEQ,
	LNXXA.LN_CAP_SEQ AS A_LN_CAP_SEQ,
	LNXXA.LD_CAP_END AS A_LD_CAP_END,
	LNXXA.LC_CAP_TYP AS A_CAP_TYP,
	LNXXA.LD_CAP_BEG AS A_LD_CAP_BEG,
	LNXXA.LC_STA_LONXX AS A_LC_STA_LONXX,
	LNXXA.LF_LST_DTS_LNXX AS A_LF_LST_DTS_LNXX,
	LNXXA.LA_CAP AS A_LA_CAP
FROM 
	PKUB.LNXX_LON_CAP_ATY LNXXI
	INNER JOIN PKUB.LNXX_LON_CAP_ATY LNXXA
		ON LNXXI.BF_SSN = LNXXA.BF_SSN
		AND LNXXI.LN_SEQ = LNXXa.LN_SEQ
		AND LNXXI.LC_STA_LONXX = ''A''
		AND  CAST(LNXXI.LF_LST_DTS_LNXX AS DATE) = ''XX/XX/XXXX''
		
WHERE 
	LNXXA.LC_STA_LONXX = ''I''
	AND  CAST(LNXXA.LF_LST_DTS_LNXX AS DATE) = ''XX/XX/XXXX''
')