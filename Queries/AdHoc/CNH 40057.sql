SELECT DISTINCT TOP X
	LTXX.DF_SPE_ACC_ID,
	LTXX.RM_DSC_LTR_PRC,
	(LNXX.LN_DLQ_MAX + X) DAYS_PAST_DUE,
	LTXX.CreatedAt as letter_sent_date
FROM
	CDW..LTXX_LTR_REQ_PRC LTXX
	INNER JOIN CDW..LNXX_LON_DLQ_HST LNXX
		ON LTXX.RF_SBJ_PRC = LNXX.BF_SSN
		AND LNXX.LC_STA_LONXX = 'X' --ACTIVE
WHERE
	RM_DSC_LTR_PRC = 'TSXXBFDLP'
	AND InactivatedAt IS NULL
	AND CreatedAt BETWEEN 'XX/XX/XXXX' AND 'XX/XX/XXXX'
ORDER BY 
	CreatedAt DESC