SELECT DISTINCT
	IBIL1.*
FROM
	UDW..RS10_BR_RPD RS10
	INNER JOIN UDW..LN65_LON_RPS IBIL1
		ON IBIL1.BF_SSN = RS10.BF_SSN
		AND IBIL1.LN_RPS_SEQ = RS10.LN_RPS_SEQ
		AND IBIL1.LC_TYP_SCH_DIS IN ('IB', 'IL')
	INNER JOIN UDW..LN65_LON_RPS L1
		ON L1.BF_SSN = IBIL1.BF_SSN
		AND L1.LN_SEQ = IBIL1.LN_SEQ
		AND L1.LN_RPS_SEQ = IBIL1.LN_RPS_SEQ + 1
		AND L1.LC_TYP_SCH_DIS IN ('L')
	INNER JOIN UDW..LN65_LON_RPS IBIL2
		ON IBIL2.BF_SSN = IBIL1.BF_SSN
		AND IBIL2.LN_SEQ = IBIL2.LN_SEQ
		AND IBIL2.LN_RPS_SEQ = L1.LN_RPS_SEQ + 1
		AND IBIL2.LC_TYP_SCH_DIS IN ('IB', 'IL')
WHERE
	RS10.LD_STA_RPST10 IN ('3/4/2019','3/5/2019')

UNION ALL 

