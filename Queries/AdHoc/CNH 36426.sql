SELECT DISTINCT
	PDXX.DM_PRS_X AS [First Name],
	PDXX.DM_PRS_LST AS [Last Name],
	LNXX.BF_SSN AS SSN,
	AYXX.PF_REQ_ACT AS DIFCR
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..LNXX_LON LNXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	INNER JOIN CDW..LNXX_LON_ATY LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
	INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON LNXX.BF_SSN = AYXX.BF_SSN
		AND LNXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
WHERE
	LNXX.LF_DOE_SCL_ORG IN ('XXXXXXXX', 'XXXXXXXX', 'XXXXXXXX', 'XXXXXXXX')
	AND AYXX.PF_REQ_ACT = 'DIFCR'
	AND AYXX.LD_ATY_REQ_RCV BETWEEN 'XXXX-XX-XX' AND 'XXXX-XX-XX'