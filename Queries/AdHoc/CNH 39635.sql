	AYXX.PF_REQ_ACT = 'DIFCR'
	AND AYXX.LC_STA_ACTYXX = 'A'
	AND LNXX.LF_DOE_SCL_ORG IN ('XXXXXXXX', 'XXXXXXXX', 'XXXXXXXX', 'XXXXXXXX')
