SELECT
	PF_ACT AS ACTION_CODE,
	COUNT(*) AS NUMBER_OF_TIMES_USED
FROM
	ODW..AY01_BR_ATY
WHERE
	PF_ACT IN 
	(
		'DL30D',
		'DLOOW',
		'DRHBA',
		'DRWTN',
		'DWFRQ',
		'DWFEM',
		'DD136',
		'QRSTR',
		'DD023',
		'DD138'
	)
	AND BF_CRT_DTS_AY01 BETWEEN '3/1/2019' AND '02/28/2020'
GROUP BY
	PF_ACT