SELECT DISTINCT
	PD01.DF_SPE_ACC_ID
FROM
	ODW..AY01_BR_ATY AY01
	INNER JOIN ODW..PD01_PDM_INF PD01
		ON PD01.DF_PRS_ID = AY01.DF_PRS_ID
	INNER JOIN ODW..DC02_BAL_INT DC02
		ON DC02.BF_SSN = AY01.DF_PRS_ID
		AND DC02.LA_CLM_BAL > 0.00
	INNER JOIN 
	( 
		SELECT
			*
		FROM
			ODW..DC11_LON_FAT
		WHERE 
			LC_TRX_TYP IN ('EP', 'GP', 'SO', 'FO')
			AND LC_REV_IND_TYP != ''
			AND LD_TRX_ADJ >= '03-13-2020'
	) PMT
		ON PMT.BF_SSN = AY01.DF_PRS_ID
WHERE 
	PF_ACT in ('DPIFR','NPIFQ')
	AND BD_ATY_PRF >= '01-13-2020'
