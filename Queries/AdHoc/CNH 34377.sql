USE CDW
GO

SELECT 
	PF_REQ_ACT,
	MONTH(LD_ATY_REQ_RCV) AS [MONTH],
	YEAR(LD_ATY_REQ_RCV) AS [YEAR],
	COUNT(*) AS [COUNT]
FROM
	CDW..AYXX_BR_LON_ATY
WHERE
	PF_REQ_ACT IN ('CODCA','CODPA','RPSFM','WREGN','WREGT','WREGX','WRELN','WRELT','WRELX','WRGRT','WRGRX','WRSTT','GXXXX','GXXXC','GXXXX' )
	AND LC_STA_ACTYXX = 'A'
	AND LD_ATY_REQ_RCV BETWEEN 'XX/XX/XXXX' AND 'XX/XX/XXXX'
GROUP BY
	PF_REQ_ACT,
	MONTH(LD_ATY_REQ_RCV),
	YEAR(LD_ATY_REQ_RCV)
ORDER BY
	PF_REQ_ACT,
	YEAR(LD_ATY_REQ_RCV),
	MONTH(LD_ATY_REQ_RCV)