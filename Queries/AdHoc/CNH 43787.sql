	DWXX.WC_DW_LON_STA,
	FSXX.LN_SEQ,
	RSXX.*
FROM
	@DATA D
	INNER JOIN CDW..FSXX_DL_LON FSXX
		ON FSXX.BF_SSN = D.SSN
		AND FSXX.LF_FED_AWD = D.AWARD_ID
		AND FSXX.LN_FED_AWD_SEQ = D.AWARD_ID_SEQ
	INNER JOIN CDW..DWXX_DW_CLC_CLU DWXX
		ON DWXX.BF_SSN = FSXX.BF_SSN
		AND DWXX.LN_SEQ = FSXX.LN_SEQ
	LEFT JOIN 
	(
		SELECT DISTINCT
			RSXX.*,
			MAX_RPS.LN_SEQ
		FROM
			CDW..RSXX_BR_RPD RSXX
			INNER JOIN 
			(
				SELECT
					LNXX.BF_SSN,
					LNXX.LN_SEQ,
					MAX(LNXX.LN_RPS_SEQ) AS LN_RPS_SEQ
				FROM
					@DATA D
					INNER JOIN CDW..FSXX_DL_LON FSXX
						ON FSXX.BF_SSN = D.SSN
						AND FSXX.LF_FED_AWD = D.AWARD_ID
						AND FSXX.LN_FED_AWD_SEQ = D.AWARD_ID_SEQ
