SELECT * FROM OPENQUERY (LEGEND,'
	SELECT DISTINCT
		 PDXX.DF_SPE_ACC_ID
		,LNXX.LN_SEQ
		,LNXX.LC_STA_LONXX
		,LNXX.PC_FAT_TYP
		,LNXX.PC_FAT_SUB_TYP
		,LNXX.LD_LON_EFF_ADD
		,LNXX.LD_LON_X_DSB
	FROM
		PKUB.LNXX_LON LNXX
		INNER JOIN PKUB.LNXX_FIN_ATY LNXX
			ON LNXX.BF_SSN = LNXX.BF_SSN
			--AND LNXX.LN_SEQ = LNXX.LN_SEQ
		INNER JOIN PKUB.PDXX_PRS_NME PDXX
			ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	WHERE
		LNXX.PC_FAT_TYP = ''XX''
		AND LNXX.PC_FAT_SUB_TYP = ''XX''
		AND DAYS(LNXX.LD_LON_EFF_ADD) BETWEEN DAYS(''XXXX-XX-XX'') AND DAYS(CURRENT DATE)
		AND DAYS(LNXX.LD_LON_X_DSB) BETWEEN DAYS(''XXXX-XX-XX'') AND DAYS(''XXXX-XX-XX'')
');