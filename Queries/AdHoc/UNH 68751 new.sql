SELECT DISTINCT
	COUNT(DISTINCT LN10.BF_SSN) AS BorrowerCount,
	COUNT(*) AS LoanCount,
	LN35.IF_BND_ISS,
	SUM(LN10.LA_CUR_PRI) AS Balance 
FROM 
	UDW..LN35_LON_OWN LN35 
	INNER JOIN UDW..LN10_LON LN10 
		ON LN10.BF_SSN = LN35.BF_SSN 
		AND LN10.LN_SEQ = LN35.LN_SEQ
WHERE 
	LN35.IF_BND_ISS IN ('93002011','9301999O','9302001R','9302001S','932006DD','932010EE')
	AND LN10.LC_STA_LON10 = 'R'
	AND LN10.LA_CUR_PRI > 0.00
	AND LN35.LC_STA_LON35 = 'A'
	AND GETDATE() BETWEEN LN35.LD_OWN_EFF_SR AND ISNULL(LN35.LD_OWN_EFF_END, '01-01-2099')
GROUP BY
	LN35.IF_BND_ISS