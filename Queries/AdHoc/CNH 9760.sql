SELECT 
	OQ.BF_SSN,
	OQ.LN_SEQ,
	CONVERT(VARCHAR,OQ.LF_LST_DTS_LNXX,XXX) AS LF_LST_DTS_LNXX
FROM CDW..[CNH XXXX] CNH
INNER JOIN OPENQUERY
(
LEGEND,
'
	SELECT
		BF_SSN,
		LN_SEQ,
		LF_LST_DTS_LNXX
	FROM
		PKUB.LNXX_LON
'
) OQ 
	ON OQ.BF_SSN = CNH.BF_SSN
	AND OQ.LN_SEQ = CNH.LN_SEQ
