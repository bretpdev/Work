SELECT
DATENAME(MONTH,LN90.LD_FAT_EFF) AS [Month],
COUNT(*) as Payment_Count
FROM
(
	SELECT DISTINCT
		LN90.LD_FAT_EFF,
		BF_SSN,
		LN_FAT_SEQ
	FROM
		UDW..LN90_FIN_ATY LN90
	WHERE
		LN90.LC_STA_LON90 = 'A'
		AND ISNULL(LN90.LC_FAT_REV_REA,'') = ''
		AND LN90.PC_FAT_TYP = '10'
		AND LN90.PC_FAT_SUB_TYP IN ('10', '30')
		AND LN90.LD_FAT_EFF > '07/01/2019'
) LN90
GROUP BY
	
	DATENAME(MONTH,LN90.LD_FAT_EFF),
	MONTH(LN90.LD_FAT_EFF) 
ORDER BY 
	MONTH(LN90.LD_FAT_EFF) 