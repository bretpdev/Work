BEGIN TRANSACTION
	DECLARE @ERROR INT = X
	DECLARE @ROWCOUNT INT = X
	DECLARE @ExpectedRowCount INT = XXXX
	DECLARE @Date DATE = 'XXXX-XX-XX'

--XXX
UPDATE
	PP
SET
	PP.PrintedAt = NULL
FROM
	CLS.[print].PrintProcessing PP
	INNER JOIN CLS.[print].ScriptData SD
		ON SD.ScriptDataId = PP.ScriptDataId
	INNER JOIN CLS.[print].Letters L
		ON L.LetterId = SD.LetterId
WHERE
	L.Letter IN('APAPPRXFED','APBNKFED','APDNYFED','APREQFED','CBPCNFFED','FORAPPREC','IBRDENFED','IDRAPRCVD','IDRPNDD','IDRPNDO','IDRPNDS','LDAFCXFED','PLRPYMTFED','REQRCVFED','RPAYDENFED','RPDISCFED')
	AND CAST(PP.PrintedAt AS DATE) = @Date

--X
UPDATE
	PP
SET
	PP.PrintedAt = NULL
FROM
	CLS.[print].PrintProcessingCoborrower PP
	INNER JOIN CLS.[print].ScriptData SD
		ON SD.ScriptDataId = PP.ScriptDataId
	INNER JOIN CLS.[print].Letters L
		ON L.LetterId = SD.LetterId
WHERE
	L.Letter IN('APAPPRXFED','APBNKFED','APDNYFED','APREQFED','CBPCNFFED','FORAPPREC','IBRDENFED','IDRAPRCVD','IDRPNDD','IDRPNDO','IDRPNDS','LDAFCXFED','PLRPYMTFED','REQRCVFED','RPAYDENFED','RPDISCFED')
	AND CAST(PP.PrintedAt AS DATE) = @Date

--XXX
UPDATE
	LTXX
SET
	LTXX.PrintedAt = NULL
FROM
	CDW..LTXX_LTR_REQ_PRC LTXX
WHERE
	LTXX.RM_DSC_LTR_PRC IN
	(
		'TSXXBAPIDR','TSXXBBAPP','TSXXBBCANC','TSXXBCARQT','TSXXBDXXX','TSXXBDXXX','TSXXBDDXX','TSXXBDDXX','TSXXBDDXXX','TSXXBDDXX','TSXXBDDXX','TSXXBDDIDR','TSXXBDHRD','TSXXBDMP','TSXXBDSCH',
		'TSXXBDUNEM','TSXXBFXXX','TSXXBFXXXC','TSXXBFXXXC','TSXXBFXXX','TSXXBIBR','TSXXBIDREC','TSXXBRPYXX','TSXXBRSMRY','TSXXBSCRAE','TSXXBTMXLM','TSXXBTPDSS','TSXXBTRTX','TSXXBTSA',
		'TSXXIDMDBR','TSXXIDTINB','TSXXBFDLP','TSXXBISF','TSXXSKPREF'
	)
	AND CAST(LTXX.PrintedAt AS DATE) = @Date

--X
UPDATE
	LTXX
SET
	LTXX.PrintedAt = NULL
FROM
	CDW..LTXX_LTR_REQ_PRC_Coborrower LTXX
WHERE
	LTXX.RM_DSC_LTR_PRC IN
	(
		'TSXXBAPIDR','TSXXBBAPP','TSXXBBCANC','TSXXBCARQT','TSXXBDXXX','TSXXBDXXX','TSXXBDDXX','TSXXBDDXX','TSXXBDDXXX','TSXXBDDXX','TSXXBDDXX','TSXXBDDIDR','TSXXBDHRD','TSXXBDMP','TSXXBDSCH',
		'TSXXBDUNEM','TSXXBFXXX','TSXXBFXXXC','TSXXBFXXXC','TSXXBFXXX','TSXXBIBR','TSXXBIDREC','TSXXBRPYXX','TSXXBRSMRY','TSXXBSCRAE','TSXXBTMXLM','TSXXBTPDSS','TSXXBTRTX','TSXXBTSA',
		'TSXXIDMDBR','TSXXIDTINB','TSXXBFDLP','TSXXBISF','TSXXSKPREF'
	)
	AND CAST(LTXX.PrintedAt AS DATE) = @Date

SELECT @ROWCOUNT = @@ROWCOUNT, @ERROR = @@ERROR

IF @ROWCOUNT = @ExpectedRowCount AND @ERROR = X
	BEGIN
		PRINT 'Transaction committed'
		COMMIT TRANSACTION
		--ROLLBACK TRANSACTION
	END
ELSE
	BEGIN
		PRINT 'ROWCOUNT:  ' + CAST(@ROWCOUNT AS VARCHAR(XX))
		PRINT 'ERROR:  ' + CAST(@ERROR AS VARCHAR(XX))
		PRINT 'Transaction NOT committed'
		ROLLBACK TRANSACTION
	END