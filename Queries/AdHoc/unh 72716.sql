SELECT
	PD10.DF_SPE_ACC_ID AS AccountNumber,
	WQ21.WF_QUE AS [Queue],
	WQ21.WF_SUB_QUE AS SubQueue,
	WQ21.WF_USR_ASN_TSK AS AgentID,
	SUBSTRING(WQ21.WX_MSG_2_TSK,19,8) AS OPEID,
	CONVERT(VARCHAR, CAST(SUBSTRING(WQ21.WX_MSG_2_TSK,37,8) AS DATE), 101) AS ESP_CertificationDate,
	CONVERT(VARCHAR, CAST(SUBSTRING(WQ21.WX_MSG_2_TSK,45,8) AS DATE), 101) AS EnrollmentBeginDate,
	'' as ACD,
	ISNULL(CONVERT(VARCHAR, CASE WHEN ISDATE(SUBSTRING(WQ21.WX_MSG_2_TSK,29,8)) = 0
		THEN  NULL
		ELSE CAST(SUBSTRING(WQ21.WX_MSG_2_TSK,29,8) AS DATE)
	END, 101), 'N/A') AS ESP_SeparationDate

	--WQ21.*
FROM
	UDW..WQ21_TSK_QUE_HST WQ21
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ21.BF_SSN
WHERE
	WF_QUE = 'RB'
	AND WF_CRT_DTS_WQ21 BETWEEN '09/01/2021' AND '09/30/2021'
	AND WC_STA_WQUE20 IN ('C','X')