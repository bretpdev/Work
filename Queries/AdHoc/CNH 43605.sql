DECLARE @BEGIN DATE = 'XX/XX/XXXX'
DECLARE @END DATE = 'XX/XX/XXXX'

SELECT DISTINCT
 PDXX.DF_SPE_ACC_ID,
 LNXX.LN_SEQ,
 LNXX.LD_LON_ACL_ADD --actual loan add date (matches ln add dt in TSXX)
FROM
 CDW..LNXX_LON LNXX
 INNER JOIN CDW..LNXX_FIN_ATY LNXX
  ON LNXX.BF_SSN = LNXX.BF_SSN
  AND LNXX.LN_SEQ = LNXX.LN_SEQ
 INNER JOIN CDW..PDXX_PRS_NME PDXX
      ON LNXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
 LNXX.PC_FAT_TYP = 'XX' --conversion
 AND LNXX.PC_FAT_SUB_TYP IN ('XX','XX') --purchase, non-purchase
 AND LNXX.LD_FAT_EFF BETWEEN @BEGIN AND @END
 AND LNXX.LC_STA_LONXX = 'A' --active
 AND ISNULL(LNXX.LC_FAT_REV_REA, '') = '' --not reversed 
