SELECT
	A.FX AS SSN,
	A.FX AS APRIL_INTEREST,
	M.FX AS MAY_INTEREST,
	LNXX.*
FROM
	CDW..CNHXXXXX_APRIL_INVOICE_X A
	INNER JOIN CDW..CNHXXXXX_MAY_INVOICE_X M
		ON A.FX = M.FX
	inner JOIN
	(
		SELECT	
			*
		FROM
			CDW..LNXX_FIN_ATY LNXX
			--INNER JOIN CDW..CNHXXXXX_APRIL_INVOICE_X A
			--	ON A.FX = LNXX.BF_SSN
		WHERE
			LD_FAT_EFF >= 'XX/XX/XXXX'
			AND PC_FAT_TYP = 'XX'
			AND PC_FAT_SUB_TYP = 'XX'
			AND LC_STA_LONXX = 'A'
			AND ISNULL(LC_FAT_REV_REA,'') = ''
	) LNXX
		ON LNXX.BF_SSN = A.FX
--WHERE
