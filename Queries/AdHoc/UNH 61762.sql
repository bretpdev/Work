SELECT
	LN65.BF_SSN,
	LN65.LN_SEQ,
	LN65.LN_RPS_SEQ,
	LN65.LC_STA_LON65,
	LN65.LC_TYP_SCH_DIS,
	[Level].LN_RPS_SEQ AS Level_LN_RPS_SEQ,
	[Level].LC_STA_LON65 AS Level_LC_STA_LON65,
	[Level].LC_TYP_SCH_DIS AS Level_LC_TYP_SCH_DIS
FROM
	UDW..LN65_LON_RPS LN65
	INNER JOIN UDW..LN65_LON_RPS [Level]
		ON [Level].BF_SSN = LN65.BF_SSN
		AND [Level].LN_SEQ = LN65.LN_SEQ
		AND [Level].LF_LST_DTS_LN65 >= LN65.LF_LST_DTS_LN65
		AND [Level].LC_TYP_SCH_DIS = 'L'

	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN65.BF_SSN
		AND LN10.LN_SEQ = LN65.LN_SEQ
WHERE
	LN65.LC_TYP_SCH_DIS IN('IB','IL')
	AND CAST(LN65.LF_LST_DTS_LN65 AS DATE) IN ('2019-03-04', '2019-03-05')
	AND LN10.LA_CUR_PRI > 0.00
	AND LN10.LC_STA_LON10 = 'R'

