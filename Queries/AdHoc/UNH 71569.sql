USE UDW;
GO

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;
SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	P.PF_REQ_ACT,
	P.LD_ATY_REQ_RCV,
	P.LX_ATY
FROM
(
SELECT DISTINCT
	AY20.BF_SSN,
	AY10.PF_REQ_ACT,
	AY10.LD_ATY_REQ_RCV,
	STUFF(
	(
		SELECT 
			' ' + SUB.LX_ATY AS [text()]
		FROM 
			AY20_ATY_TXT SUB
		WHERE
			SUB.BF_SSN = AY20.BF_SSN
			AND SUB.LN_ATY_SEQ = AY20.LN_ATY_SEQ
	FOR XML PATH('')
	)
	,1,1, '') AS LX_ATY
FROM	
	AY10_BR_LON_ATY AY10
	INNER JOIN AY15_ATY_CMT AY15
		ON AY10.BF_SSN = AY15.BF_SSN
		AND AY10.LN_ATY_SEQ = AY15.LN_ATY_SEQ
	INNER JOIN AY20_ATY_TXT AY20
		ON AY15.BF_SSN = AY20.BF_SSN
		AND AY15.LN_ATY_SEQ = AY20.LN_ATY_SEQ
		AND AY15.LN_ATY_CMT_SEQ = AY20.LN_ATY_CMT_SEQ
	INNER JOIN PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = AY20.BF_SSN
WHERE
	AY10.PF_REQ_ACT = 'P200C'
	AND AY10.LC_STA_ACTY10 = 'A'
	AND AY15.LC_STA_AY15 = 'A'
	AND AY10.LD_ATY_REQ_RCV BETWEEN '01/01/2019' and '12/31/2020'
	AND (
			AY20.LX_ATY LIKE '%LOWER PAYMENT%'
			OR AY20.LX_ATY LIKE '%CANT MAKE PAYMENT%'
			OR AY20.LX_ATY LIKE '%PAST DUE%'
			OR AY20.LX_ATY LIKE '%DELQ%'
			OR AY20.LX_ATY LIKE '%UNABLE TO MAKE PAYMENT%'
			OR AY20.LX_ATY LIKE '%FIN DIFF%'
		)
) P
INNER JOIN UDW..PD10_PRS_NME PD10
	ON PD10.DF_PRS_ID = P.BF_SSN
	


