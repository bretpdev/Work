SELECT
	COUNT(BF_SSN) AS ACCOUNT_COUNT,
	AVG(BALANCE) AS AVG_BAL
FROM
	(
		SELECT
			BF_SSN,
			SUM(GA10.AA_CUR_PRI + GA10.AA_OTS_ACR_INT + DC01.LA_LEG_CST_ACR - DC01.LA_LEG_CST_COL + DC01.LA_OTH_CHR_ACR - DC01.LA_OTH_CHR_COL + DC01.LA_COL_CST_ACR - DC01.LA_COL_CST_COL) AS BALANCE
		FROM
			ODW..DC01_LON_CLM_INF DC01
			INNER JOIN ODW..GA10_LON_APP GA10
				ON GA10.AF_APL_ID = DC01.AF_APL_ID
				AND GA10.AF_APL_ID_SFX = DC01.AF_APL_ID_SFX
		WHERE
			LC_STA_DC10 = '03'
			AND GA10.AA_CUR_PRI > 0.00
			AND RTRIM(DC01.LC_REA_CLM_ASN_DOE) = ''
		GROUP BY
			BF_SSN
	) DEFAULT_PORTFOLIO

