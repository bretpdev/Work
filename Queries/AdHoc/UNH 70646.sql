DELETE D  
FROM @DATA D
LEFT JOIN
(
SELECT
	LN10.BF_SSN,
	SUM(LA_CUR_PRI) AS BAL
FROM
	UDW..LN10_LON LN10
	INNER JOIN @DATA D
		ON D.bf_ssn = LN10.BF_SSN
GROUP BY
	LN10.BF_SSN
) BAL
	ON BAL.BF_SSN = D.BF_SSN
WHERE BAL.BAL = 0

drop table if exists #has_def

--select count(distinct bf_ssn) from @data
select distinct
	d.bf_ssn
into #has_def
from 
	@data d
	inner join udw..LN10_LON ln10
		on ln10.bf_ssn = d.bf_ssn
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN10.BF_SSN = LN50.BF_SSN
		AND LN10.LN_SEQ = LN50.LN_SEQ
	INNER JOIN UDW..DF10_BR_DFR_REQ DF10
		ON LN50.BF_SSN = DF10.BF_SSN
		AND LN50.LF_DFR_CTL_NUM = DF10.LF_DFR_CTL_NUM
where 
 getdate() BETWEEN ln50.LD_DFR_BEG  AND ln50.LD_DFR_end
AND LN50.LC_DFR_RSP != '003' --not denied
AND LN50.LC_STA_LON50 = 'A'
AND DF10.LC_DFR_STA = 'A'
AND DF10.LC_STA_DFR10 = 'A'
and ln10.LA_CUR_PRI > 0
and ln10.LC_STA_LON10 ='r'

select count(distinct bf_ssn) AS TOTAL_COUNT from @data
select count(distinct bf_ssn) AS STILL_IN_DEF from #has_def
SELECT [STATUS], COUNT(*) AS STATUS_COUNT FROM
(
select distinct
	d.bf_ssn,
	lk10.PX_DSC_MDM AS [STATUS]
from 
	@data d
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = D.BF_SSN
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = LN10.BF_SSN
		AND DW01.LN_SEQ = LN10.LN_SEQ
	INNER JOIN UDW..LK10_LS_CDE_LKP LK10
		on lk10.PM_ATR = 'WC-DW-LON-STA'
		and lk10.PX_ATR_VAL = dw01.WC_DW_LON_STA
	left join #has_def hd
		on hd.bf_ssn = d.bf_ssn

where hd.bf_ssn is null
and ln10.LA_CUR_PRI > 0
and ln10.LC_STA_LON10 ='r'
) P
GROUP BY
	P.[STATUS]

	
