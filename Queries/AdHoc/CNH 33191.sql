SELECT
	PDXX.DF_SPE_ACC_ID,
	AYXX.PF_REQ_ACT,
	CONVERT(VARCHAR, AYXX.LD_ATY_REQ_RCV, XXX) AS  LD_ATY_REQ_RCV
FROM
	CDW..AYXX_BR_LON_ATY AYXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE
	AYXX.PF_REQ_ACT IN ('ASCRA','ISCRA','USCRA')
	AND AYXX.LC_STA_ACTYXX = 'A'
	AND AYXX.LD_ATY_REQ_RCV > 'XX/XX/XXXX'
ORDER BY 
	AYXX.LD_ATY_REQ_RCV