--run on UHEAASQLDB
USE ULS
GO

BEGIN TRANSACTION
	DECLARE @ERROR INT = 0
	DECLARE @ROWCOUNT INT = 0
	DECLARE @ExpectedRowCount INT = 16

--16 rows
UPDATE 
	PP
SET
	PP.PrintedAt = NULL
FROM
	ULS.[print].PrintProcessing PP
	INNER JOIN ULS.[print].ScriptData SD
		ON SD.ScriptDataId = PP.ScriptDataId
	INNER JOIN ULS.[print].Letters L
		ON L.LetterId = SD.LetterId
		AND L.Letter IN ('BILLSTDEL3','BILLSTDEL5')
WHERE
	CAST(PP.AddedAt AS DATE) = CAST('2018-10-12' AS DATE)
	AND PP.OnEcorr = 0

SELECT @ROWCOUNT = @@ROWCOUNT, @ERROR = @@ERROR

IF @ROWCOUNT = @ExpectedRowCount AND @ERROR = 0
	BEGIN
		PRINT 'Transaction committed'
		COMMIT TRANSACTION
		--ROLLBACK TRANSACTION
	END
ELSE
	BEGIN
		PRINT 'ROWCOUNT:  ' + CAST(@ROWCOUNT AS VARCHAR(10))
		PRINT 'ERROR:  ' + CAST(@ERROR AS VARCHAR(10))
		PRINT 'Transaction NOT committed'
		ROLLBACK TRANSACTION
	END