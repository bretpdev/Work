SELECT * FROM OPENQUERY (DUSTER, '
	SELECT DISTINCT
		 PD30.DF_PRS_ID
		--,PD30.DC_DOM_ST
		--,PD30.DI_VLD_ADR
	FROM
		OLWHRM1.PD30_PRS_ADR PD30
		INNER JOIN OLWHRM1.LN10_LON LN10
			ON PD30.DF_PRS_ID = LN10.BF_SSN
	WHERE
		PD30.DC_DOM_ST = ''CA''
		AND PD30.DI_VLD_ADR = ''Y''
		AND LN10.LC_STA_LON10 = ''R''
		AND LN10.LA_CUR_PRI > ''0''


');

SELECT * FROM OPENQUERY (DUSTER, '
	SELECT
		COUNT(DISTINCT PD30.DF_PRS_ID) AS CA_Unique_Borrowers
	FROM
		OLWHRM1.PD30_PRS_ADR PD30
		INNER JOIN OLWHRM1.LN10_LON LN10
			ON PD30.DF_PRS_ID = LN10.BF_SSN
	WHERE
		PD30.DC_DOM_ST = ''CA''
		AND PD30.DI_VLD_ADR = ''Y''
		AND LN10.LC_STA_LON10 = ''R''
		AND LN10.LA_CUR_PRI > ''0''
');
