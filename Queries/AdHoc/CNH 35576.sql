SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID
	,LNXX.LN_SEQ
	,LNXX.LC_TYP_SCH_DIS
	,LNXX.LC_STA_LONXX
	,CAST(LNXX.LD_CRT_LONXX AS DATE) AS LD_CRT_LONXX
	,RSXX.BC_IBR_INF_SRC_VER
FROM
	RSXX_IBR_RPS RSXX
	INNER JOIN RSXX_BR_RPD RSXX
		ON RSXX.BF_SSN = RSXX.BF_SSN
		AND RSXX.BD_CRT_RSXX = RSXX.BD_CRT_RSXX
		AND RSXX.BN_IBR_SEQ = RSXX.BN_IBR_SEQ
	INNER JOIN LNXX_LON_RPS LNXX
		ON RSXX.BF_SSN = LNXX.BF_SSN
		AND RSXX.LN_RPS_SEQ = LNXX.LN_RPS_SEQ
	INNER JOIN PDXX_PRS_NME PDXX
		ON RSXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	LNXX.LC_TYP_SCH_DIS IN ('CA', 'CP', 'CQ', 'CX', 'CX', 'CX', 'IB', 'IL', 'IP', 'IS', 'IX', 'IX')
	AND LNXX.LC_STA_LONXX = 'A' 
	AND LNXX.LD_CRT_LONXX BETWEEN CAST('X/X/XXXX' AS DATE) AND CAST('XX/XX/XXXX' AS DATE)
	AND RSXX.BC_STA_RSXX  = 'A'
	AND RSXX.BC_IBR_INF_SRC_VER = 'ALT'
	AND RSXX.BA_AGI > X.XX
ORDER BY
	LD_CRT_LONXX