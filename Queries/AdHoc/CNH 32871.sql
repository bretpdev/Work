DECLARE @DATEX DATE = 'XX-XX-XXXX';
DECLARE @DATEX DATE = 'XX-XX-XXXX';

SELECT DISTINCT
	AYXX.BF_SSN
	,AYXX.PF_REQ_ACT
	,AYXX.LC_STA_ACTYXX
	,CAST(AYXX.LD_ATY_REQ_RCV AS DATE) [LD_ATY_REQ_RCV]
FROM
	CDW..AYXX_BR_LON_ATY AYXX
WHERE
	AYXX.PF_REQ_ACT = 'DIBKP'
	AND AYXX.LC_STA_ACTYXX = 'A'
	AND CAST(AYXX.LD_ATY_REQ_RCV AS DATE) BETWEEN @DATEX AND @DATEX
ORDER BY
	LD_ATY_REQ_RCV
;