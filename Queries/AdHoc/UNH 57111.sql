--ACTIVE DUTY
SELECT DISTINCT
	ISNULL(EndrSSN, BorrSSN) AS SSN
FROM 
	[ULS].[scra].[DataComparison]	DC
WHERE 
	ActiveRow = 1
	AND 
	(
		BorrActive  = 1
		OR 
		EndrActive = 1
	)

--SCRA
SELECT DISTINCT * FROM OPENQUERY (DUSTER,'
	SELECT
		 LN72.BF_SSN
		,LN72.LC_INT_RDC_PGM
		,LN72.LD_ITR_EFF_BEG
	FROM
		OLWHRM1.LN72_INT_RTE_HST LN72
	WHERE
		LN72.LC_INT_RDC_PGM = ''M''
		AND DAYS(LN72.LD_ITR_EFF_BEG) BETWEEN DAYS(''07/01/2017'') AND DAYS(''06/20/2018'')
')
ORDER BY
	LD_ITR_EFF_BEG;

