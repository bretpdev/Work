SELECT DISTINCT
	PD01.DF_PRS_ID AS SSN,
	PD01.DM_PRS_LST AS [LAST NAME],
	PD01.DM_PRS_1 AS [FIRST NAME],
	DC01.LC_TXO AS [FEDERAL TAX OFFSET ID],
	BR01.BD_TXO_CER AS [Date Treasury/Tax Offset Certified],
	AY01.PF_ACT,
	IIF(GA10.AA_CUR_PRI>0.00,'>0','0') AS AA_CUR_PRI,
	AY01.BF_LST_DTS_AY01
FROM
	ODW..GA10_LON_APP GA10
	INNER JOIN ODW..DC01_LON_CLM_INF DC01
		ON GA10.AF_APL_ID = DC01.AF_APL_ID
		AND GA10.AF_APL_ID_SFX = DC01.AF_APL_ID_SFX
	INNER JOIN ODW..PD01_PDM_INF PD01
		ON DC01.BF_SSN = PD01.DF_PRS_ID
	INNER JOIN ODW..BR01_BR_CRF BR01
		ON DC01.BF_SSN = BR01.BF_SSN
	INNER JOIN AY01_BR_ATY AY01
		ON DC01.BF_SSN = AY01.DF_PRS_ID
WHERE
	GA10.AA_CUR_PRI > 0.00
	AND DC01.LC_TXO IN ('02','07')
	AND AY01.PF_ACT = 'DBTRA'
	AND AY01.BF_LST_DTS_AY01 >= CONVERT(DATE,'20181101')
ORDER BY
	PD01.DF_PRS_ID,
	DC01.LC_TXO
	--AY01.BF_LST_DTS_AY01
;
