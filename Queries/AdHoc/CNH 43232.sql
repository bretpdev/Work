SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_SEQ
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	INNER JOIN CDW..GRXX_RPT_LON_APL GRXX
		ON GRXX.BF_SSN = LNXX.BF_SSN
		AND GRXX.LN_SEQ = LNXX.LN_SEQ
WHERE
	LNXX.LA_CUR_PRI <= X
	AND LNXX.LC_STA_LONXX = 'R'
	AND 
	(
		GRXX.WC_NDS_RPT_STA != 'C'
		OR
		ISNULL(LTRIM(RTRIM(GRXX.WC_NDS_RPT_STA)),'') = ''
	)
SELECT 
	PDXX.DF_SPE_ACC_ID,
	DWXX.LN_SEQ
FROM 
	CDW..DWXX_DW_CLC_CLU DWXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = DWXX.BF_SSN
WHERE 
	WX_OVR_DW_LON_STA = 'IDENTITY THEFT      '

