			ROW_NUMBER() OVER (PARTITION BY LNXX.BF_SSN, LNXX.LN_SEQ, LNXX.LD_ITR_EFF_BEG, LNXX.LD_ITR_EFF_END ORDER BY LD_STA_LONXX DESC) AS SEQ
		FROM
			CDW..LNXX_INT_RTE_HST LNXX
		WHERE
			LNXX.LC_STA_LONXX = 'A'
			AND	CAST(GETDATE() AS DATE) BETWEEN LNXX.LD_ITR_EFF_BEG AND LNXX.LD_ITR_EFF_END
	) LNXX
		ON LNXX.LN_SEQ = LNXX.LN_SEQ
		AND LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.SEQ = X
	LEFT JOIN
	(
		SELECT	
			BRXX.BF_SSN,
			LNXX.LN_SEQ
		FROM
			CDW..BRXX_BR_EFT BRXX
			INNER JOIN CDW..LNXX_EFT_TO_LON LNXX
				ON LNXX.BF_SSN = BRXX.BF_SSN
				AND LNXX.BN_EFT_SEQ = BRXX.BN_EFT_SEQ
		WHERE
			BRXX.BC_EFT_STA = 'A'
	) ACH
		ON ACH.BF_SSN = LNXX.BF_SSN
		AND ACH.LN_SEQ = LNXX.LN_SEQ


