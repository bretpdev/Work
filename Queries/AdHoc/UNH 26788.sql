SELECT
	*
FROM
	OPENQUERY
	(  -- borrowers who have ever been on an IDR plan
		DUSTER,
		'
			SELECT DISTINCT
				PD10.DM_PRS_1 || '' '' || PD10.DM_PRS_LST AS NAME,
				PD10.DF_PRS_ID AS SSN,
				PD10.DD_BRT AS DOB,
				LN10.IC_LON_PGM AS LOAN_TYPE,
				LN10.LN_SEQ,
				LN10.LA_LON_AMT_GTR AS LOAN_AMOUNT,
				LN10.LD_LON_1_DSB AS FIRST_DSB_DATE,
				LN10.LF_GTR_RFR_XTN AS Guarantor_ID,
				LN10.LD_LON_EFF_ADD AS Servicer_Responsibility_Date,
				''829769'' AS LENDER_ID
			FROM
				OLWHRM1.LN10_LON LN10
				INNER JOIN OLWHRM1.PD10_PRS_NME PD10
					ON PD10.DF_PRS_ID = LN10.BF_SSN
			WHERE
				LN10.IF_GTR = ''000725''
		'
	)