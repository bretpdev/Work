SELECT 
	WQ20.WF_QUE,
	WQ20.WF_SUB_QUE,
	WQ20.WN_CTL_TSK,
	WQ20.WD_ACT_REQ,
	AY10.PF_REQ_ACT,
	AY10.LD_ATY_REQ_RCV
FROM 
	UDW..WQ20_TSK_QUE WQ20
	INNER JOIN UDW..AY10_BR_LON_ATY AY10
		ON AY10.BF_SSN = WQ20.BF_SSN
		AND AY10.PF_REQ_ACT = 'ENCLP'
WHERE 
	WQ20.WF_QUE = 'SZ' 
	AND WQ20.WF_USR_ASN_BY_TSK LIKE 'P%'