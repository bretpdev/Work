SELECT
	PDXX.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	LTRIM(RTRIM(PDXX.DM_PRS_X)) AS FIRST_NAME,
	LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS LAST_NAME,
	WQXX.WF_CRT_DTS_WQXX AS DATE_TASK_REQUESTED
FROM 
	CDW..WQXX_TSK_QUE WQXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = WQXX.BF_SSN
WHERE
	WQXX.WF_QUE = 'RE'
ORDER BY
	ACCOUNT_NUMBER