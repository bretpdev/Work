	app.application_id,
	APP.created_at, 
	App.number_children,
	app.number_dependents,
	MS.[status] AS Marital_Status,
	fs.filing_status_description,
	CASE 
		WHEN app.agi_reflects_current_income = X THEN 'Y   '
		WHEN APP.agi_reflects_current_income = X THEN 'N   '
		ELSE 'N/A'
	END AS agi_reflects_current_income,
	CASE 
		WHEN app.taxable_income = X THEN 'Y   '
		WHEN APP.taxable_income = X THEN 'N   '
		ELSE 'N/A'
	END AS taxable_income,
	CASE 
		WHEN app.supporting_documentation_required = X THEN 'Y   '
		WHEN APP.supporting_documentation_required = X THEN 'N   '
		ELSE 'N/A'
	END AS supporting_documentation_required,
	app.received_date,
	app.[state],
	app.tax_year,
	PS.*,
	pf.adoi_paystub_frequency_description
FROM 
	@DATA D
	--INNER JOIN CDW..PDXX_PRS_NME PDXX
	--	ON PDXX.DF_SPE_ACC_ID = D.DF_SPE_ACC_ID
	--INNER JOIN CDW..RSXX_BR_RPD RSXX
	--	ON RSXX.BF_SSN = PDXX.DF_PRS_ID
	--	AND RSXX.LN_RPS_SEQ = D.LN_RPS_SEQ
	--INNER JOIN CDW..LNXX_LON_RPS LNXX
	--	ON LNXX.BF_SSN = RSXX.BF_SSN
	--	AND LNXX.LN_RPS_SEQ = RSXX.LN_RPS_SEQ
	INNER JOIN Income_Driven_Repayment..Borrowers B
		ON B.account_number = D.DF_SPE_ACC_ID
	LEFT JOIN Income_Driven_Repayment..Loans L
		ON L.borrower_id = B.borrower_id
		--AND L.loan_seq = LNXX.LN_SEQ
	LEFT JOIN
	(
		SELECT
			A.*,
			MA.borrower_id
			--MA.loan_seq
		FROM
			Income_Driven_Repayment..Applications A
			INNER JOIN
			(
				SELECT
					B.borrower_id,
					--L.loan_seq,
					MAX(A.application_id) AS application_id
				FROM
					Income_Driven_Repayment..Borrowers B
					INNER JOIN @DATA D
						ON D.DF_SPE_ACC_ID = B.account_number
					INNER JOIN Income_Driven_Repayment..Loans L
						ON L.borrower_id = B.borrower_id
					INNER JOIN Income_Driven_Repayment..Applications A
						ON A.application_id = L.application_id
				WHERE
					D.LD_CRT_LONXX >= CAST(A.created_at AS DATE)
				GROUP BY
					B.borrower_id
					--L.loan_seq
			)MA
				ON MA.application_id = A.application_id
	) APP
		ON APP.borrower_id = B.borrower_id
		--AND APP.loan_seq = L.loan_seq
	inner join Income_Driven_Repayment..Marital_Status MS
		ON MS.marital_status_id = APP.marital_status_id
	INNER JOIN Income_Driven_Repayment..Filing_Statuses FS
		ON FS.filing_status_id = APP.filing_status_id
	LEFT JOIN	Income_Driven_Repayment..Adoi_Paystubs PS
		ON PS.application_id = APP.application_id
	left join Income_Driven_Repayment..Adoi_Paystub_Frequencies pf
		on pf.adoi_paystub_frequency_id = ps.adoi_paystub_frequency_id
