SELECT DISTINCT
	AY10.*
	,AY20.LX_ATY
FROM
	AY10_BR_LON_ATY AY10
	LEFT JOIN AY20_ATY_TXT AY20
		ON AY10.BF_SSN = AY20.BF_SSN
		AND AY10.LN_ATY_SEQ = AY20.LN_ATY_SEQ
WHERE
	AY10.PF_REQ_ACT IN ('DL200', 'DL400', 'DL800', 'DL910', 'DL140', 'DL170', 'DL202', 'DL230')
	AND AY10.LC_STA_ACTY10 = 'E'
	AND AY10.LD_ATY_REQ_RCV BETWEEN CONVERT(DATE, '20190501') AND CONVERT(DATE, GETDATE())
;
