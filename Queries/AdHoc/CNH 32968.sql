SELECT DISTINCT
	LNXX.BF_SSN
	,PDXX.DF_SPE_ACC_ID
	,PDXX.DF_PRS_ID
	,PDXX.DM_FGN_CNY
FROM
	..PDXX_PRS_ADR PDXX
	LEFT JOIN ..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
	LEFT JOIN ..LNXX_LON LNXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
WHERE
	UPPER(PDXX.DM_FGN_CNY) = 'IRAN'
ORDER BY 
	BF_SSN DESC