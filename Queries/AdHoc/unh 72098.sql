SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	LN90.LN_SEQ AS LOAN_SEQ,
	FORB.LC_FOR_TYP AS FORB_TYPE,
	FORB.LD_FOR_BEG AS FORB_BEGIN,
	FORB.LD_FOR_END AS FORB_END,
	LN90.LD_FAT_EFF AS EFFECTIVE_CAP_DATE
FROM
	UDW..LN90_FIN_ATY LN90
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN90.BF_SSN
	INNER JOIN
	(
		SELECT
			LN60.BF_SSN,
			LN60.LN_SEQ,
			LN60.LD_FOR_BEG,
			LN60.LD_FOR_END,
			FB10.LD_CRT_REQ_FOR,
			LN60.LD_FOR_APL,
			FB10.LC_FOR_TYP
		FROM
			UDW..LN60_BR_FOR_APV LN60
			INNER JOIN UDW..FB10_BR_FOR_REQ FB10
				ON FB10.BF_SSN = LN60.BF_SSN
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
		WHERE
			FB10.LI_CAP_FOR_INT_REQ = 'C'
			AND LN60.LD_FOR_APL >= '02/01/2021'
			AND FB10.LC_STA_FOR10 = 'A'
			AND FB10.LC_FOR_STA = 'A'
			AND LN60.LC_STA_LON60 = 'A'
			AND LN60.LC_FOR_RSP != '003'
	) FORB
		ON FORB.BF_SSN = LN90.BF_SSN
		AND FORB.LN_SEQ = LN90.LN_SEQ
WHERE
	PC_FAT_TYP = '70'
	AND LD_FAT_EFF > '06/01/2021'
	AND LC_STA_LON90 = 'A'
	AND ISNULL(LC_FAT_REV_REA,'') = ''
	AND LN90.LD_FAT_EFF >= FORB.LD_FOR_END