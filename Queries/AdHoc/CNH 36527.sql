SELECT
	FBXX.BF_SSN,
	FSXX.LF_FED_AWD AS AWARD_ID,
	FSXX.LN_FED_AWD_SEQ AS AWARD_ID_SEQ,
	PDXX.DC_DOM_ST
FROM
	CDW..FBXX_BR_FOR_REQ FBXX
	INNER JOIN CDW..LNXX_BR_FOR_APV LNXX
		ON LNXX.BF_SSN = FBXX.BF_SSN
		AND LNXX.LF_FOR_CTL_NUM = FBXX.LF_FOR_CTL_NUM
	INNER JOIN CDW..FSXX_DL_LON FSXX
		ON FSXX.BF_SSN = LNXX.BF_SSN
		AND FSXX.LN_SEQ = LNXX.LN_SEQ
	INNER JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = FBXX.BF_SSN
		AND PDXX.DC_ADR = 'L'
		AND PDXX.DC_DOM_ST IN ('PR','VI')
WHERE
	FBXX.LC_STA_FORXX = 'A'
	AND LNXX.LC_STA_LONXX = 'A'
	AND LNXX.LC_FOR_RSP != 'XXX'
	AND FBXX.LC_FOR_STA = 'A'
	AND FBXX.LC_FOR_TYP = 'XX'
	AND 
	(
		(
			FBXX.LD_CRT_REQ_FOR >= 'XXXX-XX-XX'
			AND FBXX.LD_CRT_REQ_FOR <= 'XXXX-XX-XX'
		)
		OR
		(
			FBXX.LD_CRT_REQ_FOR >= 'XXXX-XX-XX'
			AND FBXX.LD_CRT_REQ_FOR <= 'XXXX-XX-XX'
		)
	)
