SELECT * FROM OPENQUERY(LEGEND,
'
SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	CASE WHEN PDXX.DC_DTH_STA = ''XX'' THEN ''VERIFIED'' ELSE ''DISPROVED'' END AS STATUS,
	LNXX.LD_LON_X_DSB
FROM
	PKUB.LNXX_LON LNXX
	INNER JOIN PKUB.PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	INNER JOIN PKUB.PDXX_GTR_DTH PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
	INNER JOIN PKUB.AYXX_BR_LON_ATY AYXX
		ON AYXX.BF_SSN = LNXX.BF_SSN
WHERE
	AYXX.LC_STA_ACTYXX = ''A''
	AND AYXX.PF_REQ_ACT = ''ADDTH''
	AND LNXX.IC_LON_PGM = ''DLPLUS''
	AND PDXX.DC_DTH_STA IN (''XX'',''XX'')

')