					OR BR02.BF_EMP_ID_7 = IN01.IF_IST
					OR BR02.BF_EMP_ID_8 = IN01.IF_IST
					OR BR02.BF_EMP_ID_9 = IN01.IF_IST
					OR BR02.BF_EMP_ID_10 = IN01.IF_IST
					OR BR02.BF_EMP_ID_11 = IN01.IF_IST
					OR BR02.BF_EMP_ID_12 = IN01.IF_IST
				INNER JOIN OLWHRM1.PD01_PDM_INF PD01 ON PD01.DF_PRS_ID = BR02.BF_SSN
			WHERE	
				IN01.IF_IST IN
				(
					''1939711'',
					''7007160'',
					''3346820'',
					''1620900'' 
				)
				AND
				IN01.IC_IST_TYP = ''006''
