SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
	LN10.LN_SEQ AS LOAN_SEQ,
	LN16.LN_DLQ_MAX AS DAYS_PAST_DUE
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	INNER JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = LN10.BF_SSN
		AND LN16.LN_SEQ = LN10.LN_SEQ
WHERE
	LC_STA_LON10 = 'R'
	AND LA_CUR_PRI > 0
	AND IC_LON_PGM = 'TILP'
	AND LN16.LC_STA_LON16 = '1'
	AND LN16.LN_DLQ_MAX >= 30
	AND DATEDIFF(DAY, LN16.LD_DLQ_MAX, GETDATE()) < 5