SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT
	LNXX.BF_SSN,
	FSXX.LF_FED_AWD,
	FSXX.LN_FED_AWD_SEQ,
	LNXX.LD_ITR_EFF_BEG,
	LNXX.LD_ITR_EFF_END,
	LR_ITR
FROM
	CDW..LNXX_INT_RTE_HST LNXX
	INNER JOIN CDW..FSXX_DL_LON FSXX
		ON FSXX.BF_SSN = LNXX.BF_SSN
		AND FSXX.LN_SEQ = LNXX.LN_SEQ
	INNER JOIN 
	(
		SELECT DISTINCT
			LNXX.BF_SSN,
			LNXX.LN_SEQ
		FROM
			CDW..LNXX_FIN_ATY LNXX
		WHERE
			LNXX.LC_STA_LONXX = 'A'
			AND COALESCE(LNXX.LC_FAT_REV_REA,'') = ''
			AND LNXX.PC_FAT_TYP = 'XX'
			AND LNXX.PC_FAT_SUB_TYP = 'XX'
			AND LNXX.LD_FAT_EFF >= 'XX/XX/XXXX'
		GROUP BY
			LNXX.BF_SSN,
			LNXX.LN_SEQ
	) LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
WHERE 
	LC_INT_RDC_PGM = 'M' 
	AND LC_INT_RDC_PGM_TYP = 'H'
	and LR_ITR = X
	AND LNXX.LC_STA_LONXX = 'A'