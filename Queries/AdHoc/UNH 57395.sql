SELECT distinct
	PD10.DM_PRS_LST,
	PD10.DM_PRS_1,
	PD10.DF_SPE_ACC_ID,
	LN16.LN_DLQ_MAX
FROM
	UDW.calc.RepaymentSchedules RS
	INNER JOIN UDW..LN16_LON_DLQ_HST LN16
		ON RS.BF_SSN = LN16.BF_SSN
		AND RS.LN_SEQ = LN16.LN_SEQ
		AND LN16.LC_STA_LON16 = '1'
		AND LN16.LN_DLQ_MAX >= 360
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = RS.BF_SSN
		AND DW01.LN_SEQ = RS.LN_SEQ
		AND DW01.WC_DW_LON_STA = '03'
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = RS.BF_SSN
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = RS.BF_SSN
		AND LN10.LN_SEQ = RS.LN_SEQ
		AND LN10.LC_STA_LON10 = 'R'
		AND LN10.LA_CUR_PRI > 0
WHERE
	RS.CurrentGradation = 1

