	UNH.BF_SSN,
	UNH.LN_SEQ,
	GR10.WF_LON_ALT AS Current_WF_LON_ALT,
	UNH.Revised_WF_LON_ALT,
	GR10.WN_LON_ALT_SEQ AS Current_WN_LON_ALT_SEQ,
	UNH.Revised_WN_LON_ALT_SEQ
