SELECT DISTINCT
	PD10.DM_PRS_1,
	PD10.DM_PRS_LST,
	PD10.DF_PRS_ID, 
	AY10.PF_REQ_ACT,
	LN10.LF_DOE_SCL_ORG
FROM
	PD10_PRS_NME PD10
	INNER JOIN LN10_LON LN10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
	INNER JOIN AY10_BR_LON_ATY AY10
		ON AY10.BF_SSN = LN10.BF_SSN
WHERE
	AY10.PF_REQ_ACT = 'DIFCR'
	AND LN10.LF_DOE_SCL_ORG ='02345800'
;

