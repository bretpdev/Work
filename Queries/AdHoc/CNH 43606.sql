SELECT 
	*
FROM 
	CDW..LNXX_INT_RTE_HST LNXX
	INNER JOIN 
	(
		SELECT
			LNXX.BF_SSN,
			LNXX.LN_SEQ
		FROM
			CDW..LNXX_LON LNXX
			LEFT JOIN CDW..CS_TransferXLoans TL
				ON TL.BF_SSN = LNXX.BF_SSN
				AND TL.LN_SEQ = LNXX.LN_SEQ
		WHERE
			LNXX.LC_STA_LONXX = 'R'
			AND LNXX.LA_CUR_PRI > X.XX
			OR TL.BF_SSN IS NOT NULL--Deconverted as part of transferX
	) Loans
		ON Loans.BF_SSN = LNXX.BF_SSN
		AND Loans.LN_SEQ = LNXX.LN_SEQ
WHERE
	LNXX.LR_ITR = X
	AND LNXX.LC_INT_RDC_PGM IN('M','S')
	AND LNXX.LD_ITR_EFF_BEG < 'XXXX-XX-XX'
	AND LNXX.LD_ITR_EFF_END >= 'XXXX-XX-XX'
ORDER BY
	LNXX.BF_SSN,
	LNXX.LN_SEQ

