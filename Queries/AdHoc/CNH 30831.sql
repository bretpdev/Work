SELECT * FROM OPENQUERY (LEGEND,'
	SELECT
		*
	FROM
		PKUB.LPXX_FOR_PAR LPXX
	WHERE
		LPXX.PC_FOR_TYP = XX
');


SELECT * FROM OPENQUERY (LEGEND,'
	SELECT
		*
	FROM
		PKUB.FBXX_BR_FOR_REQ FBXX
	WHERE
		FBXX.LC_FOR_TYP = XX
');

SELECT * FROM OPENQUERY (LEGEND,'
	SELECT DISTINCT
		FBXX.BF_SSN AS FBXX_SSN,
		LNXX.*
	FROM
		PKUB.LNXX_PCV_FIN_SCP LNXX
		RIGHT JOIN
		(
			SELECT DISTINCT
				FBXX.BF_SSN
			FROM
				PKUB.FBXX_BR_FOR_REQ FBXX
			WHERE
				FBXX.LC_FOR_TYP = XX
		) FBXX
			ON LNXX.BF_SSN = FBXX.BF_SSN
');