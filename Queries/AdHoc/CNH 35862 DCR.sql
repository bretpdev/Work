--run on UHEAASQLDB
USE CLS
GO

BEGIN TRANSACTION
	DECLARE @ERROR INT = X
	DECLARE @ROWCOUNT INT = X
	DECLARE @ExpectedRowCount INT = XXX

UPDATE
	PP
SET
	PP.PrintedAt = NULL
FROM
	CLS.[print].Letters L
	INNER JOIN CLS.[print].ScriptData SD
		ON SD.LetterId = L.LetterId
	INNER JOIN CLS.[print].PrintProcessing PP
		ON PP.ScriptDataId = SD.ScriptDataId
		AND PP.OnEcorr = X AND CAST(PP.PrintedAt AS DATE) = 'XXXX-XX-XX'
WHERE
	L.Letter IN ('CBPCNFFED', 'LDAURFED', 'PLRPYMTFED', 'RPDISCFED')

SELECT @ROWCOUNT = @@ROWCOUNT, @ERROR = @@ERROR

IF @ROWCOUNT = @ExpectedRowCount AND @ERROR = X
	BEGIN
		PRINT 'Transaction committed'
		COMMIT TRANSACTION
		--ROLLBACK TRANSACTION
	END
ELSE
	BEGIN
		PRINT 'ROWCOUNT:  ' + CAST(@ROWCOUNT AS VARCHAR(XX))
		PRINT 'ERROR:  ' + CAST(@ERROR AS VARCHAR(XX))
		PRINT 'Transaction NOT committed'
		ROLLBACK TRANSACTION
	END