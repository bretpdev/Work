SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	LN16.LN_DLQ_MAX,
	BR30.BD_EFT_STA
FROM 
	AY10_BR_LON_ATY AY10
	INNER JOIN PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = AY10.BF_SSN
	INNER JOIN LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = AY10.BF_SSN
	INNER JOIN BR30_BR_EFT BR30
		ON BR30.BF_SSN = AY10.BF_SSN
WHERE 
	AY10.PF_REQ_ACT = 'OLACH' 
	AND LN16.LC_STA_LON16 = '1'
	AND BR30.BC_EFT_STA = 'A'