SELECT
					*
				FROM
					OPENQUERY (DUSTER,
					'
						SELECT
							PD01.DF_SPE_ACC_ID AS ACCOUNT_NUMBER,
							RM10.LC_RMT_TRX_TYP AS TRANSACTION_TYPE,
							RM10.LX_RMT_TRX_AMT AS TRANSACTION_AMOUNT,
							RM10.LD_RMT_BCH_INI AS DATE_SUSPENDED,
							RM10.LX_RMT_TRX_EFF_DTE AS EFFECTIVE_DATE 
						
						FROM
							OLWHRM1.RM10_RMT_SUS_DTL RM10
							INNER JOIN OLWHRM1.PD01_PDM_INF PD01
								ON PD01.DF_PRS_ID = RM10.LF_PRS_ID
						WHERE
							LC_RMT_STA = ''S''
					')