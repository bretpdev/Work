SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS AccountNumber,
	DD.BF_SSN AS SSN,
	CentralData.dbo.TRIM(PD10.DM_PRS_1) + ' ' + CentralData.dbo.TRIM(PD10.DM_PRS_LST) AS [Name],
	IIF(AY10.BF_SSN IS NULL, 'N', 'Y') AS DL600
FROM
	UDW.calc.DailyDelinquency DD
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DD.BF_SSN
	LEFT JOIN UDW..AY10_BR_LON_ATY AY10
		ON AY10.BF_SSN = DD.BF_SSN
		AND AY10.PF_REQ_ACT = 'DL600'
		AND AY10.LC_STA_ACTY10 = 'A'
WHERE
	LN_DLQ_MAX = 60
	AND AddedAt BETWEEN '2019-12-01' AND '2020-01-27'