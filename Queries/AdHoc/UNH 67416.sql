SELECT DISTINCT
	LN10.BF_SSN
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..LN83_EFT_TO_LON LN83
		ON LN83.BF_SSN = LN10.BF_SSN
		AND LN83.LN_SEQ = LN10.LN_SEQ
		AND LN83.LC_STA_LN83 = 'A'
	INNER JOIN UDW..LN80_LON_BIL_CRF LN80
		ON LN80.BF_SSN = LN10.BF_SSN
		AND LN80.LN_SEQ = LN10.LN_SEQ
		AND LN80.LI_FNL_BIL_LON = 'Y'
		AND LN80.LC_STA_LON80 = 'A'
WHERE
	LC_STA_LON10 = 'R'
	AND LA_CUR_PRI > 0.00