
SELECT
	*
FROM
	OPENQUERY
	(
		LEGEND,
		'
			SELECT
				LNXX.BF_SSN AS "SSN",
				TRIM(PDXX.DM_PRS_X) || '' '' || TRIM(PDXX.DM_PRS_LST) AS "Name",
				LNXX.IC_LON_PGM AS "Loan Type",
				LNXX.LA_LON_AMT_GTR AS "Orig Loan Amount",
				LNXX.LD_CRT_LONXX AS "REPAYEE Approval Date",
				LNXX.LC_TYP_SCH_DIS AS "IDR Type",
				LNXX.LD_TRM_BEG AS "Loan Period BEGIN",
				LNXX.LD_TRM_END AS "Loan Period END"
			FROM
				PKUB.PDXX_PRS_NME PDXX
				INNER JOIN PKUB.LNXX_LON LNXX ON LNXX.BF_SSN = PDXX.DF_PRS_ID
				INNER JOIN PKUB.LNXX_LON_RPS LNXX ON LNXX.BF_SSN = PDXX.DF_PRS_ID AND LNXX.LN_SEQ = LNXX.LN_SEQ
			WHERE
				LNXX.LC_STA_LONXX = ''A''
				AND
				LNXX.LC_TYP_SCH_DIS = ''IX''
				AND
				LNXX.LD_CRT_LONXX BETWEEN ''XXXX-XX-XX'' AND ''XXXX-XX-XX''
		'
	)

/*
LNXX.LC_TYP_SCH_DIS = IX & LNXX.LC_STA_LONXX = A
LNXX.LD_CRT_LONXX is on or between X/X/XX _ XX/XX/XX



Output _ 
SSN = LNXX.BF_SSN
Name = PDXX.DM_PRS_X & PDXX.DM_PRS_LST
Loan type = LNXX.IC_LON_PGM
Orig Loan amount = LNXX.LA_LON_AMT_GTR
REPAYE Approval date = LNXX.LD_CRT_LONXX 
IDR type = LNXX.LC_TYP_SCH_DIS
loan period BEGIN = LNXX.LD_TRM_BEG
LOAN PERIOD END = LNXX.LD_TRM_END
*/