SELECT
	COUNT(*),
	PF_REQ_ACT,
	YEAR(LD_ATY_REQ_RCV),
	MONTH(LD_ATY_REQ_RCV)
FROM
	CDW..AYXX_BR_LON_ATY
WHERE
	PF_REQ_ACT IN('DIBCR','CRBKP')
	AND CAST(LD_ATY_REQ_RCV AS DATE) BETWEEN 'XXXX-XX-XX' AND 'XXXX-XX-XX'
GROUP BY
	PF_REQ_ACT,
	YEAR(LD_ATY_REQ_RCV),
	MONTH(LD_ATY_REQ_RCV)


