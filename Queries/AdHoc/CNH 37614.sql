SELECT
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	LNXX.LN_RPS_SEQ,
	LNXX.LC_STA_LONXX,
	LNXX.LC_TYP_SCH_DIS,
	LNXX.LD_PIF_RPT
FROM
	CDW..LNXX_LON_RPS LNXX
	INNER JOIN 
	(
		SELECT
			BF_SSN,
			SUM(LA_CUR_PRI) AS LA_CUR_PRI
		FROM
			CDW..LNXX_LON
		GROUP BY
			BF_SSN
	) Balance
		ON LNXX.BF_SSN = Balance.BF_SSN
	INNER JOIN CDW..LNXX_LON LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ	
WHERE
	Balance.LA_CUR_PRI <= X.XX
	AND LNXX.LC_STA_LONXX = 'A'
	AND LNXX.LC_TYP_SCH_DIS IN ('CA','CP','CQ','CX','CX','CX','IA','IB','IL','IP','IS','IX','IX')