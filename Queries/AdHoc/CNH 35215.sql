SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	--PDXX.DF_PRS_ID,
	'Alleged Death'
FROM
	CDW..PDXX_GTR_DTH PDXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = PDXX.DF_PRS_ID
WHERE
	DC_DTH_STA = 'XX'
	AND DATEDIFF(MONTH, DD_DTH_NTF, CAST(GETDATE() AS DATE)) <+ XX

UNION ALL

