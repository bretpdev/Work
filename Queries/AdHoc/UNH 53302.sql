SELECT * FROM OPENQUERY(DUSTER, '
	SELECT DISTINCT
		LN10.BF_SSN
	FROM 
		OLWHRM1.LN99_LON_SLE_FAT LN99
		INNER JOIN OLWHRM1.OW30_LON_SLE_CTL OW30
			ON LN99.IF_LON_SLE = OW30.IF_LON_SLE 
		INNER JOIN OLWHRM1.LN10_LON LN10
			ON LN99.BF_SSN = LN10.BF_SSN 
			AND LN99.LN_SEQ = LN10.LN_SEQ 
	WHERE
		LN10.IC_LON_PGM IN (''SUBCNS'', ''UNCNS'', ''SUBSPC'', ''UNSPC'')
		AND
		LN10.LA_CUR_PRI > 0
		AND
		LN10.LC_STA_LON10 = ''R''
	GROUP BY
		LN10.BF_SSN,
		OW30.II_ICL_CON_GRP_RLP
	HAVING
		COUNT(*) > 1
')