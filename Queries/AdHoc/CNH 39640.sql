SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	SDXX.LF_DOE_SCL_ENR_CUR,
	LNXX.LF_DOE_SCL_ORG,
	LNXX.LD_LON_EFF_ADD
FROM
	CDW..PDXX_PRS_NME PDXX
	LEFT JOIN CDW..LNXX_LON LNXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	LEFT JOIN CDW..SDXX_STU_SPR SDXX
		ON LNXX.LF_STU_SSN = SDXX.LF_STU_SSN
		AND SDXX.LC_STA_STUXX = 'A'
WHERE
	LNXX.LF_DOE_SCL_ORG = 'XXXXXXXX' OR SDXX.LF_DOE_SCL_ENR_CUR = 'XXXXXXXX'

