SELECT * FROM OPENQUERY(LEGEND,
'
SELECT
	AYXX.*
FROM
	PKUB.AYXX_BR_LON_ATY AYXX
	INNER JOIN PKUB.PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = AYXX.BF_SSN
WHERE
	PF_REQ_ACT IN (''IDCNT'',''IDFRF'',''IDFRT'',''MXFRG'',''PMTHR'')
	AND COALESCE(PF_RSP_ACT, '''') = ''''
	AND PDXX.DC_DOM_ST = ''WA''
')



