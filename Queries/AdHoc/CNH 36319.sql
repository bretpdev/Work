SELECT DISTINCT
	PDXX.DM_PRS_X AS FirstName,
	PDXX.DM_PRS_LST AS LastName,
	LNXX.BF_SSN AS SSN	
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..LNXX_LON_ATY LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
		AND LNXX.LF_DOE_SCL_ORG IN ('XXXXXXXX', 'XXXXXXXX', 'XXXXXXXX')
	INNER JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON LNXX.BF_SSN = AYXX.BF_SSN
		AND LNXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
		AND AYXX.PF_REQ_ACT IN ('DIFCR', 'DICSK')
		AND AYXX.LD_ATY_REQ_RCV BETWEEN 'XXXX-XX-XX' AND CONVERT(DATE, GETDATE())
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID

		