SELECT DISTINCT
	LN10.BF_SSN,
	LN10.LN_SEQ,
	LN10.LD_PIF_RPT AS DatePaid,
	PD24.DD_BKR_COR_1_RCV AS FirstCorr
FROM
	UDW..CL10_CLM_PCL CL10
	INNER JOIN UDW..LN40_LON_CLM_PCL LN40
		ON LN40.BF_SSN = CL10.BF_SSN
		AND LN40.LN_SEQ_CLM_PCL = CL10.LN_SEQ_CLM_PCL
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN40.BF_SSN
		AND LN10.LN_SEQ = LN40.LN_SEQ
	INNER JOIN UDW..PD24_PRS_BKR PD24
		ON PD24.DF_PRS_ID = LN10.BF_SSN
WHERE
	CL10.LC_REA_CLM_PCL = '03'	
	AND CAST(LN10.LD_PIF_RPT AS DATE) BETWEEN '2017-01-01' AND '2018-12-31'
	AND PD24.DD_BKR_VER IS NOT NULL
	AND PD24.DC_BKR_STA = '06'
