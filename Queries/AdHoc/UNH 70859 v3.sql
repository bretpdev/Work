SELECT DISTINCT 
	LN80.*,
	LN41.*,
	LN41J.*
 --INTO #POP
	FROM 
		[UDW].[dbo].[UNH70859] UNH
	INNER JOIN UDW..LN80_LON_BIL_CRF LN80
		ON LN80.BF_SSN = UNH.SSN
		AND LN80.LN_SEQ = CAST(UNH.[LOAN SEQ] AS INT)
		AND LN80.LC_STA_LON80 = 'I'
		AND LN80.LD_BIL_DU_LON >= '04/01/2021'
		AND CAST(LN80.LD_STA_LON80 AS DATE) = '02/25/2021'
	INNER JOIN UDW..LN41_LON_CRB_RPT LN41
		ON LN41.BF_SSN = UNH.SSN
		AND LN41.LN_SEQ = CAST(UNH.[LOAN SEQ] AS INT)
		AND LN41.LD_RPT_CRB = '02/28/2021'
		AND LN41.LC_RPT_STA_CRB IN ('78', '80', '82', '83', '84', '93')
	INNER JOIN UDW..LN41_LON_CRB_RPT LN41J
		ON LN41J.BF_SSN = UNH.SSN
		AND LN41J.LN_SEQ = CAST(UNH.[LOAN SEQ] AS INT)
		AND LN41J.LD_RPT_CRB = '01/31/2021'
		AND LN41J.LC_RPT_STA_CRB IN ('11')