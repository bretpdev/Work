SELECT 
	LN66.* 
FROM 
	UDW.CALC.RepaymentSchedules RS
	INNER JOIN UDW..LN66_LON_RPS_SPF LN66
		ON LN66.BF_SSN = RS.BF_SSN
		AND LN66.LN_SEQ = RS.LN_SEQ
		AND LN66.LN_RPS_SEQ = RS.LN_RPS_SEQ 
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = RS.BF_SSN
		AND LN10.LN_SEQ = RS.LN_SEQ
WHERE
	RS.LC_TYP_SCH_DIS IN ('IB', 'IL')
	AND RS.CurrentGradation = 1
	AND LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'
	
