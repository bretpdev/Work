SELECT * FROM OPENQUERY (LEGEND, '
	SELECT DISTINCT
		PDXX.DF_SPE_ACC_ID AS ACCOUNTNUMBER
		,LNXX.LN_SEQ AS LOANSEQUENCE
		,DATE(DAYS(LNXX.LD_DLQ_OCC)+XXX) AS DATEXXXDAYSDELINQUENT
	FROM 
		PKUB.PDXX_PRS_NME PDXX		
		INNER JOIN PKUB.LNXX_LON LNXX	
			ON PDXX.DF_PRS_ID = LNXX.BF_SSN
		INNER JOIN PKUB.LNXX_LON_DLQ_HST LNXX	
			ON LNXX.BF_SSN = LNXX.BF_SSN 
			AND LNXX.LN_SEQ = LNXX.LN_SEQ
	WHERE 
		LNXX.LN_DLQ_MAX >= ''XXX''		
		AND DAYS(LNXX.LD_DLQ_OCC)+XXX BETWEEN DAYS(''XX/XX/XXXX'') AND DAYS(CURRENT DATE)
');