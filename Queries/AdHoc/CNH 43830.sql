SELECT DISTINCT 
  pop.bf_ssn,	
  'PIF' AS[POPULATION],
  MAX(CASE WHEN LNXX.LC_FED_PGM_YR = 'LNC' THEN X ELSE X END) AS LNC_Count,
	MAX(CASE WHEN LNXX.LC_FED_PGM_YR = 'DLO' THEN X ELSE X END) AS DLO_Count,
	NULL AS LNC_SaleId,
	NULL AS DLO_SaleId,
	NULL,
	NULL 
FROM 
	@credit pop
	INNER JOIN CDW..LNXX_LON LNXX
		ON LNXX.BF_SSN = POP.BF_SSN
GROUP BY
	POP.BF_SSN

