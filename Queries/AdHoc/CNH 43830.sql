(
SELECT 
	LNXX.BF_SSN,
	LN_SEQ,
	CAST(LD_FAT_EFF AS DATE) AS START_DATE,
	ISNULL(DATEADD(DAY, -X,CAST(LEAD(LD_FAT_EFF) OVER (PARTITION BY BF_SSN, LN_SEQ ORDER BY LN_FAT_SEQ) AS DATE)), 'XX/XX/XXXX') AS END_DATE,
	lnXX.pc_fat_typ,
	SUM(LA_FAT_CUR_PRI) OVER(PARTITION BY BF_SSN, LN_SEQ ORDER BY LN_FAT_SEQ) AS BALANCE
FROM 
	CDW..LNXX_FIN_ATY LNXX
	INNER JOIN
	(
		SELECT DISTINCT 
			BF_SSN AS SSN
		FROM
			CDW..LNXX_FIN_ATY LNXX
		WHERE
			LNXX.LC_STA_LONXX = 'A'
			AND COALESCE(LNXX.LC_FAT_REV_REA,'') = ''
			AND LNXX.LD_FAT_EFF >= 'XX/XX/XXXX'
			AND LNXX.PC_FAT_TYP = 'XX'
	) P
		ON P.SSN = LNXX.BF_SSN
WHERE  
	(
		LC_FAT_REV_REA = ''
		OR LC_FAT_REV_REA IS NULL
	)
	AND LC_STA_LONXX = 'A'
)
