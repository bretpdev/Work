DECLARE @StartDate DATE = 'XXXX-X-X'
DECLARE @EndDate DATE = GETDATE()

SELECT DISTINCT
	AYXX.BF_SSN,
	AYXX.PF_REQ_ACT,
	AYXX.LD_ATY_REQ_RCV
FROM
	CDW..AYXX_BR_LON_ATY AYXX
WHERE 
	AYXX.PF_REQ_ACT IN ('BXPOC','BPOCX','BPOCR')
	AND 
	AYXX.LD_ATY_REQ_RCV BETWEEN @StartDate AND @EndDate
