SELECT
	cast(wqXX.WF_CRT_DTS_WQXX as date) as task_create_date,
	PDXX.DF_SPE_ACC_ID,
	DWXX.LN_SEQ,
	LKXX.PX_DSC_MDM
FROM
	CDW..WQXX_TSK_QUE WQXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = WQXX.BF_SSN
	INNER JOIN CDW..DWXX_DW_CLC_CLU DWXX
		ON DWXX.BF_SSN = PDXX.DF_PRS_ID
	INNER JOIN CDW..LKXX_LS_CDE_LKP LKXX
		ON LKXX.PM_ATR = 'WC-DW-LON-STA'
		AND LKXX.PX_ATR_VAL = DWXX.WC_DW_LON_STA
WHERE 
	WF_QUE = 'DP'