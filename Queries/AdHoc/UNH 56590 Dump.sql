SELECT
	PD10.DF_SPE_ACC_ID
	,LN10.LN_SEQ
	,LN10.LA_CUR_PRI
	,FB10.LC_FOR_TYP
	,FB10.LC_FOR_STA
	,CAST(LN60.LD_FOR_BEG AS DATE) AS LD_FOR_BEG
	,CAST(LN60.LD_FOR_END AS DATE) AS LD_FOR_END
	,FB10.*

FROM
	PD10_PRS_NME PD10
	INNER JOIN LN10_LON LN10
		ON PD10.DF_PRS_ID = LN10.BF_SSN
	INNER JOIN LN60_BR_FOR_APV LN60
		ON LN10.BF_SSN = LN60.BF_SSN
		AND LN10.LN_SEQ = LN60.LN_SEQ
	INNER JOIN FB10_BR_FOR_REQ FB10
		ON LN60.BF_SSN = FB10.BF_SSN
		AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM

WHERE
	LN10.IC_LON_PGM IN ('SUBCNS', 'UNCNS', 'SUBSPC', 'UNSPC')
	AND LN10.LA_CUR_PRI > 0
	AND FB10.LC_FOR_TYP = '15'
	AND FB10.LC_FOR_STA = 'A'
	AND FB10.LC_STA_FOR10 = 'A'
	--AND PD10.DF_SPE_ACC_ID IN ('0000973502', '0033585530', '0079803361')
	--AND CAST(LN60.LD_FOR_BEG AS DATE) < CAST(GETDATE() AS DATE)
	--AND CAST(LN60.LD_FOR_END AS DATE) > CAST(GETDATE() AS DATE)
ORDER BY 
	LN60.LD_FOR_END desc,
	PD10.DF_SPE_ACC_ID,
	LN10.LN_SEQ