SELECT DISTINCT
	LN10.BF_SSN,
	SUM(LN10.LA_CUR_PRI) AS BAL
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..DW01_DW_CLC_CLU DW01
		ON DW01.BF_SSN = LN10.BF_SSN
		AND DW01.LN_SEQ = LN10.LN_SEQ
	LEFT JOIN UDW..LN16_LON_DLQ_HST LN16
		ON LN16.BF_SSN = LN10.BF_SSN
		AND LN16.LN_SEQ = LN10.LN_SEQ
		AND LN16.LC_STA_LON16 = '1'
WHERE
	LN10.LC_STA_LON10 = 'R'
	AND LN10.LA_CUR_PRI > 0
	AND DW01.WC_DW_LON_STA = '03'
	AND LN16.LN_DLQ_MAX BETWEEN 120 AND 270
GROUP BY
LN10.BF_SSN
ORDER BY 
BAL DESC
