SELECT
	PDXX.DF_SPE_ACC_ID,
	PDXX.DM_PRS_LST,
	LNXX.LN_SEQ,
	LNXX.LD_FAT_PST
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..LNXX_FIN_ATY LNXX	
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	INNER JOIN CDW..GRXX_RPT_LON_APL GRXX
		ON GRXX.BF_SSN = LNXX.BF_SSN
		AND GRXX.LN_SEQ = LNXX.LN_SEQ
WHERE
	GRXX.LC_WOF_WUP_REA = 'V'
	AND LNXX.PC_FAT_TYP = 'XX'
	AND LNXX.PC_FAT_SUB_TYP = 'XX'
	AND LNXX.LC_STA_LONXX = 'A'
	AND LNXX.LC_FAT_REV_REA =''