

SELECT DISTINCT
	PD10.DF_SPE_ACC_ID AS AccountNumber,
	RTRIM(PD10.DM_PRS_1) AS FirstName,
	RTRIM(PD10.DM_PRS_LST) AS LastName,
	DF10.LD_CRT_REQ_DFR AS DefermentCreateDate,
	LN50.LD_DFR_BEG AS DefermentBegin,
	LN50.LD_DFR_END AS DefermentEnd,
	DF10.LF_USR_CRT_REQ_DFR as UT_ID,
	LN50.LD_DFR_APL AS DefermentAppliedDate

FROM
	UDW..DF10_BR_DFR_REQ DF10
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN50.BF_SSN = DF10.BF_SSN
		AND LN50.LF_DFR_CTL_NUM = DF10.LF_DFR_CTL_NUM
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DF10.BF_SSN
WHERE
	DF10.LC_DFR_TYP = '13'
	AND DF10.LD_CRT_REQ_DFR BETWEEN '03/04/2021' AND GETDATE()
UNION 
SELECT DISTINCT
	
	PD10.DF_SPE_ACC_ID AS AccountNumber,
	RTRIM(PD10.DM_PRS_1) AS FirstName,
	RTRIM(PD10.DM_PRS_LST) AS LastName,
	DF10.LD_CRT_REQ_DFR AS DefermentCreateDate,
	LN50.LD_DFR_BEG AS DefermentBegin,
	LN50.LD_DFR_END AS DefermentEnd,
	DF10.LF_USR_CRT_REQ_DFR as UT_ID,
	LN50.LD_DFR_APL AS DefermentAppliedDate

FROM
	UDW..DF10_BR_DFR_REQ DF10
	INNER JOIN UDW..LN50_BR_DFR_APV LN50
		ON LN50.BF_SSN = DF10.BF_SSN
		AND LN50.LF_DFR_CTL_NUM = DF10.LF_DFR_CTL_NUM
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DF10.BF_SSN
WHERE
	DF10.LC_DFR_TYP = '13'
	AND ln50.LD_DFR_APL
	 BETWEEN '03/04/2021' AND GETDATE()

