	PD42.DN_DOM_PHN_ARA + PD42.DN_DOM_PHN_XCH + PD42.DN_DOM_PHN_LCL,
	'C954402949',
	PD42.DD_PHN_VER
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..PD42_PRS_PHN PD42
		ON PD42.DF_PRS_ID = LN10.BF_SSN
