	LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'
	AND PD42.DI_PHN_VLD = 'Y'
	AND PD42.DC_ALW_ADL_PHN IN ('X','P')
	and PD42.DN_DOM_PHN_ARA + PD42.DN_DOM_PHN_XCH + PD42.DN_DOM_PHN_LCL != ''

