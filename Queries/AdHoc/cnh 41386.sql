SELECT DISTINCT
PD10.DF_SPE_ACC_ID,
LN80.LD_BIL_CRT,
max(LN16.LN_DLQ_MAX) as days_past_due
FROM
	CDW..LN16_LON_DLQ_HST LN16
	INNER JOIN CDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN16.BF_SSN
	INNER JOIN CDW..LN80_LON_BIL_CRF LN80
		ON LN80.BF_SSN = LN16.BF_SSN
		AND LN80.LN_SEQ = LN16.LN_SEQ
	INNER JOIN CDW..LN10_LON LN10
		ON LN10.BF_SSN = LN16.BF_SSN
		AND LN10.LN_SEQ = LN16.LN_SEQ
WHERE
	LC_STA_LON16 = '1'
	AND LN_DLQ_MAX > 30
	AND LN80.LD_BIL_CRT BETWEEN '04/04/2020' AND '04/07/2020'
	AND LC_STA_LON80 = 'A'
	AND LA_CUR_PRI > 0
	AND LC_STA_LON10 = 'R'
group by	
	PD10.DF_SPE_ACC_ID,
	LN80.LD_BIL_CRT