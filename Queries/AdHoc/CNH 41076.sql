SELECT DISTINCT
	COUNT(DISTINCT LNXX.BF_SSN) AS Borrowers,
	COUNT(LNXX.BF_SSN) AS Loans,
	SUM(LNXX.LA_CUR_PRI) AS CurrentPrincipalOutstanding
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
		AND PDXX.DC_ADR = 'L'
		AND PDXX.DC_DOM_ST = 'RI'
		AND PDXX.DI_VLD_ADR = 'Y'
WHERE
	CAST(LD_LON_X_DSB AS DATE) >= 'XXXX-XX-XX'

SELECT
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	LNXX.LA_CUR_PRI,
	LNXX.LD_LON_X_DSB,
	PDXX.DX_STR_ADR_X,
	PDXX.DX_STR_ADR_X,
	PDXX.DM_CT,
	PDXX.DC_DOM_ST,
	PDXX.DF_ZIP_CDE
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
		AND PDXX.DC_ADR = 'L'
		AND PDXX.DC_DOM_ST = 'RI'
		AND PDXX.DI_VLD_ADR = 'Y'
WHERE
	CAST(LD_LON_X_DSB AS DATE) >= 'XXXX-XX-XX'