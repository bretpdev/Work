SELECT
	A.*,
	IIF(AYXX.PF_REQ_ACT IS NOT NULL, 'YES', 'NO') AS [Closed School Arc Sent]
FROM
	@ACCTS A
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON A.DF_SPE_ACC_ID = PDXX.DF_SPE_ACC_ID
	LEFT JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON AYXX.BF_SSN = PDXX.DF_PRS_ID
		AND AYXX.PF_REQ_ACT = 'DICSK'
		AND AYXX.LD_ATY_REQ_RCV >= CONVERT(DATE,'XXXXXXXX')
ORDER BY
	A.DF_SPE_ACC_ID