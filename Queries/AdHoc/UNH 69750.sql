SELECT
	SUM(PRI + ALT)
FROM
(
SELECT DISTINCT
	CASE WHEN PD03.DI_PHN_VLD = 'Y' AND PD03.DC_CEP IN ('L','U') THEN 1 ELSE 0 END AS PRI,
	CASE WHEN PD03.DI_OTH_PHN_VLD = 'Y' AND PD03.DC_ALT_CEP IN ('L','U') THEN 1 ELSE 0 END AS ALT,
	PD03.*
FROM
	ODW..GA14_LON_STA GA14
	INNER JOIN ODW..DC01_LON_CLM_INF DC01
		ON GA14.AF_APL_ID = DC01.AF_APL_ID
		AND GA14.AF_APL_ID_SFX = DC01.AF_APL_ID_SFX
	INNER JOIN ODW..PD01_PDM_INF PD01
		ON DC01.BF_SSN = PD01.DF_PRS_ID
	INNER JOIN ODW..PD03_PRS_ADR_PHN PD03
		ON PD03.DF_PRS_ID = PD01.DF_PRS_ID
WHERE
	GA14.AC_STA_GA14 = 'A'
	AND GA14.AC_LON_STA_TYP = 'CP'
	AND DC01.LC_STA_DC10 = '03'
	AND DC01.LC_REA_CLM_ASN_DOE NOT IN ('01','03','04','08','21','23','24','25')
) POP

