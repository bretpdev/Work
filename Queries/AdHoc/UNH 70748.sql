SELECT DISTINCT
	RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS [NAME],
	PD10.DF_SPE_ACC_ID,
	DF10.LD_CRT_REQ_DFR AS APPLIED_DATE,
	LN50.LD_DFR_BEG,
	LN50.LD_DFR_END
FROM
	UDW..LN50_BR_DFR_APV LN50
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = LN50.BF_SSN
	INNER JOIN UDW..DF10_BR_DFR_REQ DF10
		ON DF10.BF_SSN = LN50.BF_SSN
		AND DF10.LF_DFR_CTL_NUM = LN50.LF_DFR_CTL_NUM
WHERE
	LC_DFR_TYP = '13'
	AND DF10.LD_CRT_REQ_DFR >= '01/01/2020'
	AND LN50.LC_STA_LON50 = 'A'
	AND DF10.LC_STA_DFR10 = 'A'
	AND DF10.LC_DFR_STA = 'A' --removing this to account for denied requests