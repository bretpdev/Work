USE UDW
GO
SELECT DISTINCT
	RS.BF_SSN
	,RS.LN_SEQ
	,RS.LC_TYP_SCH_DIS
	,CAST(LN65.LD_CRT_LON65 AS DATE) AS LD_CRT_LON65
	,RS05.BC_IBR_INF_SRC_VER 
FROM 
	UDW.calc.RepaymentSchedules RS
	INNER JOIN UDW..LN65_LON_RPS LN65
		ON LN65.BF_SSN = RS.BF_SSN
		AND LN65.LN_SEQ = RS.LN_SEQ
		AND LN65.LN_RPS_SEQ = RS.LN_RPS_SEQ
	INNER JOIN UDW..RS10_BR_RPD RS10
		ON RS10.BF_SSN = LN65.BF_SSN
		AND RS10.LN_RPS_SEQ = LN65.LN_RPS_SEQ
	INNER JOIN UDW..RS05_IBR_RPS RS05
		ON RS05.BF_SSN = RS10.BF_SSN
		AND RS05.BN_IBR_SEQ = RS10.BN_IBR_SEQ
		AND RS05.BD_CRT_RS05 = RS10.BD_CRT_RS05
WHERE 
	RS.CurrentGradation = 1 
	AND RS.LC_TYP_SCH_DIS IN('IB','IS')
	AND RS05.BA_AGI != 0.00
	AND LN65.LD_CRT_LON65 BETWEEN CAST('10/1/2015' AS DATE) AND CAST('12/31/2017' AS DATE)
	AND RS05.BC_STA_RS05 = 'A'
	AND RS05.BC_IBR_INF_SRC_VER = 'ALT'
ORDER BY
	CAST(LN65.LD_CRT_LON65 AS DATE)
