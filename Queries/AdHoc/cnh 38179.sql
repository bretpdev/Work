SELECT DISTINCT
	LN20.BF_SSN AS [BORROWER SSN],
	LTRIM(RTRIM(PD10B.DM_PRS_1)) + ' ' + LTRIM(RTRIM(PD10B.DM_PRS_LST)) AS [BORROWER NAME],
	LTRIM(RTRIM(PD10E.DM_PRS_1)) + ' ' + LTRIM(RTRIM(PD10E.DM_PRS_LST)) AS [RECIPIENT NAME],
	PD40.DC_PHN AS [PHONE TYPE], 
	(PD40.DN_DOM_PHN_ARA + PD40.DN_DOM_PHN_XCH + PD40.DN_DOM_PHN_LCL) AS [PHONE NUMBER]
FROM
	CDW..LN20_EDS LN20
	INNER JOIN CDW..PD10_PRS_NME PD10B
		ON PD10B.DF_PRS_ID = LN20.BF_SSN
	INNER JOIN CDW..PD10_PRS_NME PD10E
		ON PD10E.DF_PRS_ID = LN20.LF_EDS
	INNER JOIN CDW..LN10_LON LN10
		ON LN10.BF_SSN = LN20.BF_SSN
		AND LN10.LN_SEQ = LN20.LN_SEQ
	INNER JOIN CDW..LN16_LON_DLQ_HST LN16
		 ON LN16.BF_SSN = LN10.BF_SSN
		 AND LN16.LN_SEQ = LN10.LN_SEQ
	INNER JOIN CDW..PD40_PRS_PHN PD40
		ON PD40.DF_PRS_ID = LN20.LF_EDS
WHERE
	LN10.LA_CUR_PRI > 0
	AND LN10.LC_STA_LON10 = 'R'
	AND LN16.LC_STA_LON16 = '1'
	AND PD40.DI_PHN_VLD = 'Y'