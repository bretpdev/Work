SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LTRIM(RTRIM(PDXX.DM_PRS_X)) + ' ' + LTRIM(RTRIM(PDXX.DM_PRS_LST)) AS [NAME],
	PDXX.DX_STR_ADR_X AS ADDRESSX,
	PDXX.DX_STR_ADR_X AS ADDRESSX,
	PDXX.DM_CT AS CITY,
	PDXX.DC_DOM_ST AS [STATE],
	PDXX.DF_ZIP_CDE AS ZIP,
	PDXX.DI_VLD_ADR AS VALID_ADDRESS
FROM 
	CDW..LNXX_FIN_ATY LNXX
	INNER JOIN CDW..LNXX_LON_PAY_FAT LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN 
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
		AND LNXX.LN_FAT_SEQ = LNXX.LN_FAT_SEQ
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
	LEFT JOIN CDW..PDXX_PRS_ADR PDXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
		AND PDXX.DC_ADR = 'L'

WHERE 
	LD_FAT_PST = 'XX/XX/XXXX' 
	AND LD_FAT_EFF = 'XX/XX/XXXX'
	AND LC_RMT_BCH_SRC_IPT = 'T'
	AND LC_STA_LONXX = 'A'
	AND LN_RMT_BCH_SEQ = X
	AND ISNULL(LC_FAT_REV_REA,'') = ''