select
linedata
from
(
SELECT
	'20297AA' AS LineData
	,1 AS Ord

UNION 

SELECT
	CAST(LN10.SSN AS VARCHAR(9)) + RIGHT('0000' + CAST(LN10.LN_SEQ AS VARCHAR(2)),4) AS LineData
	,2 AS Ord
FROM
	UDW..unh69227v2 LN10
	

UNION 

SELECT
	'20297AB' AS LineData
	,3 AS Ord

UNION

SElECT
	CAST(LN10.BF_SSN AS VARCHAR(9)) + RIGHT('0000' + CAST(LN10.LN_SEQ AS VARCHAR(2)),4) AS LineData
	,4 AS Ord
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..LN35_LON_OWN LN35
		ON LN35.BF_SSN = LN10.BF_SSN
		AND LN35.LN_SEQ = LN10.LN_SEQ
		AND LN35.LC_STA_LON35 = 'A'
		AND CAST(GETDATE() AS DATE) BETWEEN CAST(LN35.LD_OWN_EFF_SR AS DATE) AND ISNULL(LN35.LD_OWN_EFF_END,'2099-01-01')
		AND LN35.IF_BND_ISS = 'WHSE'
	LEFT JOIN UDW..unh69227v2 UNH
		ON UNH.SSN = LN10.BF_SSN
		AND UNH.LN_SEQ = LN10.LN_SEQ
WHERE
	LN10.LC_STA_LON10 = 'R'
	AND LN10.LA_CUR_PRI > 0.00
	AND UNH.SSN IS NULL

) p
order by p.Ord