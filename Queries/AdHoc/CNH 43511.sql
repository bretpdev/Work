SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
SELECT 
	CNH.* 
FROM
	CDW..CNHXXXXX CNH
	INNER JOIN CDW..CS_TransferX TX
		ON TX.BF_SSN = CNH.BF_SSN
	INNER JOIN 
	(
		SELECT DISTINCT 
			LNXX.BF_SSN,
			LNXX.LN_SEQ
		FROM
			CDW..LNXX_FIN_ATY LNXX
			INNER JOIN CDW..LNXX_LON LNXX
				ON LNXX.BF_SSN = LNXX.BF_SSN
				AND LNXX.LN_SEQ = LNXX.LN_SEQ
				AND LNXX.LA_CUR_PRI >= X.XX
				AND LNXX.LC_FED_PGM_YR = 'LNC'
				AND LNXX.LD_PIF_RPT IS NULL
		WHERE
			LNXX.LC_STA_LONXX = 'A'
			AND COALESCE(LNXX.LC_FAT_REV_REA,'') = ''
			AND LNXX.PC_FAT_TYP = 'XX'
			AND LNXX.PC_FAT_SUB_TYP = 'XX'
			AND LNXX.LD_FAT_EFF > ='XX/XX/XXXX'
		GROUP BY
			LNXX.BF_SSN,
			LNXX.LN_SEQ
	) Trans
		ON Trans.BF_SSN = CNH.BF_SSN


SELECT 
	CNH.* 
FROM
	CDW..CNHXXXXX CNH
	inner join cdw..LNXX_LON LNXX
		ON CNH.BF_SSN = LNXX.BF_SSN
WHERE
	LNXX.LA_CUR_PRI > X
	AND LNXX.LC_STA_LONXX = 'R'