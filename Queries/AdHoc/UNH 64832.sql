SELECT 
	WQ21.WF_QUE,
	WQ21.WF_SUB_QUE,
	WQ21.WC_STA_WQUE20 AS CompletionStatus,
	WQ21.WF_USR_ASN_TSK AS Agent,
	WQ21.WD_ACT_REQ AS TaskCreateDate,
	WQ21.WF_LST_DTS_WQ20 AS TaskCompleteDate,
	PD10.DF_SPE_ACC_ID AS AccountNumber
FROM 
	UDW..WQ21_TSK_QUE_HST WQ21
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = WQ21.BF_SSN
WHERE 
	WQ21.WF_QUE IN('2A','2P') 
	AND WQ21.WC_STA_WQUE20 IN('X','C') 
	AND CAST(WQ21.WF_LST_DTS_WQ20 AS DATE) BETWEEN '2019-12-01' AND '2019-12-19'
	AND WQ21.WF_USR_ASN_TSK IN('UT02776','UT02777','UT02762','UT02774','UT02775')
ORDER BY
	Agent,
	WF_QUE,
	TaskCompleteDate