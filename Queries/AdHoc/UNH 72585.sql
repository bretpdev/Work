SELECT DISTINCT
	PD42.DF_PRS_ID,
	PD42.DC_PHN,
	PD42.DI_PHN_VLD,
	PD42.DC_ALW_ADL_PHN AS "DC_ALW_ADL_PHN",
	PD42.DN_DOM_PHN_ARA + PD42.DN_DOM_PHN_XCH + PD42.DN_DOM_PHN_LCL AS PHN
FROM
	UDW..[UNH 72585] UNH
	INNER JOIN UDW..PD42_PRS_PHN PD42
		ON PD42.DN_DOM_PHN_ARA + PD42.DN_DOM_PHN_XCH + PD42.DN_DOM_PHN_LCL = UNH. TN
		AND PD42.DC_ALW_ADL_PHN = 'P'
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = PD42.DF_PRS_ID
		AND LN10.LA_CUR_PRI > 0.00
		AND LN10.LC_STA_LON10 = 'R'
WHERE
	UNH.disconnected = 'yes'