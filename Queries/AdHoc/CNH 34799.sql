DECLARE @BEGIN DATE = 'XXXX-XX-XX'
DECLARE @END DATE = 'XXXX-XX-XX'


SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID,
	LNXX.LN_SEQ
FROM
	CDW..LNXX_FIN_ATY LNXX
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	LNXX.PC_FAT_TYP = 'XX'
	AND LNXX.PC_FAT_SUB_TYP IN ('XX','XX')
	AND LNXX.LD_FAT_EFF BETWEEN @BEGIN AND @END
ORDER BY
	PDXX.DF_SPE_ACC_ID