DECLARE @REVIEWDATE DATE = 'X/XX/XXXX'

SELECT DISTINCT
	PDXX.DF_SPE_ACC_ID
	,LNXX.PC_FAT_TYP
	,LNXX.PC_FAT_SUB_TYP
	,LNXX.LD_FAT_APL
FROM
	LNXX_FIN_ATY LNXX
	INNER JOIN PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
WHERE
	LNXX.PC_FAT_TYP = 'XX'
	AND LNXX.PC_FAT_SUB_TYP IN ('XX','XX','XX')
	AND LNXX.LD_FAT_APL > @REVIEWDATE
