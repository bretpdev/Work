DECLARE @BEGIN DATE = '2018-07-01'
DECLARE @END DATE = '06/30/2019'

SELECT DISTINCT
	LN33.BF_SSN,
	LN33.LN_SEQ,
	LN33.LD_CU_END
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..LN33_LON_CU_INF LN33
		ON LN10.BF_SSN = LN33.BF_SSN
		AND LN10.LN_SEQ = LN33.LN_SEQ
WHERE 
	LN33.LD_CU_END BETWEEN @BEGIN AND @END