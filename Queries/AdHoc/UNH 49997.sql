SELECT
	R.BF_SSN,
	R.LN_SEQ,
	R.IF_OWN,
	R.LN_LON_OWN_SEQ,
	R.LF_OWN_EFT_RIR_ASN
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN OPENQUERY
	(
		DUSTER,
		'
			SELECT	
				LN35.BF_SSN,
				LN35.LN_SEQ,
				LN35.IF_OWN,
				LN35.LN_LON_OWN_SEQ,
				ROW_NUMBER() OVER (PARTITION BY LN35.BF_SSN, LN35.LN_SEQ, LN35.IF_OWN ORDER BY LN35.LN_LON_OWN_SEQ DESC) "max_OWN_SEQ",
				LN35.LF_OWN_EFT_RIR_ASN
			FROM	
				OLWHRM1.LN10_LON LN10
				INNER JOIN OLWHRM1.LN35_LON_OWN LN35 ON LN35.BF_SSN = LN10.BF_SSN AND LN35.LN_SEQ = LN10.LN_SEQ
			WHERE	
				 LN10.LF_LON_CUR_OWN LIKE ''829769%''
				 AND
				 LN35.LC_STA_LON35 = ''A''
				 AND
				 LN35.LF_OWN_EFT_RIR_ASN IS NOT NULL
		'
	) R ON R.BF_SSN = PD10.DF_PRS_ID
WHERE
	R.max_OWN_SEQ = 1
	AND
	R.IF_OWN != R.LF_OWN_EFT_RIR_ASN
