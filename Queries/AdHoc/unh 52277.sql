SELECT DISTINCT
	DW01.BF_SSN,
	DW01.LN_SEQ
FROM
	UDW..DW01_DW_CLC_CLU DW01
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = DW01.BF_SSN
	LEFT JOIN
	(
		SELECT	
			BF_SSN,
			LN_SEQ
		FROM
			UDW..LN90_FIN_ATY LN90
		WHERE
			(LN90.PC_FAT_TYP = '80' AND LN90.PC_FAT_SUB_TYP = '01')
	 )LN90
		ON LN90.BF_SSN = DW01.BF_SSN
		AND LN90.LN_SEQ = DW01.LN_SEQ
	INNER JOIN UDW..LN72_INT_RTE_HST LN72
		ON LN72.BF_SSN = DW01.BF_SSN
		AND LN72.LN_SEQ = DW01.LN_SEQ
WHERE
	WC_DW_LON_STA IN ('07','08','12')
	AND (LN90.BF_SSN IS NULL AND LN90.LN_SEQ IS NULL)
	AND LN72.LC_INT_RDC_PGM = 'P'