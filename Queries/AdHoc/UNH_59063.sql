SELECT
	PD10.DF_SPE_ACC_ID
FROM
	UDW..LN10_LON LN10
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON LN10.BF_SSN = PD10.DF_PRS_ID
		AND LN10.IC_LON_PGM = 'TILP'
	INNER JOIN UDW..DF10_Deferment DF10
		ON PD10.DF_SPE_ACC_ID = DF10.DF_SPE_ACC_ID
		AND DF10.LC_DFR_TYP = '06'
		AND DF10.LD_DFR_BEG = '2017-07-01'
		AND DF10.LD_DFR_END IN ('2018-06-30','2018-09-30')
	LEFT JOIN UDW..LN90_FIN_ATY LN90
		ON LN10.BF_SSN = LN90.BF_SSN
		AND LN10.LN_SEQ = LN90.LN_SEQ
		AND LN90.PC_FAT_TYP = '10'
		AND PC_FAT_SUB_TYP = '54'
		AND LN90.LD_FAT_APL > '2018.08/30'
WHERE
	LN90.BF_SSN IS NULL