SELECT distinct
	UNH.SSN,
	UNH.[LOAN SEQ],
	ISNULL(DD1.LN_DLQ_MAX,0) AS FEB25TH,
	DD2.LN_DLQ_MAX AS FEB26TH,
	dd3.LN_DLQ_MAX AS mar1st,
	LN41J.LC_RPT_STA_CRB AS JAN_CREDIT_RPT,
	LN41.LC_RPT_STA_CRB AS FEB_CREDIT_RPT,
	LN41.LC_RPT_STA_CRB_ORG,
	LN41J.LC_RPT_STA_CRB_ORG
FROM
	[UDW].[dbo].[UNH70859] UNH
	INNER JOIN UDW.calc.DailyDelinquency DD2
		ON DD2.BF_SSN = UNH.SSN
		AND DD2.LN_SEQ = UNH.[LOAN SEQ]
		AND DD2.AddedAt = '02/26/2021'
	INNER JOIN UDW..LN41_LON_CRB_RPT LN41
		ON LN41.BF_SSN = UNH.SSN
		AND LN41.LN_SEQ = CAST(UNH.[LOAN SEQ] AS INT)
		AND LN41.LD_RPT_CRB = '02/28/2021'
		--AND LN41.LC_RPT_STA_CRB IN ('78', '80', '82', '83', '84', '93')
	INNER JOIN UDW..LN41_LON_CRB_RPT LN41J
		ON LN41J.BF_SSN = UNH.SSN
		AND LN41J.LN_SEQ = CAST(UNH.[LOAN SEQ] AS INT)
		AND LN41J.LD_RPT_CRB = '01/31/2021'
		--AND LN41J.LC_RPT_STA_CRB IN ('11')
	LEFT JOIN UDW.calc.DailyDelinquency DD1
		ON DD1.BF_SSN = UNH.SSN
		AND DD1.LN_SEQ = UNH.[LOAN SEQ]
		AND DD1.AddedAt = '02/25/2021'
	LEFT JOIN UDW.calc.DailyDelinquency DD3
		ON DD3.BF_SSN = UNH.SSN
		AND DD3.LN_SEQ = UNH.[LOAN SEQ]
		AND DD3.AddedAt = '03/01/2021'
	--LEFT JOIN UDW.calc.DailyDelinquency DD2
	--	ON DD2.BF_SSN = UNH.SSN
	--	AND DD2.LN_SEQ = UNH.[LOAN SEQ]
	--	AND DD2.AddedAt = '02/26/2021'
WHERE DD2.LN_DLQ_MAX != ISNULL(DD1.LN_DLQ_MAX,0) + 1
