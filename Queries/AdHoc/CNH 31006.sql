SELECT * FROM OPENQUERY (LEGEND,'
	SELECT DISTINCT
		LNXX.BF_SSN				AS SSN
		,LNXX.LN_SEQ			AS LOAN_SEQ
		,LNXX.LD_LON_ACL_ADD	AS LOAN_ADD_DATE
		,LNXX.LR_ITR			AS INTEREST_RATE
		,LNXX.LC_STA_LONXX		AS STATUS
		--,LNXX.LD_ITR_EFF_BEG
		--,LNXX.LD_ITR_EFF_END
		,LNXX.LD_LON_X_DSB		AS FIRST_DISB_DATE
	FROM
		PKUB.LNXX_LON LNXX
		INNER JOIN PKUB.LNXX_INT_RTE_HST LNXX
			ON LNXX.BF_SSN = LNXX.BF_SSN
			AND LNXX.LN_SEQ = LNXX.LN_SEQ
	WHERE
		LNXX.LR_ITR = ''X''
		--AND DAYS(LNXX.LD_LON_ACL_ADD) = DAYS(''XX/X/XXXX'')
		AND LNXX.LC_STA_LONXX = ''A''
	ORDER BY 
		LNXX.LD_LON_ACL_ADD
');