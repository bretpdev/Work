SELECT DISTINCT * FROM OPENQUERY(DUSTER,
'
	SELECT DISTINCT
		LN10.BF_SSN,
		COALESCE(REPLACE(PD10.DD_BRT,''-'',''''),''        '') AS DD_BRT,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_LST)), 26, '' '') AS DM_PRS_LST,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_1)), 20, '' '') AS DM_PRS_1,
		RPAD(PD10.DF_SPE_ACC_ID, 28, '' '') AS DF_SPE_ACC_ID,
		REPLACE(CURRENT DATE,''-'','''') AS ActiveDutyStatusDate,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_MID)), 20, '' '') AS DM_PRS_MID
	FROM
		OLWHRM1.LN10_LON LN10
		INNER JOIN OLWHRM1.PD10_PRS_NME PD10
			ON PD10.DF_PRS_ID = LN10.BF_SSN
	WHERE
		LN10.LD_LON_1_DSB <= ''2014-07-01''
		OR LN10.LA_CUR_PRI > 0.00
		OR LN10.LD_PIF_RPT >= ''2008-08-14'' 

	UNION ALL
	
	SELECT DISTINCT
		LN20.LF_EDS,
		COALESCE(REPLACE(PD10.DD_BRT,''-'',''''),''        '') AS DD_BRT,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_LST)), 26, '' '') AS DM_PRS_LST,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_1)), 20, '' '') AS DM_PRS_1,
		RPAD(PD10.DF_SPE_ACC_ID, 28, '' '') AS DF_SPE_ACC_ID,
		REPLACE(CURRENT DATE,''-'','''') AS ActiveDutyStatusDate,
		RPAD(RTRIM(LTRIM(PD10.DM_PRS_MID)), 20, '' '') AS DM_PRS_MID
	FROM
		OLWHRM1.LN10_LON LN10
		INNER JOIN OLWHRM1.LN20_EDS LN20
			ON LN20.BF_SSN = LN10.BF_SSN
			AND LN20.LN_SEQ = LN10.LN_SEQ
			AND LN20.LF_EDS NOT LIKE ''P%''
		INNER JOIN OLWHRM1.PD10_PRS_NME PD10
			ON PD10.DF_PRS_ID = LN20.LF_EDS
	WHERE
		LN10.LD_LON_1_DSB <= ''2014-07-01''
		OR LN10.LA_CUR_PRI > 0.00
		OR LN10.LD_PIF_RPT >= ''2008-08-14'' 
'
)

