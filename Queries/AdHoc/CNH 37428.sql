--AYXX.PF_REQ_ACT = DICSK

--OUTPUT:
--PDXX.DM_PRS_X =  First Name
--PDXX.DM_PRS_LST = Last Name
--LNXX.BF_SSN = SSN
--AYXX.PF_REQ_ACT = DICSK

SELECT DISTINCT
	PDXX.DM_PRS_X AS [First Name],
	PDXX.DM_PRS_LST AS [Last Name],
	LNXX.BF_SSN AS SSN,
	AYXX.LD_ATY_REQ_RCV AS [DICSK Requested]
FROM
	CDW..LNXX_LON LNXX
	INNER JOIN CDW..LNXX_LON_ATY LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
		AND LNXX.LF_DOE_SCL_ORG = 'XXXXXXXX'
	INNER JOIN CDW..PDXX_PRS_NME PDXX
		ON LNXX.BF_SSN = PDXX.DF_PRS_ID
	LEFT JOIN CDW..AYXX_BR_LON_ATY AYXX
		ON LNXX.BF_SSN = AYXX.BF_SSN
		AND LNXX.LN_ATY_SEQ = AYXX.LN_ATY_SEQ
		AND AYXX.PF_REQ_ACT = 'DICSK'