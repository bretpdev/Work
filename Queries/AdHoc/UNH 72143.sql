USE ODW
GO
DROP TABLE IF EXISTS #POP

SELECT
	*
INTO #POP
FROM
	ODW..DC01_LON_CLM_INF DC01
WHERE
	LC_STA_DC10 = '04'
	AND DC01.LD_LDR_POF < '03/13/2020'
	AND DC01.LC_PCL_REA IN ('DB','DF','IN')
	--AND DC01.LC_AUX_STA = ''

SELECT 
	P.*,
	DC01.*
FROM 
	#POP P
	INNER JOIN ODW..DC01_LON_CLM_INF DC01
		ON DC01.AF_APL_ID = P.AF_APL_ID
		AND DC01.AF_APL_ID_SFX = P.AF_APL_ID_SFX
		AND DC01.BF_SSN = P.BF_SSN
	--INNER JOIN ODW..DC11_LON_FAT DC11
	--	ON DC11.AF_APL_ID = DC01.AF_APL_ID
	--	AND DC11.AF_APL_ID_SFX = DC01.AF_APL_ID_SFX
	--	AND DC11.LC_REV_IND_TYP != ''
	--	AND DC11.LD_TRX_EFF >= '03/13/2020'
WHERE
	DC01.LC_STA_DC10 = '03'
	AND DC01.LD_LDR_POF >= '03/13/2020'
	AND DC01.LC_PCL_REA IN ('DB','DF','IN')