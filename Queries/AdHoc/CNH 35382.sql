	*
FROM
	OPENQUERY
	(LEGEND,
		'
			SELECT
				PDXX.DF_SPE_ACC_ID,
				LNXX.LN_SEQ
			FROM
				PKUB.LNXX_LON LNXX
				INNER JOIN PKUB.PDXX_PRS_NME PDXX
					ON LNXX.BF_SSN = PDXX.DF_PRS_ID
			WHERE
				LNXX.IC_LON_PGM = ''DLSSPL''
				AND LNXX.LF_RGL_CAT_LPXX = ''XXXXXXX''
		'
	)