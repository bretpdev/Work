IF OBJECT_ID('tempdb..#POP') IS NOT NULL 
	DROP TABLE #POP

SELECT 
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	LNXX.LN_DLQ_MAX,
	CASE WHEN AYXX.BF_SSN IS NULL THEN X ELSE X END AS HASARC
INTO #POP
FROM 
	CDW..LNXX_LON_DLQ_HST LNXX 
	LEFT JOIN CDW..AYXX_BR_LON_ATY AYXX 
		ON AYXX.BF_SSN = LNXX.BF_SSN 
		AND AYXX.PF_REQ_ACT = 'DLFDL'
WHERE 
	LNXX.LC_STA_LONXX = 'X' 
	AND LNXX.LN_DLQ_MAX >= XXX 
	AND CAST(LNXX.LD_DLQ_MAX AS DATE) = CAST(GETDATE() -X AS DATE) 
	


SELECT 
	* 
FROM 
	#POP
WHERE
	HASARC = X

SELECT 
	* 
FROM 
	#POP
WHERE
	HASARC = X