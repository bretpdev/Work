SELECT
	LNXX.BF_SSN,
	LNXX.LN_SEQ,
	CAST(FSXX.LF_FED_AWD AS VARCHAR) + CAST(FSXX.LN_FED_AWD_SEQ AS VARCHAR) AS AWARD_ID,
	LNXX.IF_LON_SRV_DFL_LON
FROM
	CDW..LNXX_LON LNXX
	JOIN CDW..FSXX_DL_LON FSXX
		ON LNXX.BF_SSN = FSXX.BF_SSN
		AND LNXX.LN_SEQ = FSXX.LN_SEQ
	JOIN CDW..LNXX_RPD_PIO_CVN LNXX
		ON LNXX.BF_SSN = LNXX.BF_SSN
		AND LNXX.LN_SEQ = LNXX.LN_SEQ
WHERE
	LNXX.LC_STA_LONXX = 'L'