SELECT
	PD10.DF_SPE_ACC_ID,
	RS.LN_SEQ,
	RS.LC_TYP_SCH_DIS,
	RS10.BD_CRT_RS05,
	RS10.BN_IBR_SEQ,
	CONVERT(VARCHAR(10), RS05.BD_ANV_QLF_IBR, 101) BD_ANV_QLF_IBR 
FROM
	UDW.CALC.RepaymentSchedules RS
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = RS.BF_SSN
	INNER JOIN UDW..RS10_BR_RPD RS10
		ON RS.BF_SSN = RS10.BF_SSN
		AND RS.LN_RPS_SEQ = RS10.LN_RPS_SEQ
	INNER JOIN UDW..RS05_IBR_RPS RS05
		ON RS05.BF_SSN = RS10.BF_SSN
		AND RS05.BN_IBR_SEQ = RS10.BN_IBR_SEQ
		AND RS05.BD_CRT_RS05 = RS10.BD_CRT_RS05
WHERE
	RS.LC_TYP_SCH_DIS = 'IB'
	AND CurrentGradation = 1
	AND RS05.BD_ANV_QLF_IBR BETWEEN '03-13-2020' AND '09-30-2020'
ORDER BY	
	RS05.BD_ANV_QLF_IBR