SELECT
	PD10.DF_SPE_ACC_ID,
	PD30.*
FROM
	UDW..PD30_PRS_ADR PD30
	INNER JOIN UDW..PD10_PRS_NME PD10
		ON PD10.DF_PRS_ID = PD30.DF_PRS_ID
WHERE
	PD30.DI_VLD_ADR = 'Y'
	AND PD30.DF_PRS_ID LIKE ('[0-9]%')
	AND
		(
			ISNULL(DM_FGN_CNY , '') != ''
			OR ISNULL(DM_FGN_ST, '') != ''
			OR ISNULL(DC_FGN_CNY, '') != ''
		)