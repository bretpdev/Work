SELECT DISTINCT
	PD10.DF_SPE_ACC_ID,
	'(' + PD42.DN_DOM_PHN_ARA + ')' + PD42.DN_DOM_PHN_XCH + '-' + PD42.DN_DOM_PHN_LCL AS PNH_FORMATTED,
	PD42.DN_DOM_PHN_ARA + PD42.DN_DOM_PHN_XCH + PD42.DN_DOM_PHN_LCL AS PNH_NO_FORMATTING,
	PD42.DC_ALW_ADL_PHN
FROM
	UDW..PD10_PRS_NME PD10
	INNER JOIN UDW..PD42_PRS_PHN PD42
		ON PD10.DF_PRS_ID = PD42.DF_PRS_ID
	INNER JOIN UDW..LN10_LON LN10
		ON PD42.DF_PRS_ID = LN10.BF_SSN
		AND LN10.LC_STA_LON10 = 'R'
		AND LN10.LA_CUR_PRI > 0.00
	WHERE
		PD42.DC_PHN = 'M'
		--AND PD42.DC_ALW_ADL_PHN = 'Y'
		AND PD42.DI_PHN_VLD = 'Y'

