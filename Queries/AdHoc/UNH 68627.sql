SELECT DISTINCT
	LN10.IF_DOE_LDR,
	LN35.IF_BND_ISS
FROM 
	UDW..LN35_LON_OWN LN35
	INNER JOIN UDW..LN10_LON LN10
		ON LN10.BF_SSN = LN35.BF_SSN
		AND LN10.LN_SEQ = LN35.LN_SEQ
WHERE 
	IF_BND_ISS IN 
	(
		'93002011',
		'9301999O',
		'9302001R',
		'9302001S',
		'932006DD',
		'932010EE'
	)
	AND LC_STA_LON35 = 'A'
	AND GETDATE() BETWEEN LD_OWN_EFF_SR AND ISNULL(LD_OWN_EFF_END, '01-01-2099')
	AND LA_CUR_PRI > 0
	AND LC_STA_LON10 = 'R'
