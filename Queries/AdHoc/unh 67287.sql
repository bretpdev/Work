USE uDW
GO

SELECT
	MONTH(LD_FAT_EFF) AS [MONTH],
	YEAR(LD_FAT_EFF) AS [YEAR],
	COUNT(DISTINCT BF_SSN) AS BORROWER_COUNT,
	COUNT(*) AS LOAN_COUNT
--	*
FROM
	uDW..LN90_FIN_ATY LN90
WHERE
	LC_STA_LON90 = 'A'
	AND ISNULL(LC_FAT_REV_REA,'') = ''
	AND PC_FAT_TYP = '50' 
	AND PC_FAT_SUB_TYP = '02'
	AND LD_FAT_EFF BETWEEN '09-01-2019' AND '02-28-2020'
GROUP BY
	MONTH(LD_FAT_EFF),
	YEAR(LD_FAT_EFF) 