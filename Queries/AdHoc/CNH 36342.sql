--RSXX, RSXX, RSXX and LNXX for account XXXXXXXXXX

--RSXX
SELECT 
	RSXX.*
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..RSXX_IBR_RPS RSXX
		ON PDXX.DF_PRS_ID = RSXX.BF_SSN
WHERE
	PDXX.DF_SPE_ACC_ID = 'XXXXXXXXXX'


--RSXX
SELECT 
	RSXX.*
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..RSXX_BR_RPD RSXX
		ON PDXX.DF_PRS_ID = RSXX.BF_SSN
WHERE
	PDXX.DF_SPE_ACC_ID = 'XXXXXXXXXX'


--RSXX
SELECT *
FROM
	OPENQUERY
	(
		LEGEND,
		'
		SELECT 
			RSXX.*
		FROM
			PKUB.PDXX_PRS_NME PDXX
			INNER JOIN PKUB.RSXX_IBR_IRL_LON RSXX
				ON PDXX.DF_PRS_ID = RSXX.BF_SSN
		WHERE
			PDXX.DF_SPE_ACC_ID = ''XXXXXXXXXX''
		'
	)



--LNXX
SELECT 
	LNXX.*
FROM
	CDW..PDXX_PRS_NME PDXX
	INNER JOIN CDW..LNXX_LON_RPS LNXX
		ON PDXX.DF_PRS_ID = LNXX.BF_SSN
WHERE
	PDXX.DF_SPE_ACC_ID = 'XXXXXXXXXX'