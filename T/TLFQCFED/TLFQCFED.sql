INSERT INTO CLS.tlfqcfed.TeacherLoanForgiveness
	(
		DF_SPE_ACC_ID,
		WF_QUE,
		WF_SUB_QUE,
		WN_CTL_TSK,
		PF_REQ_ACT,
		WD_ACT_REQ,
		WC_STA_WQUE20,
		WF_LST_DTS_WQ20
	)

SELECT 
	PD10.DF_SPE_ACC_ID,
	WQ20.WF_QUE,
	WQ20.WF_SUB_QUE,
	WQ20.WN_CTL_TSK,
	WQ20.PF_REQ_ACT,
	WQ20.WD_ACT_REQ,
	WQ20.WC_STA_WQUE20,
	WQ20.WF_LST_DTS_WQ20
FROM
	CDW..PD10_PRS_NME PD10
	INNER JOIN CDW..WQ20_TSK_QUE WQ20
		ON PD10.DF_PRS_ID = WQ20.BF_SSN
WHERE
	WQ20.WC_STA_WQUE20 NOT IN ('C','X')
	AND WQ20.WF_QUE = '15'
	AND WQ20.WF_SUB_QUE = '01'
	AND WQ20.PF_REQ_ACT = 'DITLF'
