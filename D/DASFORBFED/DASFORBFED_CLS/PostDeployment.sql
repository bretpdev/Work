MERGE dasforbfed.ForbearanceTypes AS t USING
(
	SELECT 1 [ForbearanceTypeId], '90 Day Forb' [Description], 'Disaster Administrative applied due to Natural Disaster' [ActivityComment]
	UNION
	SELECT 2, '30 Day Forb', 'Disaster Administrative applied due to Natural Disaster'
	UNION
	SELECT 3, 'Historical Disasters', 'Disasters before DisasterId was added.'
) AS s
ON t.ForbearanceTypeId = s.ForbearanceTypeId
WHEN NOT MATCHED BY target THEN
	INSERT (ForbearanceTypeId, [Description], ActivityComment)
	VALUES (s.ForbearanceTypeId, [Description], s.ActivityComment)
WHEN NOT MATCHED BY source THEN
	DELETE
WHEN MATCHED THEN
	UPDATE SET ActivityComment = s.ActivityComment, [Description] = s.Description
;

