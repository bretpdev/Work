CREATE PROCEDURE [dbo].[LT_US06BFNBIL_FormFields]
	@BF_SSN VARCHAR(9),
	@IsCoborrower BIT = 0,
	@RN_ATY_SEQ_PRC INT
AS
BEGIN
	
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DECLARE	@LetterId VARCHAR(10) = 'US06BFNBIL'
DECLARE @PF_REQ_ACT VARCHAR(5) = (SELECT AC11.PF_REQ_ACT FROM AC11_ACT_REQ_LTR AC11 WHERE PF_LTR = @LetterId)

IF @IsCoborrower = 0
	BEGIN
		SELECT
			SUM(ISNULL(LN10.LA_CUR_PRI,0.00) + ISNULL(MAX_BILL.LA_LTE_FEE_OTS_PRT,0.00) + ISNULL(DW01.WA_TOT_BRI_OTS,0.00)) AS [Total Payoff Amount],
			CONVERT(VARCHAR, RS.TermStartDate, 101) AS [Payoff Date]
		FROM	
			PD10_PRS_NME PD10
			INNER JOIN LN10_LON LN10
				ON LN10.BF_SSN = PD10.DF_PRS_ID
			INNER JOIN DW01_DW_CLC_CLU DW01
				ON DW01.BF_SSN = LN10.BF_SSN
				AND DW01.LN_SEQ = LN10.LN_SEQ
			INNER JOIN calc.RepaymentSchedules RS
				ON RS.BF_SSN = LN10.BF_SSN
				AND RS.LN_SEQ = LN10.LN_SEQ
				AND RS.CurrentGradation = 1
			INNER JOIN
			(	
				SELECT 
					LN80.BF_SSN,
					LN80.LN_SEQ,
					MaxLN80.MAX_CREATE_DATE AS MAX_CREATE_DATE,
					ISNULL(LN80.LA_LTE_FEE_OTS_PRT,0.00) AS LA_LTE_FEE_OTS_PRT
				FROM 
					LN80_LON_BIL_CRF LN80
					INNER JOIN PD10_PRS_NME PD10
						ON PD10.DF_PRS_ID = LN80.BF_SSN
					INNER JOIN 
					(
						SELECT
							LN80MaxBill.BF_SSN,
							LN80MaxBill.LN_SEQ,
							CAST(MAX(LN80MaxBill.LD_BIL_CRT) AS DATE) AS MAX_CREATE_DATE
						FROM
							LN80_LON_BIL_CRF LN80MaxBill
						WHERE
							LN80MaxBill.LC_STA_LON80 = 'A'
						GROUP BY
							LN80MaxBill.BF_SSN,
							LN80MaxBill.LN_SEQ
					) MaxLN80
						ON MaxLN80.BF_SSN = LN80.BF_SSN
						AND MaxLN80.LN_SEQ = LN80.LN_SEQ
						AND MaxLN80.MAX_CREATE_DATE = LN80.LD_BIL_CRT
				WHERE
					PD10.DF_PRS_ID = @BF_SSN
					AND LN80.LC_STA_LON80 = 'A'
			) MAX_BILL
				ON MAX_BILL.BF_SSN = LN10.BF_SSN
				AND MAX_BILL.LN_SEQ = LN10.LN_SEQ
			INNER JOIN
			(
				SELECT
					LN72.BF_SSN, 
					LN72.LN_SEQ,
					LN72.LR_ITR,
					ROW_NUMBER() OVER (PARTITION BY LN72.BF_SSN, LN72.LN_SEQ ORDER BY LF_LST_DTS_LN72 DESC) AS SEQ
				FROM
					LN72_INT_RTE_HST LN72
					INNER JOIN PD10_PRS_NME PD10 
						ON PD10.DF_PRS_ID = LN72.BF_SSN
					WHERE
						LN72.LC_STA_LON72 = 'A'
						AND	CAST(GETDATE() AS DATE) BETWEEN LN72.LD_ITR_EFF_BEG AND LN72.LD_ITR_EFF_END
						AND PD10.DF_PRS_ID = @BF_SSN
			) LN72 
				ON LN10.BF_SSN = LN72.BF_SSN
				AND LN10.LN_SEQ = LN72.LN_SEQ
				AND LN72.SEQ = 1
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND AY10.BF_SSN = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ 
			LEFT JOIN FormatTranslation FMT
				ON FMT.Start = LN10.IC_LON_PGM
				AND FMT.FmtName = '$LNPROG'
		WHERE
			PD10.DF_PRS_ID = @BF_SSN
		GROUP BY
			RS.TermStartDate
	END
ELSE --Begin coborrower portion
	BEGIN
		SELECT
			SUM(ISNULL(LN10.LA_CUR_PRI,0.00) + ISNULL(MAX_BILL.LA_LTE_FEE_OTS_PRT,0.00) + ISNULL(DW01.WA_TOT_BRI_OTS,0.00)) AS [Total Payoff Amount],
			CONVERT(VARCHAR, RS.TermStartDate, 101) AS [Payoff Date]
		FROM	
			PD10_PRS_NME PD10
			INNER JOIN LN20_EDS LN20
				ON LN20.LF_EDS = PD10.DF_PRS_ID
				AND LN20.LC_EDS_TYP = 'M'
				AND LN20.LC_STA_LON20 = 'A'
			INNER JOIN LN10_LON LN10
				ON LN10.BF_SSN = LN20.BF_SSN
				AND LN10.LN_SEQ = LN20.LN_SEQ
			INNER JOIN DW01_DW_CLC_CLU DW01
				ON DW01.BF_SSN = LN10.BF_SSN
				AND DW01.LN_SEQ = LN10.LN_SEQ
			INNER JOIN calc.RepaymentSchedules RS
				ON RS.BF_SSN = LN10.BF_SSN
				AND RS.LN_SEQ = LN10.LN_SEQ
				AND RS.CurrentGradation = 1
			INNER JOIN
			(	
				SELECT 
					LN80.BF_SSN,
					LN80.LN_SEQ,
					MaxLN80.MAX_CREATE_DATE AS MAX_CREATE_DATE,
					ISNULL(LN80.LA_LTE_FEE_OTS_PRT,0.00) AS LA_LTE_FEE_OTS_PRT
				FROM 
					LN80_LON_BIL_CRF LN80
					INNER JOIN LN20_EDS LN20
						ON LN20.BF_SSN = LN80.BF_SSN
						AND LN20.LN_SEQ = LN80.LN_SEQ
						AND LN20.LC_EDS_TYP = 'M'
						AND LN20.LC_STA_LON20 = 'A'
					INNER JOIN PD10_PRS_NME PD10
						ON PD10.DF_PRS_ID = LN20.LF_EDS
					INNER JOIN 
					(
						SELECT
							LN80MaxBill.BF_SSN,
							LN80MaxBill.LN_SEQ,
							CAST(MAX(LN80MaxBill.LD_BIL_CRT) AS DATE) AS MAX_CREATE_DATE
						FROM
							LN80_LON_BIL_CRF LN80MaxBill
							INNER JOIN LN20_EDS LN20
								ON LN20.BF_SSN = LN80MaxBill.BF_SSN
								AND LN20.LN_SEQ = LN80MaxBill.LN_SEQ
								AND LN20.LC_EDS_TYP = 'M'
								AND LN20.LC_STA_LON20 = 'A'
							INNER JOIN PD10_PRS_NME PD10
								ON PD10.DF_PRS_ID = LN20.LF_EDS
						WHERE
							PD10.DF_PRS_ID = @BF_SSN
							AND LN80MaxBill.LC_STA_LON80 = 'A'
						GROUP BY
							LN80MaxBill.BF_SSN,
							LN80MaxBill.LN_SEQ
					) MaxLN80
						ON MaxLN80.BF_SSN = LN80.BF_SSN
						AND MaxLN80.LN_SEQ = LN80.LN_SEQ
						AND MaxLN80.MAX_CREATE_DATE = LN80.LD_BIL_CRT
				WHERE
					PD10.DF_PRS_ID = @BF_SSN
					AND LN80.LC_STA_LON80 = 'A'			
			) MAX_BILL
				ON MAX_BILL.BF_SSN = LN10.BF_SSN
				AND MAX_BILL.LN_SEQ = LN10.LN_SEQ
			INNER JOIN
			(
				SELECT
					LN72.BF_SSN, 
					LN72.LN_SEQ,
					LN72.LR_ITR,
					ROW_NUMBER() OVER (PARTITION BY LN72.BF_SSN, LN72.LN_SEQ ORDER BY LF_LST_DTS_LN72 DESC) AS SEQ
				FROM
					LN72_INT_RTE_HST LN72
					INNER JOIN LN20_EDS LN20
						ON LN20.BF_SSN = LN72.BF_SSN
						AND LN20.LN_SEQ = LN72.LN_SEQ
						AND LN20.LC_EDS_TYP = 'M'
						AND LN20.LC_STA_LON20 = 'A'
					INNER JOIN PD10_PRS_NME PD10 
						ON PD10.DF_PRS_ID = LN20.LF_EDS
					WHERE
						LN72.LC_STA_LON72 = 'A'
						AND	CAST(GETDATE() AS DATE) BETWEEN LN72.LD_ITR_EFF_BEG AND LN72.LD_ITR_EFF_END
						AND PD10.DF_PRS_ID = @BF_SSN
			) LN72 
				ON LN10.BF_SSN = LN72.BF_SSN
				AND LN10.LN_SEQ = LN72.LN_SEQ
				AND LN72.SEQ = 1
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
					INNER JOIN LN20_EDS LN20
						ON LN20.BF_SSN = LN85.BF_SSN
						AND LN20.LN_SEQ = LN85.LN_SEQ
						AND LN20.LC_STA_LON20 = 'A'
						AND LN20.LC_EDS_TYP = 'M'
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND LN20.LF_EDS = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ 
			LEFT JOIN FormatTranslation FMT
				ON FMT.Start = LN10.IC_LON_PGM
				AND FMT.FmtName = '$LNPROG'
		WHERE
			PD10.DF_PRS_ID = @BF_SSN
		GROUP BY
			RS.TermStartDate
	END
END
