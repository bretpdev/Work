CREATE PROCEDURE [dbo].[LT_US10BTREFC_FormFields]
	@BF_SSN  VARCHAR(9),
	@IsCoborrower BIT = 0,
	@RN_ATY_SEQ_PRC INT
AS

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
--Special case letter to references

	SELECT
		RTRIM(PD10.DM_PRS_1) + ' ' + RTRIM(PD10.DM_PRS_LST) AS BorrName
	FROM
		PD10_PRS_NME PD10
	WHERE
		PD10.DF_PRS_ID = @BF_SSN