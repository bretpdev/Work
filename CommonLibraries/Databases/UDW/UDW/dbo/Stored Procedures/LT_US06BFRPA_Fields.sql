CREATE  PROCEDURE [dbo].[LT_US06BFRPA_Fields]
	@BF_SSN char(9)
AS
BEGIN

	SELECT DISTINCT
		'$' + CONVERT(VARCHAR(15), SUM(LN60.LA_ACL_RDC_PAY), 1) AS LA_ACL_RDC_PAY
	FROM
		UDW.[dbo].[FB10_BR_FOR_REQ] FB10
		INNER JOIN UDW..LN60_BR_FOR_APV LN60
			ON LN60.BF_SSN = FB10.BF_SSN
			AND LN60.LF_FOR_CTL_NUM = FB10.LF_FOR_CTL_NUM
			
	WHERE
		FB10.BF_SSN = @BF_SSN
		AND
		FB10.LC_FOR_TYP = '31'
		AND
		FB10.LC_FOR_STA = 'A'
		AND
		FB10.LC_STA_FOR10 = 'A'
		AND
		LN60.LD_FOR_END > GETDATE()
		AND 
		LN60.LC_STA_LON60 = 'A'

END