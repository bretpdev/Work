CREATE PROCEDURE [dbo].[spMD_BorrowerHasEndorsers]
	@BorrowerAccountNumber char(10)
AS
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	IF (EXISTS(SELECT * FROM LN20_EDS LN20 INNER JOIN PD10_PRS_NME PD10 ON PD10.DF_PRS_ID = LN20.BF_SSN WHERE PD10.DF_SPE_ACC_ID = @BorrowerAccountNumber)) 
		SELECT CAST(1 AS BIT)
	ELSE 
		SELECT CAST(0 AS BIT)
	
RETURN 0