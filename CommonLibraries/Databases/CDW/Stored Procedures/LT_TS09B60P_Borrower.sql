CREATE PROCEDURE [dbo].[LT_TS09B60P_Borrower]
	@AccountNumber		char(10)
AS
BEGIN
	SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

	SELECT
		LN16.DefaultDate AS DEFAULTDATE,
		DUE.CUR_DUE AS DELINQUENCYPAYMENT
	FROM
		(
			SELECT
				DF_SPE_ACC_ID,
				CONVERT(CHAR(10),DATEADD(D,270 - MAX(LN_DLQ_MAX),GETDATE()),101) AS DefaultDate
			FROM 
				LN16_Delinquency
			WHERE 
				DF_SPE_ACC_ID = @AccountNumber
			GROUP BY
				DF_SPE_ACC_ID
		) LN16
		JOIN 
			(
				SELECT 
					BL10.DF_SPE_ACC_ID,
					SUM(COALESCE(BL10.LA_BIL_CUR_DU,0) - COALESCE(BL10.LA_TOT_BIL_STS,0)) AS CUR_DUE
				FROM 
					BL10_Bill BL10
				WHERE
					BL10.DF_SPE_ACC_ID = @AccountNumber
					AND CAST(BL10.LD_BIL_DU_LON AS DATE) < DATEADD(M,1,GETDATE())
					AND BL10.LC_IND_BIL_SNT <> '0'
				GROUP BY
					BL10.DF_SPE_ACC_ID
			) DUE
			ON LN16.DF_SPE_ACC_ID = DUE.DF_SPE_ACC_ID
	WHERE 
		LN16.DF_SPE_ACC_ID = @AccountNumber
END

IF @@ROWCOUNT = 0 
	BEGIN
		RAISERROR('No data returned. ([dbo].[LT_TS09B60P_Borrower])',11,2)
	END

GO
GRANT EXECUTE
    ON OBJECT::[dbo].[LT_TS09B60P_Borrower] TO [db_executor]
    AS [dbo];
