CREATE VIEW dbo.[DFR_Remaining]
AS
SELECT DISTINCT 
                      A.DF_SPE_ACC_ID, CASE WHEN A.LC_DFR_TYP IN ('01', '04') THEN '01' WHEN A.LC_DFR_TYP IN ('29', '41', '42', '43') 
                      THEN '29' ELSE A.LC_DFR_TYP END AS LC_DFR_TYP, SUM(A.MONTHS_USED) AS MONTHS_USED, MAX(B.MAX_MONTHS) - SUM(A.MONTHS_USED) 
                      AS MONTHS_LEFT
FROM         (SELECT DISTINCT DF_SPE_ACC_ID, LF_DFR_CTL_NUM, LC_DFR_TYP, MONTHS_USED
                       FROM          dbo.DF10_Deferment) AS A LEFT OUTER JOIN
                      dbo.LST_Deferment AS B ON A.LC_DFR_TYP = B.DEFERMENT_CODE
GROUP BY A.DF_SPE_ACC_ID, A.LC_DFR_TYP

GO
