CREATE PROCEDURE [dbo].[UpdateEndorserEcorrOnRecord]
	@DF_SPE_ACC_ID VARCHAR(10),
	@RM_APL_PGM_PRC VARCHAR(8),
	@RT_RUN_SRT_DTS_PRC DATETIME,
	@RN_SEQ_LTR_CRT_PRC INT,
	@RN_SEQ_REC_PRC INT,
	@RM_DSC_LTR_PRC VARCHAR(10),
	@OnEcorr BIT
AS
	
UPDATE	LT20_LTR_REQ_PRC
SET
	OnEcorr = @OnEcorr
WHERE
	DF_SPE_ACC_ID = @DF_SPE_ACC_ID
	AND RM_APL_PGM_PRC = @RM_APL_PGM_PRC
	AND RT_RUN_SRT_DTS_PRC = @RT_RUN_SRT_DTS_PRC
	AND RN_SEQ_LTR_CRT_PRC = @RN_SEQ_LTR_CRT_PRC
	AND RN_SEQ_REC_PRC = @RN_SEQ_REC_PRC
	AND RM_DSC_LTR_PRC = @RM_DSC_LTR_PRC
	AND EcorrDocumentCreatedAt IS NULL
	AND InactivatedAt IS NULL
	
RETURN 0