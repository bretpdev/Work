CREATE PROCEDURE [dbo].[LT_TS06BF201_Loans]
	@AccountNumber CHAR(10),
	@IsCoborrower BIT = 0
AS
BEGIN
	
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DECLARE	@LetterId VARCHAR(10) = 'TS06BF201'
DECLARE @PF_REQ_ACT VARCHAR(5) = (SELECT AC11.PF_REQ_ACT FROM AC11_ACT_REQ_LTR AC11 WHERE PF_LTR = @LetterId)

IF @IsCoborrower = 0
	BEGIN
		SELECT
			COALESCE(FMT.Label, LN10.IC_LON_PGM) AS [Loan Program],
			CONVERT(VARCHAR, LN10.LD_LON_1_DSB, 101) AS [First Disbursement Date],
			'$' + CAST(LN10.LA_CUR_PRI AS VARCHAR) AS [Current Principal Balance],
			CONVERT(VARCHAR, DW01.WD_LON_RPD_SR, 101) AS [Repayment Start Date],
			COALESCE(FMTType.Label, FB10.LC_FOR_TYP) AS [Forbearance Type]
		FROM
			LN10_LON LN10
			INNER JOIN PD10_PRS_NME PD10
				ON PD10.DF_PRS_ID = LN10.BF_SSN
			INNER JOIN DW01_DW_CLC_CLU DW01
				ON DW01.BF_SSN = PD10.DF_PRS_ID
				AND DW01.LN_SEQ = LN10.LN_SEQ
			INNER JOIN LN60_BR_FOR_APV LN60
				ON LN60.BF_SSN = LN10.BF_SSN
				AND LN60.LN_SEQ = LN10.LN_SEQ
				AND LN60.LC_STA_LON60 = 'I'
				AND LN60.LC_FOR_RSP != '003' --DENIED FORBEARANCE
			INNER JOIN FB10_BR_FOR_REQ FB10
				ON FB10.BF_SSN = LN60.BF_SSN
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
				AND FB10.LC_STA_FOR10 = 'I'
				AND FB10.LC_FOR_STA = 'A'
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ,
					AY10.LD_ATY_REQ_RCV
				FROM
					LN85_LON_ATY LN85
					INNER JOIN --GETS THE MOST RECENT ARC LEFT ON THE BORROWERS ACCOUNT TO GET THE LOAN SEQ'S THE LETTER APPLIES TO
					(
						SELECT
							AY10.BF_SSN,
							MAX(AY10.LN_ATY_SEQ) AS LN_ATY_SEQ,
							MAX(AY10.LD_ATY_REQ_RCV) AS LD_ATY_REQ_RCV
						FROM 
							AY10_BR_LON_ATY AY10
							INNER JOIN PD10_PRS_NME PD10
								ON PD10.DF_PRS_ID = AY10.BF_SSN
						WHERE
							AY10.PF_REQ_ACT = @PF_REQ_ACT
							AND PD10.DF_SPE_ACC_ID = @AccountNumber
						GROUP BY
							AY10.BF_SSN
					)AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ
				AND CAST(LN85.LD_ATY_REQ_RCV AS DATE) = CAST(LN60.LF_LST_DTS_LN60 AS DATE)
			LEFT JOIN FormatTranslation FMT
				ON FMT.Start = LN10.IC_LON_PGM
				AND FMT.FmtName = '$LNPROG'
			LEFT JOIN FormatTranslation FMTType
				ON FMTType.Start = FB10.LC_FOR_TYP
				AND FMTType.FmtName = '$FORSTA'
		WHERE
			PD10.DF_SPE_ACC_ID = @AccountNumber
	END
ELSE
	BEGIN
		SELECT
			COALESCE(FMT.Label, LN10.IC_LON_PGM) AS [Loan Program],
			CONVERT(VARCHAR, LN10.LD_LON_1_DSB, 101) AS [First Disbursement Date],
			'$' + CAST(LN10.LA_CUR_PRI AS VARCHAR) AS [Current Principal Balance],
			CONVERT(VARCHAR, DW01.WD_LON_RPD_SR, 101) AS [Repayment Start Date],
			COALESCE(FMTType.Label, FB10.LC_FOR_TYP) AS [Forbearance Type]
		FROM
			LN10_LON LN10
			INNER JOIN LN20_EDS LN20
				ON LN20.BF_SSN = LN10.BF_SSN
				AND LN20.LN_SEQ = LN10.LN_SEQ
				AND LN20.LC_EDS_TYP = 'M'
				AND LN20.LC_STA_LON20 = 'A'
			INNER JOIN PD10_PRS_NME PD10
				ON PD10.DF_PRS_ID = LN20.LF_EDS
			INNER JOIN DW01_DW_CLC_CLU DW01
				ON DW01.BF_SSN = PD10.DF_PRS_ID
				AND DW01.LN_SEQ = LN10.LN_SEQ
			INNER JOIN LN60_BR_FOR_APV LN60
				ON LN60.BF_SSN = LN10.BF_SSN
				AND LN60.LN_SEQ = LN10.LN_SEQ
				AND LN60.LC_STA_LON60 = 'I'
				AND LN60.LC_FOR_RSP != '003' --DENIED FORBEARANCE
			INNER JOIN FB10_BR_FOR_REQ FB10
				ON FB10.BF_SSN = LN60.BF_SSN
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
				AND FB10.LC_STA_FOR10 = 'I'
				AND FB10.LC_FOR_STA = 'A'
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ,
					AY10.LD_ATY_REQ_RCV
				FROM
					LN85_LON_ATY LN85
					INNER JOIN --GETS THE MOST RECENT ARC LEFT ON THE BORROWERS ACCOUNT TO GET THE LOAN SEQ'S THE LETTER APPLIES TO
					(
						SELECT
							AY10.BF_SSN,
							MAX(AY10.LN_ATY_SEQ) AS LN_ATY_SEQ,
							MAX(AY10.LD_ATY_REQ_RCV) AS LD_ATY_REQ_RCV
						FROM 
							AY10_BR_LON_ATY AY10
							INNER JOIN LN20_EDS LN20
								ON LN20.BF_SSN = AY10.BF_SSN
								AND LN20.LC_EDS_TYP = 'M'
								AND LN20.LC_STA_LON20 = 'A'
							INNER JOIN PD10_PRS_NME PD10
								ON PD10.DF_PRS_ID = LN20.LF_EDS
						WHERE
							AY10.PF_REQ_ACT = @PF_REQ_ACT
							AND PD10.DF_SPE_ACC_ID = @AccountNumber
						GROUP BY
							AY10.BF_SSN
					)AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ
				AND CAST(LN85.LD_ATY_REQ_RCV AS DATE) = CAST(LN60.LF_LST_DTS_LN60 AS DATE)
			LEFT JOIN FormatTranslation FMT
				ON FMT.Start = LN10.IC_LON_PGM
				AND FMT.FmtName = '$LNPROG'
			LEFT JOIN FormatTranslation FMTType
				ON FMTType.Start = FB10.LC_FOR_TYP
				AND FMTType.FmtName = '$FORSTA'
		WHERE
			PD10.DF_SPE_ACC_ID = @AccountNumber
	END
END
