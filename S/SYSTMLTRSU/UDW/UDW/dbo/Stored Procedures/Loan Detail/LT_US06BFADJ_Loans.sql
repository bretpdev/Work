CREATE PROCEDURE [dbo].[LT_US06BFADJ_Loans]
	@LetterId VARCHAR(10),
	@BF_SSN VARCHAR(9),
	@IsCoborrower BIT = 0,
	@RN_ATY_SEQ_PRC INT
AS
BEGIN

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DECLARE @PF_REQ_ACT VARCHAR(5) = (SELECT AC11.PF_REQ_ACT FROM AC11_ACT_REQ_LTR AC11 WHERE PF_LTR = @LetterId)

IF @IsCoborrower = 0
	BEGIN
		SELECT
			ISNULL(Forb.Label, FB10.LC_FOR_TYP) AS LetterTypeCode,
			LN10.LN_SEQ AS LN_SEQ,
			COALESCE(FT.Label, LN10.IC_LON_PGM, '') AS LoanProgram,
			CAST(LN60.LD_FOR_BEG AS DATE) AS BeginDate,
			CAST(LN60.LD_FOR_END AS DATE) AS EndDate
		FROM 
			LN10_LON LN10
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND AY10.BF_SSN = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ 
			INNER JOIN LN60_BR_FOR_APV LN60
				ON LN60.BF_SSN = LN10.BF_SSN
				AND LN60.LN_SEQ = LN10.LN_SEQ
				AND LN60.LC_FOR_RSP != '003'
				AND LN60.LC_STA_LON60 = 'A' 
				AND CAST(LN60.LD_FOR_APL AS DATE) BETWEEN CAST(DATEADD(DAY,-3,GETDATE()) AS DATE) AND CAST(GETDATE() AS DATE)
			INNER JOIN FB10_BR_FOR_REQ FB10
				ON FB10.BF_SSN = LN60.BF_SSN
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
				AND FB10.LC_STA_FOR10 = 'A'
				AND FB10.LC_FOR_STA = 'A'
			LEFT JOIN FormatTranslation FT
				ON LN10.IC_LON_PGM = FT.Start
				AND FT.FmtName = '$LNPROG'
			LEFT JOIN FormatTranslation Forb
				ON Forb.Start = FB10.LC_FOR_TYP
				AND Forb.FmtName = '$FORSTA'
		WHERE 
			LN10.BF_SSN = @BF_SSN
		ORDER BY
			LN10.LN_SEQ
	END
ELSE
	BEGIN
		SELECT
			ISNULL(Forb.Label,FB10.LC_FOR_TYP) AS LetterTypeCode,
			LN10.LN_SEQ AS LN_SEQ,
			COALESCE(FT.Label, LN10.IC_LON_PGM, '') AS LoanProgram,
			CAST(LN60.LD_FOR_BEG AS DATE) AS BeginDate,
			CAST(LN60.LD_FOR_END AS DATE) AS EndDate
		FROM 
			LN10_LON LN10
			INNER JOIN LN20_EDS LN20
				ON LN20.BF_SSN = LN10.BF_SSN
				AND LN20.LN_SEQ = LN10.LN_SEQ
				AND LN20.LC_STA_LON20 = 'A'
				AND LN20.LC_EDS_TYP = 'M'
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
					INNER JOIN LN20_EDS LN20
						ON LN20.BF_SSN = LN85.BF_SSN
						AND LN20.LN_SEQ = LN85.LN_SEQ
						AND LN20.LC_STA_LON20 = 'A'
						AND LN20.LC_EDS_TYP = 'M'
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND LN20.LF_EDS = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN10.BF_SSN
				AND LN85.LN_SEQ = LN10.LN_SEQ 
			INNER JOIN LN60_BR_FOR_APV LN60
				ON LN60.BF_SSN = LN10.BF_SSN
				AND LN60.LN_SEQ = LN10.LN_SEQ
				AND LN60.LC_FOR_RSP != '003'
				AND LN60.LC_STA_LON60 = 'A' 
				AND CAST(LN60.LD_FOR_APL AS DATE) BETWEEN CAST(DATEADD(DAY,-3,GETDATE()) AS DATE) AND CAST(GETDATE() AS DATE)
			INNER JOIN FB10_BR_FOR_REQ FB10
				ON FB10.BF_SSN = LN60.BF_SSN
				AND FB10.LF_FOR_CTL_NUM = LN60.LF_FOR_CTL_NUM
				AND FB10.LC_STA_FOR10 = 'A'
				AND FB10.LC_FOR_STA = 'A'
			LEFT JOIN FormatTranslation FT
				ON LN10.IC_LON_PGM = FT.Start
				AND FT.FmtName = '$LNPROG'
			LEFT JOIN FormatTranslation Forb
				ON Forb.Start = FB10.LC_FOR_TYP
				AND Forb.FmtName = '$FORSTA'
		WHERE 
			LN20.LF_EDS = @BF_SSN
		ORDER BY
			LN10.LN_SEQ
	END
END