CREATE PROCEDURE [dbo].[LT_US06BSCRAE_FormFields]
	@BF_SSN VARCHAR(9),
	@IsCoborrower BIT = 0,
	@RN_ATY_SEQ_PRC INT
AS
BEGIN

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

DECLARE @PF_REQ_ACT VARCHAR(5) = (SELECT AC11.PF_REQ_ACT FROM AC11_ACT_REQ_LTR AC11 WHERE PF_LTR = 'US06BSCRAE')

IF @IsCoborrower = 0
	BEGIN
		SELECT
			CONVERT(VARCHAR(10), MIN(LN72.LD_ITR_EFF_BEG), 101) AS EftDate
		FROM
			LN72_INT_RTE_HST LN72
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND AY10.BF_SSN = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN72.BF_SSN
				AND LN85.LN_SEQ = LN72.LN_SEQ 
		WHERE
			LN72.LD_ITR_EFF_BEG > CAST(GETDATE() AS DATE)
			AND LN72.LC_STA_LON72 = 'A'
			AND LN72.LC_INT_RDC_PGM != 'M'
			AND LN72.BF_SSN = @BF_SSN
	END
ELSE
	BEGIN
		SELECT
			CONVERT(VARCHAR(10), MIN(LN72.LD_ITR_EFF_BEG), 101) AS EftDate
		FROM
			LN72_INT_RTE_HST LN72
			INNER JOIN LN20_EDS LN20
				ON LN20.BF_SSN = LN72.BF_SSN
				AND LN20.LN_SEQ = LN72.LN_SEQ
				AND LN20.LC_STA_LON20 = 'A'
				AND LN20.LC_EDS_TYP = 'M'
			INNER JOIN
			(
				SELECT
					LN85.BF_SSN,
					LN85.LN_SEQ
				FROM
					LN85_LON_ATY LN85
					INNER JOIN AY10_BR_LON_ATY AY10
						ON AY10.BF_SSN = LN85.BF_SSN
						AND AY10.LN_ATY_SEQ = LN85.LN_ATY_SEQ
					INNER JOIN LN20_EDS LN20
						ON LN20.BF_SSN = LN85.BF_SSN
						AND LN20.LN_SEQ = LN85.LN_SEQ
						AND LN20.LC_STA_LON20 = 'A'
						AND LN20.LC_EDS_TYP = 'M'
				WHERE   
					AY10.PF_REQ_ACT = @PF_REQ_ACT
					AND LN20.LF_EDS = @BF_SSN
					AND AY10.LN_ATY_SEQ = @RN_ATY_SEQ_PRC
					--Active flag ignored, as LT20 provides the exact record that is tied to this request
			)LN85
				ON LN85.BF_SSN = LN72.BF_SSN
				AND LN85.LN_SEQ = LN72.LN_SEQ 
		WHERE
			LN72.LD_ITR_EFF_BEG > CAST(GETDATE() AS DATE)
			AND LN72.LC_STA_LON72 = 'A'
			AND LN72.LC_INT_RDC_PGM != 'M'
			AND LN20.LF_EDS = @BF_SSN
	END
END